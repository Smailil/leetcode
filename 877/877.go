package main

/*
Алиса и Боб играют в игру с кучами камней. Есть чётное число куч,
расположенных в ряд, и в каждой куче положительное целое число камней piles[i].

Цель игры — закончить с наибольшим числом камней. Общее число камней во всех кучах нечётное,
поэтому ничьих не бывает.

Алиса и Боб ходят по очереди, первой ходит Алиса. За один ход игрок забирает всю кучу камней
либо с начала, либо с конца ряда. Игра продолжается, пока не останется куч;
после этого выигрывает тот, у кого больше камней.

Если Алиса и Боб играют оптимально, верните true, если Алиса выигрывает игру,
или false, если выигрывает Боб.

Пример 1:
Вход: piles = [5,3,4,5]
Выход: true
Пояснение:
Алиса ходит первой и может взять только первую 5 или последнюю 5.
Предположим, она берёт первую 5, тогда ряд становится [3, 4, 5].
Если Боб берёт 3, то поле становится [4, 5], и Алиса берёт 5,
чтобы выиграть с 10 очками.
Если Боб берёт последнюю 5, то поле становится [3, 4],
и Алиса берёт 4, чтобы выиграть с 9 очками.
Это показывает, что взять первую 5 было выигрышным ходом для Алисы,
поэтому возвращаем true.

Пример 2:
Вход: piles = [3,7,2,3]
Выход: true

Ограничения:
2 <= piles.length <= 500
piles.length — чётное.
1 <= piles[i] <= 500
sum(piles[i]) — нечётная.
*/

// stoneGame вычисляет значение DP по интервалам,
// которое затем трактуется как счёт Алисы.
// Время: O(n²), память: O(n).
func stoneGame(piles []int) bool {
    n := len(piles)

    // dp — одномерная версия DP по интервалам.
    // Для текущей левой границы l ячейка dp[r] хранит
    // лучшее накопленное значение для интервала piles[l:r+1].
    // Сжатие до одного массива возможно, потому что переход
    // использует только dp[r] и dp[r-1].
    dp := make([]int, n)

    // Перебираем все интервалы: l двигается справа налево,
    // а r — от l к концу массива.
    for l := n - 1; l >= 0; l-- {
        for r := l; r < n; r++ {
            // r-l чётно тогда и только тогда,
            // когда длина интервала r-l+1 нечётна.
            // На таком слое DP выбранная крайняя куча
            // добавляется к накопленному значению.
            even := (r-l)%2 == 0
            left, right := 0, 0
            if even {
                left = piles[l]
                right = piles[r]
            }

            if l == r {
                // База: интервал состоит из одной кучи,
                // поэтому её размер и есть начальный счёт.
                dp[r] = left
            } else {
                // Два варианта перехода:
                // 1. dp[r] + left — взять левую кучу и добавить её
                //    к уже рассчитанному интервалу [l+1, r];
                // 2. dp[r-1] + right — взять правую кучу и добавить её
                //    к уже рассчитанному интервалу [l, r-1].
                // Из них сохраняем вариант с большим значением.
                dp[r] = max(dp[r]+left, dp[r-1]+right)
            }
        }
    }

    // Сумма всех камней нужна, чтобы по счёту Алисы
    // получить счёт Боба:
    // bobScore = total - aliceScore.
    total := 0
    for _, pile := range piles {
        total += pile
    }

    // После обработки всего массива итоговое значение DP
    // лежит в dp[n-1].
    aliceScore := dp[n-1]

    // Алиса побеждает, если её счёт больше части общей суммы,
    // оставшейся Бобу.
    return aliceScore > total-aliceScore
}
