package main

/*
Дана строка s с индексацией с нуля и словарь слов dictionary.
Нужно разбить s на одну или несколько непересекающихся подстрок,
каждая из которых присутствует в dictionary.
В строке s могут быть лишние символы, не вошедшие ни в одну из подстрок.

Верните минимальное количество лишних символов при оптимальном разбиении s.

Пример 1:
Вход: s = "leetscode", dictionary = ["leet","code","leetcode"]
Выход: 1
Пояснение: Разбиваем s на две подстроки: "leet" с индекса 0 по 3
           и "code" с индекса 5 по 8. Остаётся 1 неиспользованный символ
           (индекс 4) — возвращаем 1.

Пример 2:
Вход: s = "sayhelloworld", dictionary = ["hello","world"]
Выход: 3
Пояснение: Разбиваем s на две подстроки: "hello" с индекса 3 по 7
           и "world" с индекса 8 по 12. Символы с индексами 0, 1, 2
           не вошли ни в одну подстроку и считаются лишними. Возвращаем 3.

Ограничения:
1 <= s.length <= 50
1 <= dictionary.length <= 50
1 <= dictionary[i].length <= 50
dictionary[i] и s состоят только из строчных букв английского алфавита
dictionary содержит уникальные слова

*/

// TrieNode представляет узел префиксного дерева (Trie).
type TrieNode struct {
    // children хранит ссылки на дочерние узлы для каждой буквы английского алфавита (a-z).
    children [26]*TrieNode
    // isWord указывает, является ли текущий узел концом какого-либо слова из словаря.
    isWord   bool
}

// Trie представляет собой префиксное дерево для эффективного поиска слов по префиксам.
type Trie struct {
    root *TrieNode
}

// newTrie создает и возвращает новое префиксное дерево с инициализированным корневым узлом.
func newTrie() *Trie {
    return &Trie{root: &TrieNode{}}
}

// addWord добавляет слово в префиксное дерево.
func (t *Trie) addWord(word string) {
    curr := t.root
    for _, c := range word {
        idx := c - 'a' // Вычисляем индекс буквы (0 для 'a', 25 для 'z')
        
        // Если путь для текущей буквы еще не создан, создаем новый узел
        if curr.children[idx] == nil {
            curr.children[idx] = &TrieNode{}
        }
        curr = curr.children[idx] // Переходим к следующему узлу
    }
    curr.isWord = true // Помечаем конец добавленного слова
}

// minExtraChar вычисляет минимальное количество лишних символов в строке s,
// если разбить её на слова из dictionary и отдельные (лишние) символы.
func minExtraChar(s string, dictionary []string) int {
    // Инициализируем Trie и добавляем в него все слова из словаря для быстрого поиска
    trie := newTrie()
    for _, word := range dictionary {
        trie.addWord(word)
    }

    // dp[i] будет хранить минимальное количество лишних символов для подстроки s[i:]
    // Инициализируем массив значением -1, что означает "результат еще не вычислен"
    dp := make([]int, len(s)+1)
    for i := range dp {
        dp[i] = -1
    }

    // dfs — это рекурсивная функция с мемоизацией для поиска оптимального разбиения
    var dfs func(i int) int
    dfs = func(i int) int {
        // Базовый случай: если мы дошли до конца строки, лишних символов больше нет
        if i == len(s) {
            return 0
        }
        // Если результат для текущей позиции уже вычислен, возвращаем его из кэша (мемоизация)
        if dp[i] != -1 {
            return dp[i]
        }

        // Вариант 1: Считаем текущий символ s[i] лишним (+1 к результату) и переходим к следующему
        res := 1 + dfs(i+1)
        
        // Вариант 2: Пытаемся найти слово из словаря, начинающееся с индекса i
        curr := trie.root

        for j := i; j < len(s); j++ {
            idx := s[j] - 'a'
            // Если текущего символа нет в Trie, значит дальше совпадений не будет, прерываем цикл
            if curr.children[idx] == nil {
                break
            }
            curr = curr.children[idx] // Спускаемся ниже по дереву
            
            // Если мы нашли слово из словаря (curr.isWord == true), пробуем пропустить его.
            // Сравниваем текущий лучший результат с результатом при пропуске найденного слова.
            if curr.isWord {
                res = min(res, dfs(j+1))
            }
        }

        // Сохраняем вычисленный минимальный результат в мемоизационную таблицу
        dp[i] = res
        return res
    }

    // Запускаем рекурсию с самого начала строки (с индекса 0)
    return dfs(0)
}