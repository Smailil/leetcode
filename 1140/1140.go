package main

import "math"

/*
Алиса и Боб продолжают играть с кучами камней.
Несколько куч расположены в ряд.
В каждой из них находится положительное целое число камней,
равное piles[i].
Цель игры — набрать как можно больше камней.

Алиса и Боб ходят по очереди, первой ходит Алиса.

В свой ход игрок может забрать все камни из первых X оставшихся куч,
где 1 <= X <= 2M.
После этого M = max(M, X).
Изначально M = 1.

Игра продолжается, пока не будут забраны все камни.

Предполагая, что Алиса и Боб играют оптимально, верните наибольшее
количество камней, которое может получить Алиса.

Пример 1:
Вход: piles = [2,7,9,4,4]
Выход: 10
Пояснение:
Если Алиса вначале заберёт одну кучу, Боб заберёт две кучи,
а затем Алиса снова заберёт две кучи.
Всего Алиса получит 2 + 4 + 4 = 10 камней.
Если Алиса вначале заберёт две кучи, Боб сможет забрать
все три оставшиеся кучи.
В этом случае Алиса получит 2 + 7 = 9 камней.
Поэтому мы возвращаем 10, поскольку это большее из двух значений.

Пример 2:
Вход: piles = [1,2,3,4,5,100]
Выход: 104
Ограничения:
1 <= piles.length <= 100
1 <= piles[i] <= 10^4
*/

// stoneGameII возвращает максимальное количество камней, которое Алиса
// сможет собрать при оптимальной игре обоих игроков.
//
// Состояние dp[turn][i][M] хранит ответ для части игры, в которой:
//   - turn == 1 — ход Алисы, поэтому она максимизирует свой результат;
//   - turn == 0 — ход Боба, поэтому он минимизирует результат Алисы;
//   - i — индекс первой ещё не взятой кучи;
//   - M — текущее ограничение, позволяющее взять от 1 до 2*M куч.
//
// Боб может минимизировать результат Алисы, потому что все камни обязательно
// будут разобраны: чем больше камней получит Боб, тем меньше достанется Алисе.
//
// Время: O(n^3), поскольку перебираются i, M и количество взятых куч X.
// Память: O(n^2) для двух слоёв состояний по игроку, i и M.
func stoneGameII(piles []int) int {
    n := len(piles)

    // Значения с i == n остаются нулевыми. Это базовый случай: если куч
    // больше нет, Алиса уже не сможет получить дополнительные камни.
    dp := make([][][]int, 2)
    for a := range 2 {
        dp[a] = make([][]int, n+1)
        for i := range n+1 {
            dp[a][i] = make([]int, n+1)
        }
    }

    // Идём справа налево, чтобы при вычислении состояния с индексом i
    // все состояния для следующего индекса i+X уже были рассчитаны.
    for i := n - 1; i >= 0; i-- {
        for M := 1; M <= n; M++ {
            // total — сумма камней в первых X доступных кучах.
            // При увеличении X добавляем только одну новую кучу.
            total := 0

            // Для хода Алисы ищем максимум, поэтому начинаем с нуля.
            // Для хода Боба ищем минимум будущего результата Алисы,
            // поэтому начинаем с заведомо большого значения.
            dp[1][i][M] = 0
            dp[0][i][M] = math.MaxInt32

            // Текущий игрок может взять X куч, где 1 <= X <= 2*M.
            for X := 1; X <= 2*M; X++ {
                // Нельзя выйти за границу массива, если оставшихся куч
                // меньше, чем выбранное значение X.
                if i+X > n {
                    break
                }
                total += piles[i+X-1]

                // Ход Алисы: она получает total камней прямо сейчас,
                // после чего наступает ход Боба с обновлённым M.
                dp[1][i][M] = max(dp[1][i][M], total+dp[0][i+X][max(M, X)])

                // Ход Боба: взятые им камни не входят в результат Алисы.
                // Боб выбирает X, оставляющее Алисе минимальный результат.
                dp[0][i][M] = min(dp[0][i][M], dp[1][i+X][max(M, X)])
            }
        }
    }

    // В начале ходит Алиса, первая доступная куча имеет индекс 0, а M == 1.
    return dp[1][0][1]
}
