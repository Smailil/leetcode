package main

/*
Всего есть numCourses курсов, которые нужно пройти; они пронумерованы
от 0 до numCourses - 1.
Дан массив prerequisites, где prerequisites[i] = [a_i, b_i] означает,
что нужно сначала пройти курс a_i, если вы хотите пройти курс b_i.

Например, пара [0, 1] означает, что нужно пройти курс 0,
прежде чем можно будет пройти курс 1.
Предварительные требования также могут быть косвенными.
Если курс a является предварительным требованием для курса b,
а курс b является предварительным требованием для курса c,
то курс a является предварительным требованием для курса c.

Также дан массив queries, где queries[j] = [u_j, v_j].
Для j-го запроса нужно ответить, является ли курс u_j
предварительным требованием для курса v_j.

Верните булев массив answer, где answer[j] — ответ на j-й запрос.

Пример 1:
https://assets.leetcode.com/uploads/2021/05/01/courses4-1-graph.jpg
Вход: numCourses = 2, prerequisites = [[1,0]], queries = [[0,1],[1,0]]
Выход: [false,true]
Пояснение: пара [1, 0] означает, что нужно пройти курс 1,
прежде чем можно будет пройти курс 0.
Курс 0 не является предварительным требованием для курса 1,
но обратное утверждение верно.

Пример 2:
Вход: numCourses = 2, prerequisites = [], queries = [[1,0],[0,1]]
Выход: [false,false]
Пояснение: предварительных требований нет, и каждый курс независим.

Пример 3:
https://assets.leetcode.com/uploads/2021/05/01/courses4-3-graph.jpg
Вход: numCourses = 3, prerequisites = [[1,2],[1,0],[2,0]], queries = [[1,0],[1,2]]
Выход: [true,true]

Ограничения:
2 <= numCourses <= 100
0 <= prerequisites.length <= (numCourses * (numCourses - 1) / 2)
prerequisites[i].length == 2
0 <= a_i, b_i <= numCourses - 1
a_i != b_i
Все пары [a_i, b_i] уникальны.
В графе предварительных требований нет циклов.
1 <= queries.length <= 10^4
0 <= u_i, v_i <= numCourses - 1
u_i != v_i

*/

func checkIfPrerequisite(numCourses int, prerequisites [][]int, queries [][]int) []bool {
    // Создаем список смежности для представления графа зависимостей.
    // adj[crs] будет хранить список всех прямых предварительных курсов для курса crs.
    adj := make([][]int, numCourses)
    for i := range adj {
        adj[i] = []int{}
    }
    
    // Заполняем граф. Направление ребер: от курса к его предварительным требованиям.
    for _, pre := range prerequisites {
        prereq, crs := pre[0], pre[1]
        adj[crs] = append(adj[crs], prereq)
    }

    // prereqMap будет хранить множество ВСЕХ (прямых и косвенных) предварительных курсов для каждого курса.
    // Ключ - номер курса, значение - map[int]bool, представляющее set (множество) всех его предков.
    prereqMap := make(map[int]map[int]bool)

    // dfs (обход в глубину) рекурсивно собирает все предварительные курсы для заданного курса.
    var dfs func(crs int) map[int]bool
    dfs = func(crs int) map[int]bool {
        // Если мы еще не вычисляли предков для этого курса, делаем это сейчас.
        if _, exists := prereqMap[crs]; !exists {
            prereqMap[crs] = make(map[int]bool)
            // Для каждого прямого предварительного курса...
            for _, prereq := range adj[crs] {
                // ...рекурсивно получаем всех его предков и добавляем их в наше множество.
                for p := range dfs(prereq) {
                    prereqMap[crs][p] = true
                }
            }
            // Добавляем сам курс в его множество предков (база для рекурсии).
            prereqMap[crs][crs] = true
        }
        // Возвращаем множество всех предков для текущего курса.
        return prereqMap[crs]
    }

    // Запускаем DFS для каждого курса, чтобы заранее вычислить (закешировать) все транзитивные зависимости.
    // Это позволяет в дальнейшем отвечать на любые запросы за время O(1).
    for crs := range numCourses {
        dfs(crs)
    }

    // Формируем массив ответов на запросы.
    res := make([]bool, len(queries))
    for i, q := range queries {
        // Запрос q имеет вид [u, v], где нужно проверить: u - предок v?
        // q[1] - это v (целевой курс), q[0] - это u (возможный предварительный курс).
        // Проверяем наличие u в множестве предков курса v.
        // Если u нет в карте, Go автоматически вернет false (нулевое значение для bool).
        res[i] = prereqMap[q[1]][q[0]]
    }
    
    return res
}