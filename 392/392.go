package main

/*
Даны две строки s и t.
Верните true, если s является подпоследовательностью t,
иначе верните false.

Подпоследовательность строки — это новая строка, полученная из исходной
удалением некоторых символов (возможно, ни одного) без изменения
относительного порядка оставшихся символов.
Например, "ace" является подпоследовательностью "abcde",
а "aec" — не является.

Пример 1:
Ввод: s = "abc", t = "ahbgdc"
Вывод: true

Пример 2:
Ввод: s = "axc", t = "ahbgdc"
Вывод: false

Ограничения:
0 <= s.length <= 100
0 <= t.length <= 10^4
Строки s и t состоят только из строчных английских букв.

Дополнительный вопрос: предположим, поступает много строк s,
например s_1, s_2, ..., s_k, где k >= 10^9,
и нужно по очереди проверять,
является ли каждая из них подпоследовательностью t.
Как бы вы изменили свой код в этом случае?
*/

// isSubsequence сопоставляет символы s с символами t слева направо.
// Время работы — O(len(t)), а дополнительная память — O(1).
func isSubsequence(s string, t string) bool {
    // i указывает на следующий символ s, который нужно найти,
    // а j — на очередной проверяемый символ t.
    i, j := 0, 0

    // Инвариант: s[:i] уже является подпоследовательностью t[:j].
    // Цикл заканчивается, когда вся s сопоставлена или t исчерпана.
    for i < len(s) && j < len(t) {
        // При совпадении переходим к следующему требуемому символу s.
        if s[i] == t[j] {
            i++
        }

        // Указатель по t движется всегда, поэтому порядок символов сохраняется.
        j++
    }

    // Равенство означает, что для каждого символа s нашлась позиция в t.
    // Для пустой s условие также сразу истинно.
    return i == len(s)
}
