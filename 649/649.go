package main

/*
В мире Dota2 есть две партии: Radiant и Dire.

Сенат Dota2 состоит из сенаторов, представляющих эти две партии.
Теперь Сенат хочет принять решение об изменении в игре Dota2.
Голосование по этому изменению проходит по раундам.
В каждом раунде каждый сенатор может воспользоваться одним из двух прав:

Запретить право одного сенатора: сенатор может лишить другого сенатора
всех прав в текущем и во всех последующих раундах.
Объявить победу: если сенатор обнаружил, что все сенаторы,
у которых еще есть право голоса, принадлежат одной партии,
он может объявить победу и принять решение об изменении в игре.

Дана строка senate, представляющая принадлежность каждого сенатора к партии.
Символы 'R' и 'D' обозначают партии Radiant и Dire.
Если сенаторов n, размер данной строки равен n.

Процедура по раундам начинается с первого сенатора
и идет до последнего сенатора в заданном порядке.
Эта процедура продолжается до завершения голосования.
Все сенаторы, потерявшие свои права, пропускаются во время процедуры.

Предположим, что каждый сенатор достаточно умен
и будет использовать лучшую стратегию для своей партии.
Предскажите, какая партия в итоге объявит победу
и изменит игру Dota2.
Ответ должен быть "Radiant" или "Dire".

Пример 1:
Вход: senate = "RD"
Выход: "Radiant"
Пояснение:
Первый сенатор из Radiant может просто запретить право следующего сенатора
в 1-м раунде.
Второй сенатор больше не может пользоваться никакими правами,
поскольку его право было запрещено.
Во 2-м раунде первый сенатор может просто объявить победу,
поскольку он единственный человек в сенате, который может голосовать.

Пример 2:
Вход: senate = "RDD"
Выход: "Dire"
Пояснение:
Первый сенатор из Radiant может просто запретить право следующего сенатора
в 1-м раунде.
Второй сенатор больше не может пользоваться никакими правами,
поскольку его право было запрещено.
Третий сенатор из Dire может запретить право первого сенатора
в 1-м раунде.
Во 2-м раунде третий сенатор может просто объявить победу,
поскольку он единственный человек в сенате, который может голосовать.

Ограничения:
n == senate.length
1 <= n <= 10^4
senate[i] равен либо 'R', либо 'D'.

*/

func predictPartyVictory(senate string) string {
    // R хранит индексы сенаторов партии Radiant (Сияние)
    R := []int{}
    // D хранит индексы сенаторов партии Dire (Ужас)
    D := []int{}
    n := len(senate)

    // Заполняем очереди индексами сенаторов каждой партии
    for i, c := range senate {
        if c == 'R' {
            R = append(R, i) // Добавляем индекс сенатора Radiant
        } else {
            D = append(D, i) // Добавляем индекс сенатора Dire
        }
    }

    // Моделируем процесс голосования
    // Пока есть сенаторы обеих партий
    for len(R) > 0 && len(D) > 0 {
        // Берём первого сенатора из каждой очереди
        rTurn := R[0]
        R = R[1:] // Удаляем его из очереди
        dTurn := D[0]
        D = D[1:] // Удаляем его из очереди

        // Сравниваем позиции сенаторов
        // Побеждает тот, чей индекс меньше (голосует раньше)
        if rTurn < dTurn {
            // Senатор Radiant голосует первым и запрещает голосовать сенатору Dire
            // Он остаётся в игре и голосует в следующем раунде
            // Добавляем его индекс + n (полный круг), чтобы он оказался в конце очереди
            R = append(R, rTurn+n)
        } else {
            // Senатор Dire голосует первым и запрещает голосовать сенатору Radiant
            // Он остаётся в игре и голосует в следующем раунде
            D = append(D, dTurn+n)
        }
    }

    // Проверяем, какая партия осталась
    if len(R) > 0 {
        return "Radiant" // Победила партия Сияния
    }
    return "Dire" // Победила партия Ужаса
}