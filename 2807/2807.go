package main

/*
Дана голова связного списка head, каждый узел которого содержит целое число.

Между каждой парой соседних узлов вставьте новый узел со значением, равным их наибольшему общему делителю.

Верните связный список после вставки новых узлов.

Наибольший общий делитель двух чисел — это наибольшее положительное целое число, на которое оба числа делятся без остатка.

Пример 1:
https://assets.leetcode.com/uploads/2023/07/18/ex1_copy.png
Ввод: head = [18,6,10,3]
Вывод: [18,6,6,2,10,1,3]
Пояснение: на 1-й схеме изображён исходный связный список, а на 2-й — связный список после вставки новых узлов (вставленные узлы выделены синим цветом).
- Между 1-м и 2-м узлами вставляем наибольший общий делитель чисел 18 и 6, равный 6.
- Между 2-м и 3-м узлами вставляем наибольший общий делитель чисел 6 и 10, равный 2.
- Между 3-м и 4-м узлами вставляем наибольший общий делитель чисел 10 и 3, равный 1.
Других соседних узлов нет, поэтому возвращаем связный список.

Пример 2:
https://assets.leetcode.com/uploads/2023/07/18/ex2_copy1.png
Ввод: head = [7]
Вывод: [7]
Пояснение: на 1-й схеме изображён исходный связный список, а на 2-й — связный список после вставки новых узлов.
Пар соседних узлов нет, поэтому возвращаем исходный связный список.

Ограничения:
Количество узлов в списке находится в диапазоне [1, 5000].
1 <= Node.val <= 1000
*/

type ListNode struct {
    Val int
    Next *ListNode
}

// insertGreatestCommonDivisors вставляет ровно один новый узел между каждой
// парой соседних узлов исходного списка. При фиксированной разрядности int
// время работы составляет O(n), создаётся O(n) узлов результата,
// а вспомогательная память занимает O(1).
func insertGreatestCommonDivisors(head *ListNode) *ListNode {
    gcd := func(a, b int) int {
        // Алгоритм Евклида сохраняет НОД при замене пары (a, b)
        // на (b, a % b) и уменьшает второй элемент до нуля.
        for b > 0 {
            a, b = b, a%b
        }

        // Когда остаток стал нулевым, a содержит НОД исходной пары.
        return a
    }

    // Инвариант: cur указывает на левый исходный узел следующей пары.
    // По условию список непуст, поэтому начинать можно непосредственно с head.
    cur := head
    for cur.Next != nil {
        // Сохраняем значения соседей до изменения связей между узлами.
        n1, n2 := cur.Val, cur.Next.Val

        // Новый узел сразу ссылается на прежнего правого соседа,
        // поэтому необработанная часть списка остаётся доступной.
        newNode := &ListNode{Val: gcd(n1, n2), Next: cur.Next}
        cur.Next = newNode

        // Переходим через вставленный узел к исходному правому соседу.
        // Он станет левым узлом следующей пары исходного списка.
        cur = newNode.Next
    }

    return head
}
