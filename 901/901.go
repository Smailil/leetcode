/*
Разработайте алгоритм, который собирает ежедневные котировки акций
и возвращает «охват» цены акции за текущий день.

Охват цены акции за один день — это максимальное количество
последовательных дней (начиная с текущего и двигаясь назад),
в которые цена акции была меньше или равна цене текущего дня.

Например, если цены акции за последние четыре дня равны [7,2,1,2],
а цена сегодня — 2, то охват сегодняшнего дня равен 4,
потому что начиная с сегодняшнего дня цена была меньше или равна 2
на протяжении 4 последовательных дней.
Также, если цены за последние четыре дня равны [7,34,1,2],
а цена сегодня — 8, то охват равен 3,
потому что начиная с сегодняшнего дня цена была меньше или равна 8
на протяжении 3 последовательных дней.
Реализуйте класс StockSpanner:

StockSpanner() Инициализирует объект класса.
int next(int price) Возвращает охват цены акции, если цена сегодня равна price.


Пример 1:
Вход
["StockSpanner", "next", "next", "next", "next", "next", "next", "next"]
[[], [100], [80], [60], [70], [60], [75], [85]]
Выход
[null, 1, 1, 1, 2, 1, 4, 6]

Пояснение
StockSpanner stockSpanner = new StockSpanner();
stockSpanner.next(100); // вернуть 1
stockSpanner.next(80);  // вернуть 1
stockSpanner.next(60);  // вернуть 1
stockSpanner.next(70);  // вернуть 2
stockSpanner.next(60);  // вернуть 1
stockSpanner.next(75);  // вернуть 4, потому что последние 4 цены (включая сегодняшнюю цену 75) были меньше или равны сегодняшней цене.
stockSpanner.next(85);  // вернуть 6


Ограничения:
1 <= price <= 10^5
Метод next будет вызван не более 10^4 раз.

*/

// StockSpanner — структура для вычисления "спэна" (span) цены акции.
// Спэн — это количество последовательных дней (включая текущий), 
// в течение которых цена акции была меньше или равна текущей цене.
type StockSpanner struct {
    // stack хранит пары [цена, спэн].
    // Стек поддерживается в порядке убывания цен (монотонно убывающий стек).
    // Это позволяет эффективно "схлопывать" предыдущие дни при обработке новой цены.
    stack [][]int // [price, span]
}

func Constructor() StockSpanner {
    return StockSpanner{stack: [][]int{}}
}

// Next обрабатывает новую цену акции и возвращает её спэн.
// Алгоритм:
// 1. Начинаем со спэна = 1 (текущий день).
// 2. Пока стек не пуст и цена на вершине стека <= текущей цены:
//    - Добавляем спэн вершины к текущему спэну (т.к. все эти дни "поглощаются" текущей ценой).
//    - Удаляем вершину стека (она больше не нужна для будущих вычислений).
// 3. Добавляем в стек пару [текущая цена, вычисленный спэн].
// 4. Возвращаем спэн.
func (this *StockSpanner) Next(price int) int {
    span := 1 // Текущий день всегда учитывается
    
    // "Схлопываем" все предыдущие дни с ценой <= текущей.
    // Их спэны суммируются, т.к. они образуют непрерывный блок, "покрытый" текущей ценой.
    for len(this.stack) > 0 && this.stack[len(this.stack)-1][0] <= price {
        span += this.stack[len(this.stack)-1][1] // Добавляем спэн "поглощённого" дня
        this.stack = this.stack[:len(this.stack)-1] // Удаляем вершину стека (pop)
    }
    
    // Сохраняем текущую цену и её спэн в стек для будущих вычислений.
    // Эта запись может быть использована при обработке более высоких цен в будущем.
    this.stack = append(this.stack, []int{price, span})
    
    return span
}

