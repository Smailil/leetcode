package main

/*
Дерево — это неориентированный граф, в котором любые две вершины
соединены ровно одним путем. Иначе говоря, любой связный граф
без простых циклов является деревом.

Дано дерево из n узлов, помеченных от 0 до n - 1,
и массив из n - 1 ребер, где edges[i] = [a_i, b_i] означает,
что между узлами a_i и b_i есть неориентированное ребро.
В качестве корня можно выбрать любой узел дерева. Когда узел x выбран
корнем, получившееся дерево имеет высоту h. Среди всех возможных
укорененных деревьев деревья с минимальной высотой (то есть min(h))
называются деревьями минимальной высоты (MHT).

Верните список меток корней всех MHT. Ответ можно вернуть в любом порядке.

Высота укорененного дерева — это количество ребер на самом длинном пути
вниз от корня до листа.

Пример 1:
https://assets.leetcode.com/uploads/2020/09/01/e1.jpg
Вход: n = 4, edges = [[1,0],[1,2],[1,3]]
Выход: [1]
Пояснение: Как показано на рисунке, высота дерева равна 1,
если корнем является узел с меткой 1; это единственное MHT.
Пример 2:
https://assets.leetcode.com/uploads/2020/09/01/e2.jpg
Вход: n = 6, edges = [[3,0],[3,1],[3,2],[3,4],[5,4]]
Выход: [3,4]

Ограничения:
1 <= n <= 2 * 10^4
edges.length == n - 1
0 <= a_i, b_i < n
a_i != b_i
Все пары (a_i, b_i) различны.
Гарантируется, что входные данные задают дерево,
и повторяющихся ребер не будет.

*/

// Алгоритм работает по принципу "очистки лука" (топологическая сортировка с удалением листьев).
// В любом дереве может быть максимум 2 центроида (корня минимальной высоты).
func findMinHeightTrees(n int, edges [][]int) []int {
    // Базовый случай: если в графе только одна вершина, она и является единственным корнем.
    if n == 1 {
        return []int{0}
    }

    // Создаем список смежности для представления неориентированного графа.
    adj := make([][]int, n)
    for i := range adj {
        adj[i] = []int{}
    }
    // Заполняем список смежности. Ребро добавляется в обе стороны.
    for _, edge := range edges {
        adj[edge[0]] = append(adj[edge[0]], edge[1])
        adj[edge[1]] = append(adj[edge[1]], edge[0])
    }

    // edgeCnt хранит степень (количество связей) каждой вершины.
    // leaves хранит текущие листья (вершины со степенью 1).
    edgeCnt := make([]int, n)
    leaves := []int{}
    for i := range n {
        edgeCnt[i] = len(adj[i])
        // Если у вершины только одна связь, она является листом.
        if edgeCnt[i] == 1 {
            leaves = append(leaves, i)
        }
    }

    // Итеративно удаляем листья слой за слоем, пока не останется 1 или 2 вершины.
    for len(leaves) > 0 {
        // Если осталось 2 или меньше вершин, они и будут искомыми корнями.
        if n <= 2 {
            return leaves
        }
        
        // Запоминаем количество листьев на текущем слое, чтобы не обрабатывать 
        // новые листья, которые появятся в процессе этого же цикла.
        size := len(leaves)
        
        // Обрабатываем все листья текущего слоя.
        for range size {
            node := leaves[0]   // Берем первый лист из очереди.
            leaves = leaves[1:] // Удаляем его из очереди листьев.
            n--                 // Уменьшаем общее количество оставшихся вершин в графе.
            
            // "Отрываем" текущий лист от всех его соседей.
            for _, nei := range adj[node] {
                edgeCnt[nei]-- // Уменьшаем степень соседа.
                // Если степень соседа стала равна 1, он сам превратился в новый лист 
                // и будет добавлен в очередь для обработки на следующем слое.
                if edgeCnt[nei] == 1 {
                    leaves = append(leaves, nei)
                }
            }
        }
    }

    // Недостижимая строка для корректных входных данных (связного дерева), 
    // но требуется для успешной компиляции функции.
    return []int{}
}
