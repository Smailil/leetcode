package main

/*
Дан массив различных целых чисел nums и целое число target.
Верните количество возможных комбинаций, сумма которых равна target.

Тесты сгенерированы так, что ответ помещается в 32-битное целое число.

Пример 1:
Вход: nums = [1,2,3], target = 4
Выход: 7
Пояснение:
Возможные способы составить комбинации:
(1, 1, 1, 1)
(1, 1, 2)
(1, 2, 1)
(1, 3)
(2, 1, 1)
(2, 2)
(3, 1)
Обратите внимание, что разные последовательности считаются разными комбинациями.

Пример 2:
Вход: nums = [9], target = 3
Выход: 0

Ограничения:
1 <= nums.length <= 200
1 <= nums[i] <= 1000
Все элементы nums уникальны.
1 <= target <= 1000

*/

func combinationSum4(nums []int, target int) int {
    // dp — это карта, где ключ — это сумма, а значение — количество
    // способов получить эту сумму из элементов массива nums.
    dp := make(map[int]int)
    
    // Базовый случай: сумму 0 можно получить одним способом —
    // не взяв ни одного элемента (пустая комбинация).
    dp[0] = 1

    // Внешний цикл: последовательно вычисляем количество способов
    // получить каждую сумму от 1 до target.
    for total := 1; total <= target; total++ {
        dp[total] = 0
        
        // Внутренний цикл: перебираем все доступные числа.
        for _, num := range nums {
            // Проверяем, существует ли уже вычисленное количество способов
            // получить сумму (total - num).
            // Если существует, значит, мы можем добавить текущее число num
            // ко всем тем комбинациям, чтобы получить сумму total.
            if val, ok := dp[total-num]; ok {
                dp[total] += val
            }
        }
    }

    // Возвращаем количество способов получить искомую сумму target.
    return dp[target]
}