package main

/*
Дан массив целых чисел nums. Отсортируйте массив по возрастанию и верните его.

Задачу необходимо решить без использования встроенных функций,
с временной сложностью O(n log n) и минимально возможной пространственной сложностью.

Пример 1:
Вход: nums = [5,2,3,1]
Выход: [1,2,3,5]
Пояснение: После сортировки позиции некоторых чисел не изменились (например, 2 и 3),
    тогда как позиции других — изменились (например, 1 и 5).

Пример 2:
Вход: nums = [5,1,1,2,0,0]
Выход: [0,0,1,1,2,5]
Пояснение: Обратите внимание, что значения в nums не обязательно уникальны.

Ограничения:
1 <= nums.length <= 5 * 10^4
-5 * 10^4 <= nums[i] <= 5 * 10^4

*/

// sortArray сортирует переданный срез целых чисел с помощью алгоритма сортировки слиянием (Merge Sort).
// Возвращает отсортированный срез. Изменяет исходный срез на месте.
// Временная сложность: O(n log n), пространственная: O(n).
func sortArray(nums []int) []int {
    // Запускаем рекурсивную сортировку для всего диапазона индексов
    mergeSort(nums, 0, len(nums)-1)
    return nums
}

// mergeSort рекурсивно разделяет массив на подмассивы до базового случая (1 элемент),
// после чего вызывает merge для последовательного слияния отсортированных частей.
// arr - сортируемый срез, l - левая граница текущего диапазона, r - правая граница.
func mergeSort(arr []int, l, r int) {
    // Базовый случай: подмассив из 0 или 1 элемента уже считается отсортированным
    if l >= r {
        return
    }

    // Находим середину текущего диапазона для разделения
    m := (l + r) / 2

    // Рекурсивно сортируем левую половину (от l до m)
    mergeSort(arr, l, m)
    // Рекурсивно сортируем правую половину (от m+1 до r)
    mergeSort(arr, m+1, r)

    // Сливаем две отсортированные половины в один упорядоченный диапазон
    merge(arr, l, m, r)
}

// merge объединяет два соседних отсортированных подмассива: arr[l..m] и arr[m+1..r]
// в один отсортированный подмассив arr[l..r]. Использует временные срезы для безопасного слияния.
func merge(arr []int, l, m, r int) {
    // Создаем временные срезы для левой и правой частей
    // Размер левой части: от l до m включительно => m - l + 1 элементов
    left := make([]int, m-l+1)
    // Размер правой части: от m+1 до r включительно => r - m элементов
    right := make([]int, r-m)

    // Копируем данные из исходного массива во временные срезы
    copy(left, arr[l:m+1])
    copy(right, arr[m+1:r+1])

    // i - индекс для left, j - индекс для right, k - индекс для перезаписи в arr
    i, j, k := 0, 0, l

    // Основной цикл слияния: сравниваем элементы из обеих частей и записываем меньший
    for i < len(left) && j < len(right) {
        if left[i] <= right[j] {
            arr[k] = left[i]
            i++
        } else {
            arr[k] = right[j]
            j++
        }
        k++
    }

    // Если в левой части остались элементы, копируем их в конец результирующего диапазона
    for i < len(left) {
        arr[k] = left[i]
        i++
        k++
    }

    // Если в правой части остались элементы, копируем их в конец результирующего диапазона
    for j < len(right) {
        arr[k] = right[j]
        j++
        k++
    }
}
