package main

/*
(Это задача с интерактивным вводом.)

Массив arr называется горным массивом тогда и только тогда, когда:

arr.length >= 3
Существует такой индекс i, что 0 < i < arr.length - 1, и при этом:
arr[0] < arr[1] < ... < arr[i - 1] < arr[i]
arr[i] > arr[i + 1] > ... > arr[arr.length - 1]
Дан горный массив mountainArr. Верните минимальный индекс,
при котором mountainArr.get(index) == target.
Если такого индекса не существует, верните -1.

Вы не можете обращаться к горному массиву напрямую.
Работа с массивом возможна только через интерфейс MountainArray:

MountainArray.get(k) — возвращает элемент массива по индексу k (индексация с 0).
MountainArray.length() — возвращает длину массива.
Решения, в которых MountainArray.get вызывается более 100 раз,
будут засчитаны как неверные.
Попытки обойти проверяющую систему влекут дисквалификацию.

Пример 1:

Вход: mountainArr = [1,2,3,4,5,3,1], target = 3
Выход: 2
Пояснение: 3 присутствует в массиве по индексам 2 и 5. Верните минимальный индекс — 2.

Пример 2:

Вход: mountainArr = [0,1,2,4,2,1], target = 3
Выход: -1
Пояснение: 3 не присутствует в массиве, поэтому возвращаем -1.

Ограничения:

3 <= mountainArr.length() <= 10^4
0 <= target <= 10^9
0 <= mountainArr.get(index) <= 10^9

*/

/**
 * // Это интерфейс API для MountainArray.
 * // Реализовывать его не нужно — не стройте догадок о его реализации.
 * type MountainArray struct {
 * }
 *
 * func (this *MountainArray) get(index int) int {}
 * func (this *MountainArray) length() int {}
 */

func findInMountainArray(target int, mountainArr *MountainArray) int {
    length := mountainArr.length()

    // === ШАГ 1: Поиск пика горы с помощью бинарного поиска ===
    // Пик — это элемент, который больше обоих соседей
    // Начинаем с индексов 1 и length-2, чтобы гарантированно иметь доступ к m-1 и m+1
    l, r, peak := 1, length-2, 0
    for l <= r {
        m := (l + r) / 2  // Вычисляем средний индекс
        
        // Получаем значения текущего элемента и его соседей
        left := mountainArr.get(m - 1)
        mid := mountainArr.get(m)
        right := mountainArr.get(m + 1)
        
        // Случай 1: восходящий склон (left < mid < right)
        // Пик находится правее, сдвигаем левую границу
        if left < mid && mid < right {
            l = m + 1
        // Случай 2: нисходящий склон (left > mid > right)
        // Пик находится левее, сдвигаем правую границу
        } else if left > mid && mid > right {
            r = m - 1
        // Случай 3: нашли пик (mid больше обоих соседей)
        } else {
            peak = m
            break
        }
    }

    // === ШАГ 2: Бинарный поиск в левой (возрастающей) части ===
    // Ищем target в диапазоне [0, peak-1]
    l, r = 0, peak-1
    for l <= r {
        m := (l + r) / 2
        val := mountainArr.get(m)
        
        if val < target {
            // Искомое значение больше текущего — ищем правее
            l = m + 1
        } else if val > target {
            // Искомое значение меньше текущего — ищем левее
            r = m - 1
        } else {
            // Нашли target, возвращаем индекс
            return m
        }
    }

    // === ШАГ 3: Бинарный поиск в правой (убывающей) части ===
    // Ищем target в диапазоне [peak, length-1]
    // ВАЖНО: логика сравнений инвертирована из-за убывающего порядка
    l, r = peak, length-1
    for l <= r {
        m := (l + r) / 2
        val := mountainArr.get(m)
        
        if val > target {
            // В убывающей части: если текущее значение больше target,
            // значит target находится правее (в меньших значениях)
            l = m + 1
        } else if val < target {
            // Если текущее значение меньше target,
            // значит target находится левее (в больших значениях)
            r = m - 1
        } else {
            // Нашли target, возвращаем индекс
            return m
        }
    }

    // Если target не найден ни в одной из частей, возвращаем -1
    return -1
}