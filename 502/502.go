package main

/*
Предположим, LeetCode скоро начнет IPO.
Чтобы выгодно продать свои акции венчурным инвесторам,
LeetCode хочет выполнить несколько проектов и увеличить капитал перед IPO.
Поскольку ресурсы ограничены, до IPO можно завершить не более k различных проектов.
Помогите LeetCode выбрать лучший способ максимизировать общий капитал
после завершения не более k различных проектов.

Даны n проектов: i-й проект приносит чистую прибыль profits[i],
а для его запуска нужен минимальный капитал capital[i].

Изначально у вас есть капитал w.
Когда вы завершаете проект, вы получаете его чистую прибыль,
и эта прибыль добавляется к вашему общему капиталу.

Выберите список из не более чем k различных проектов среди данных,
чтобы максимизировать итоговый капитал,
и верните максимальный итоговый капитал.

Гарантируется, что ответ помещается в 32-битное знаковое целое число.

Пример 1:
Вход: k = 2, w = 0, profits = [1,2,3], capital = [0,1,1]
Выход: 4
Пояснение: поскольку начальный капитал равен 0,
вы можете начать только проект с индексом 0.
После его завершения вы получите прибыль 1,
и ваш капитал станет равен 1.
С капиталом 1 можно начать либо проект с индексом 1,
либо проект с индексом 2.
Так как можно выбрать не более 2 проектов,
нужно завершить проект с индексом 2, чтобы получить максимальный капитал.
Поэтому итоговый максимальный капитал равен 0 + 1 + 3 = 4.

Пример 2:
Вход: k = 3, w = 0, profits = [1,2,3], capital = [0,1,2]
Выход: 6

Ограничения:
1 <= k <= 10^5
0 <= w <= 10^9
n == profits.length
n == capital.length
1 <= n <= 10^5
0 <= profits[i] <= 10^4
0 <= capital[i] <= 10^9

*/

import (
    "container/heap"
)

// MinCapitalHeap — куча с минимумом по стартовому капиталу.
// Каждый элемент — это [capital, profit] (минимальный капитал, требуемый для проекта, и его прибыль).
// Используется для быстрого выбора всех проектов, которые мы можем себе позволить с текущим капиталом w.
type MinCapitalHeap [][]int
func (h MinCapitalHeap) Len() int           { return len(h) }
func (h MinCapitalHeap) Less(i, j int) bool { return h[i][0] < h[j][0] }
func (h MinCapitalHeap) Swap(i, j int)      { h[i], h[j] = h[j], h[i] }
func (h *MinCapitalHeap) Push(x any)        { *h = append(*h, x.([]int)) }
func (h *MinCapitalHeap) Pop() any {
    old := *h
    n := len(old)
    x := old[n-1]
    *h = old[0 : n-1]
    return x
}

// MaxProfitHeap — куча с максимумом по прибыли.
// Хранит только прибыли доступных нам проектов (те, что требуют капитала <= текущего w).
// На каждом шаге мы берём отсюда самый выгодный проект.
type MaxProfitHeap []int
func (h MaxProfitHeap) Len() int           { return len(h) }
func (h MaxProfitHeap) Less(i, j int) bool { return h[i] > h[j] } // инвертированное сравнение → max-heap
func (h MaxProfitHeap) Swap(i, j int)      { h[i], h[j] = h[j], h[i] }
func (h *MaxProfitHeap) Push(x any)        { *h = append(*h, x.(int)) }
func (h *MaxProfitHeap) Pop() any {
    old := *h
    n := len(old)
    x := old[n-1]
    *h = old[0 : n-1]
    return x
}

// Идея (жадный алгоритм):
//   1. Кладём все проекты в min-кучу по требуемому капиталу.
//   2. На каждом из k шагов перекладываем в max-кучу по прибыли все проекты,
//      которые мы можем себе позволить (требуемый капитал <= w).
//   3. Берём из max-кучи проект с максимальной прибылью — это жадный выбор,
//      дающий наибольший прирост капитала на данном шаге.
//   4. Увеличиваем капитал w на полученную прибыль и повторяем.
func findMaximizedCapital(k int, w int, profits []int, capital []int) int {
    minCapital := &MinCapitalHeap{}
    maxProfit := &MaxProfitHeap{}

    // Заполняем min-кучу парами [требуемый капитал, прибыль].
    for i := range capital {
        heap.Push(minCapital, []int{capital[i], profits[i]})
    }

    // Выполняем не более k проектов.
    for range k {
        // Перекладываем из minCapitalHeap в maxProfitHeap все проекты,
        // которые мы теперь можем себе позволить с текущим капиталом w.
        // Благодаря min-куче мы гарантированно перебираем их в порядке возрастания требуемого капитала.
        for minCapital.Len() > 0 && (*minCapital)[0][0] <= w {
            item := heap.Pop(minCapital).([]int)
            heap.Push(maxProfit, item[1]) // кладём только прибыль
        }

        // Если доступных проектов нет — дальнейшее увеличение капитала невозможно.
        if maxProfit.Len() == 0 {
            break
        }

        // Жадно выбираем самый прибыльный проект и добавляем его прибыль к нашему капиталу.
        w += heap.Pop(maxProfit).(int)
    }

    return w
}