package main

import "sort"

/*
Дан взвешенный неориентированный связный граф с n вершинами,
пронумерованными от 0 до n - 1, и массив edges,
где edges[i] = [a_i, b_i, weight_i] представляет двунаправленное
взвешенное ребро между вершинами a_i и b_i.
Минимальное остовное дерево (MST) - это подмножество ребер графа,
которое соединяет все вершины без циклов
и имеет минимально возможный суммарный вес.

Найдите все критические и псевдокритические ребра
в минимальном остовном дереве (MST) данного графа.
Ребро MST, при удалении которого из графа вес MST увеличивается,
называется критическим ребром.
Псевдокритическим называется ребро, которое может входить
в некоторые MST, но не во все.

Обратите внимание, что индексы ребер можно вернуть в любом порядке.

Пример 1:
https://assets.leetcode.com/uploads/2020/06/04/ex1.png
Вход: n = 5, edges = [[0,1,1],[1,2,1],[2,3,2],[0,3,2],[0,4,3],[3,4,3],[1,4,6]]
Выход: [[0,1],[2,3,4,5]]
Пояснение: На рисунке выше показан граф.
Следующий рисунок показывает все возможные MST:
https://assets.leetcode.com/uploads/2020/06/04/msts.png
Заметьте, что два ребра 0 и 1 входят во все MST,
поэтому они являются критическими ребрами,
и мы возвращаем их в первом списке ответа.
Ребра 2, 3, 4 и 5 входят только в некоторые MST,
поэтому они считаются псевдокритическими ребрами.
Мы добавляем их во второй список ответа.

Пример 2:
https://assets.leetcode.com/uploads/2020/06/04/ex2.png
Вход: n = 4, edges = [[0,1,1],[1,2,1],[2,3,1],[0,3,1]]
Выход: [[],[0,1,2,3]]
Пояснение: Можно заметить, что, так как все 4 ребра имеют одинаковый вес,
выбор любых 3 ребер из данных 4 даст MST.
Поэтому все 4 ребра являются псевдокритическими.

Ограничения:
2 <= n <= 100
1 <= edges.length <= min(200, n * (n - 1) / 2)
edges[i].length == 3
0 <= a_i < b_i < n
1 <= weight_i <= 1000
Все пары (a_i, b_i) различны.
*/

type UnionFind struct {
    par  []int // Массив родителей: par[i] хранит родителя элемента i
    rank []int // Массив размеров компонентов (хранит именно размер, а не глубину, для оптимизации объединения)
}

// NewUnionFind создает и инициализирует СНМ для n элементов.
// Изначально каждый элемент является родителем самому себе и имеет размер 1.
func NewUnionFind(n int) *UnionFind {
    par := make([]int, n)
    rank := make([]int, n)
    for i := range n {
        par[i] = i
        rank[i] = 1
    }
    return &UnionFind{par: par, rank: rank}
}

// Find находит корень (представителя) множества, которому принадлежит элемент v.
// Использует эвристику сжатия путей для ускорения последующих запросов.
func (uf *UnionFind) Find(v int) int {
    if uf.par[v] != v {
        uf.par[v] = uf.Find(uf.par[v]) // Сжатие пути: переподвешиваем v напрямую к корню
    }
    return uf.par[v]
}

// Union объединяет множества, содержащие элементы v1 и v2.
// Использует эвристику объединения по размеру: меньшее дерево подвешивается к корню большего.
// Возвращает true, если элементы были в разных множествах и объединение произошло.
// Возвращает false, если элементы уже находятся в одном множестве (образовался бы цикл).
func (uf *UnionFind) Union(v1, v2 int) bool {
    p1, p2 := uf.Find(v1), uf.Find(v2) // Находим корни обоих элементов
    if p1 == p2 {
        return false // Уже в одном множестве
    }
    
    // Объединяем по размеру (дерево с меньшим размером подвешиваем к дереву с большим)
    if uf.rank[p1] > uf.rank[p2] {
        uf.par[p2] = p1
        uf.rank[p1] += uf.rank[p2]
    } else {
        uf.par[p1] = p2
        uf.rank[p2] += uf.rank[p1]
    }
    return true
}

// MaxRank возвращает максимальный размер компоненты связности.
// Если он равен n, значит все вершины графа соединены в единую компоненту (граф связен).
func (uf *UnionFind) MaxRank() int {
    maxR := 0
    for _, r := range uf.rank {
        if r > maxR {
            maxR = r
        }
    }
    return maxR
}

// findCriticalAndPseudoCriticalEdges находит критические и псевдокритические ребра в минимальном остовном дереве (МОД).
// - Критическое ребро: присутствует во ВСЕХ возможных МОД. Его удаление увеличивает вес МОД или делает граф несвязным.
// - Псевдокритическое ребро: присутствует в НЕКОТОРЫХ (но не во всех) МОД. Его принудительное включение позволяет построить МОД того же минимального веса.
func findCriticalAndPseudoCriticalEdges(n int, edges [][]int) [][]int {
    // Создаем список ребер, добавляя их оригинальный индекс, 
    // чтобы мы могли идентифицировать их после сортировки.
    // Формат: [вершина1, вершина2, вес, оригинальный_индекс]
    edgeList := make([][4]int, len(edges))
    for i, e := range edges {
        edgeList[i] = [4]int{e[0], e[1], e[2], i}
    }

    // Сортируем ребра по весу по возрастанию (подготовка для алгоритма Краскала)
    sort.Slice(edgeList, func(i, j int) bool {
        return edgeList[i][2] < edgeList[j][2]
    })

    // === Шаг 1: Вычисляем базовый вес минимального остовного дерева (МОД) ===
    mstWeight := 0
    uf := NewUnionFind(n)
    for _, edge := range edgeList {
        if uf.Union(edge[0], edge[1]) {
            mstWeight += edge[2]
        }
    }

    var critical, pseudo []int
    
    // === Шаг 2: Проверяем каждое ребро на принадлежность к критическим или псевдокритическим ===
    for _, edge := range edgeList {
        
        // --- Проверка на критическое ребро ---
        // Пробуем построить МОД БЕЗ текущего ребра.
        ufWithout := NewUnionFind(n)
        weight := 0
        for _, other := range edgeList {
            if other[3] != edge[3] && ufWithout.Union(other[0], other[1]) {
                weight += other[2]
            }
        }
        
        // Если граф стал несвязным (максимальный размер компоненты != n) 
        // ИЛИ вес нового МОД стал больше базового, значит ребро критическое.
        if ufWithout.MaxRank() != n || weight > mstWeight {
            critical = append(critical, edge[3])
            continue // Критическое ребро не может быть псевдокритическим, переходим к следующему
        }

        // --- Проверка на псевдокритическое ребро ---
        // Принудительно ВКЛЮЧАЕМ текущее ребро в МОД и пытаемся достроить остов.
        ufWith := NewUnionFind(n)
        ufWith.Union(edge[0], edge[1]) // Принудительно соединяем концы текущего ребра
        weight = edge[2] // Начинаем суммирование с веса этого ребра
        
        // Добавляем остальные ребра по алгоритму Краскала
        for _, other := range edgeList {
            if ufWith.Union(other[0], other[1]) {
                weight += other[2]
            }
        }
        
        // Если при принудительном включении ребра мы получили МОД с тем же минимальным весом,
        // значит оно может входить в какое-то МОД (но не во все, так как оно не прошло проверку на критичность).
        if weight == mstWeight {
            pseudo = append(pseudo, edge[3])
        }
    }

    // Возвращаем списки оригинальных индексов критических и псевдокритических ребер
    return [][]int{critical, pseudo}
}