package main

/*
Дан массив целых чисел nums и целое число k. Разбей nums на k непустых подмассивов
так, чтобы наибольшая сумма любого подмассива была минимально возможной.

Верни эту минимизированную наибольшую сумму разбиения.

Подмассив — это непрерывная часть массива.

Пример 1:
Вход: nums = [7,2,5,10,8], k = 2
Выход: 18
Пояснение: Существует четыре способа разбить nums на два подмассива.
Лучший вариант — разбить на [7,2,5] и [10,8],
где наибольшая сумма среди двух подмассивов равна всего 18.

Пример 2:
Вход: nums = [1,2,3,4,5], k = 2
Выход: 9
Пояснение: Существует четыре способа разбить nums на два подмассива.
Лучший вариант — разбить на [1,2,3] и [4,5],
где наибольшая сумма среди двух подмассивов равна всего 9.

Ограничения:
1 <= nums.length <= 1000
0 <= nums[i] <= 10^6
1 <= k <= min(50, nums.length)
*/

func splitArray(nums []int, k int) int {
	// canSplit проверяет, можно ли разбить массив на ≤ k подмассивов,
	// где сумма каждого подмассива не превышает largest.
	canSplit := func(largest int) bool {
		subarray := 1   // счётчик используемых подмассивов (минимум 1)
		curSum := 0     // текущая сумма формируемого подмассива

		for _, num := range nums {
			curSum += num
			// Если сумма текущего подмассива превысила допустимый лимит,
			// вынуждены начать новый подмассив
			if curSum > largest {
				subarray++
				// Если потребовалось больше k подмассивов, лимит слишком мал
				if subarray > k {
					return false
				}
				curSum = num // новый подмассив начинается с текущего элемента
			}
		}
		return true
	}

	// Определяем границы бинарного поиска по возможным значениям ответа:
	// l (нижняя граница) = максимальный отдельный элемент.
	//    Ответ не может быть меньше него, т.к. каждый подмассив обязан содержать ≥1 элемент.
	// r (верхняя граница) = сумма всех элементов массива (случай k=1).
	l, r := 0, 0
	for _, num := range nums {
		if num > l {
			l = num
		}
		r += num
	}
	res := r // начальное значение результата (худший возможный случай)

	// Классический бинарный поиск по отрезку [l, r]
	for l <= r {
		mid := l + (r-l)/2 // безопасное вычисление середины без переполнения

		if canSplit(mid) {
			// Если разбивка возможна с лимитом mid, запоминаем как потенциальный ответ
			// и пытаемся найти ещё меньший допустимый лимит
			res = mid
			r = mid - 1
		} else {
			// Если лимит mid слишком мал (требуется > k подмассивов),
			// увеличиваем нижнюю границу
			l = mid + 1
		}
	}

	return res
}
