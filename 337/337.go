package main

/*
Вор снова нашёл себе новое место для грабежа.
В эту область есть только один вход — корень (root).

Кроме корня, у каждого дома ровно один родительский дом.
Осмотревшись, умный вор понял, что все дома образуют бинарное дерево.
Система автоматически вызовет полицию, если два непосредственно связанных
дома будут ограблены в одну ночь.

Дан корень бинарного дерева.
Вернуть максимальную сумму денег, которую вор может украсть, не подняв тревогу.

Пример 1:
https://assets.leetcode.com/uploads/2021/03/10/rob1-tree.jpg
Вход: root = [3,2,3,null,3,null,1]
Выход: 7
Пояснение: Максимальная сумма = 3 + 3 + 1 = 7.

Пример 2:
https://assets.leetcode.com/uploads/2021/03/10/rob2-tree.jpg
Вход: root = [3,4,5,1,3,null,1]
Выход: 9
Пояснение: Максимальная сумма = 4 + 5 = 9.

Ограничения:
Количество узлов в дереве находится в диапазоне [1, 10^4].
0 <= Node.val <= 10^4
*/

/**
 * Определение узла бинарного дерева.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */

// rob — главная функция для решения задачи.
// Задача: найти максимальную сумму, которую можно украсть из бинарного дерева,
// при условии, что нельзя грабить два напрямую связанных узла (родителя и ребенка).
func rob(root *TreeNode) int {
    // Запускаем рекурсивный обход дерева.
    result := dfs(root)
    
    // Функция dfs возвращает массив из двух чисел:
    // result[0] — макс. сумма, если мы ГРАБИМ корень дерева.
    // result[1] — макс. сумма, если мы НЕ ГРАБИМ корень дерева.
    // Итоговый ответ — максимум из этих двух сценариев для самого корня.
    return max(result[0], result[1])
}

// dfs выполняет post-order обход (снизу вверх) и для каждого узла возвращает пару значений:
// [0] (withRoot): максимальная прибыль, если ТЕКУЩИЙ узел ограблен.
// [1] (withoutRoot): максимальная прибыль, если ТЕКУЩИЙ узел НЕ ограблен.
func dfs(root *TreeNode) [2]int {
    // Базовый случай: если узел не существует, прибыль равна 0 в обоих сценариях.
    if root == nil {
        return [2]int{0, 0}
    }

    // Сначала рекурсивно спускаемся до листьев и получаем результаты для левого и правого поддеревьев.
    leftPair := dfs(root.Left)
    rightPair := dfs(root.Right)

    // Сценарий 1: Мы ГРАБИМ текущий узел (withRoot).
    // По условию задачи, мы не можем ограбить его прямых потомков (детей).
    // Поэтому к значению текущего узла мы прибавляем прибыль от детей, 
    // при условии, что сами дети НЕ были ограблены (берем значение по индексу 1).
    withRoot := root.Val + leftPair[1] + rightPair[1]
    
    // Сценарий 2: Мы НЕ ГРАБИМ текущий узел (withoutRoot).
    // В этом случае ограничений на детей нет. Для каждого ребенка мы можем выбрать 
    // оптимальный вариант: ограбить его или нет. Берем максимум для левого и правого поддеревьев.
    withoutRoot := max(leftPair[0], leftPair[1]) + max(rightPair[0], rightPair[1])

    // Возвращаем вычисленную пару значений на уровень выше (родителю).
    return [2]int{withRoot, withoutRoot}
}
