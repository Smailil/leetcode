package main

/*
Дано целое число n. Разбейте его на сумму k положительных целых чисел,
где k >= 2, и максимизируйте произведение этих чисел.

Верните максимальное произведение, которое можно получить.

Пример 1:
Вход: n = 2
Выход: 1
Пояснение: 2 = 1 + 1, 1 × 1 = 1.

Пример 2:
Вход: n = 10
Выход: 36
Пояснение: 10 = 3 + 3 + 4, 3 × 3 × 4 = 36.

Ограничения:
2 <= n <= 58
*/

import "math"

func integerBreak(n int) int {
    // Базовые случаи для малых чисел.
    // По условию задачи число `n` нужно разбить как минимум на 2 положительных целых числа.
    // Для n = 2 лучшее разбиение: 1 + 1, произведение = 1 * 1 = 1.
    // Для n = 3 лучшее разбиение: 1 + 2, произведение = 1 * 2 = 2.
    // В обоих случаях максимальное произведение равно `n - 1`.
    if n <= 3 {
        return n - 1
    }

    // Количество троек, на которые мы разбиваем число.
    count := n / 3

    // Вычисляем 3^count за O(1) с помощью встроенной функции math.Pow.
    // В Go math.Pow работает только с типом float64, поэтому нам нужно:
    // 1. Привести `count` к float64 на входе.
    // 2. Привести результат обратно к int на выходе.
    // (Для ограничений задачи LeetCode, где n <= 58, float64 обладает 
    // достаточной точностью, и потери данных не произойдет).
    res := int(math.Pow(3, float64(count)))

    // Теперь нужно правильно обработать остаток от деления `n` на 3.

    // 1. Если остаток равен 1 (например, n = 4, 7, 10...):
    // Умножать результат на 1 бессмысленно, это не увеличит итоговое значение.
    // Математически выгоднее "забрать" одну тройку из `res` и объединить её с остатком 1,
    // чтобы получить 4. А 4 оптимально разбивать на 2 * 2 (произведение 4).
    // Поэтому мы "откатываем" умножение на одну тройку (делим на 3) и умножаем на 4.
    if n%3 == 1 {
        return (res / 3) * 4
    }

    // 2. Если остаток равен 0 (n делится на 3 без остатка):
    // Мы уже собрали идеальное произведение, состоящее только из троек, просто возвращаем его.
    if n%3 == 0 {
        return res
    }
    
    // 3. Если остаток равен 2 (например, n = 5, 8, 11...):
    // Двойку разбивать не нужно.
    // Просто умножаем наше произведение троек на оставшуюся двойку.
    // (В этой точке n % 3 гарантированно равно 2).
    return res * 2
}

