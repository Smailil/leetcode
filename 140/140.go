package main

/*
Дана строка s и словарь строк wordDict. Добавьте пробелы в s так, чтобы получить предложение,
в котором каждое слово является корректным словом из словаря. Верните все возможные такие предложения
в любом порядке.

Обратите внимание, что одно и то же слово из словаря можно использовать в разбиении несколько раз.

Пример 1:
Вход: s = "catsanddog", wordDict = ["cat","cats","and","sand","dog"]
Выход: ["cats and dog","cat sand dog"]

Пример 2:
Вход: s = "pineapplepenapple", wordDict = ["apple","pen","applepen","pine","pineapple"]
Выход: ["pine apple pen apple","pineapple pen apple","pine applepen apple"]
Пояснение: обратите внимание, что слово из словаря разрешается использовать повторно.

Пример 3:
Вход: s = "catsandog", wordDict = ["cats","dog","sand","and","cat"]
Выход: []

Ограничения:
1 <= s.length <= 20
1 <= wordDict.length <= 1000
1 <= wordDict[i].length <= 10
s и wordDict[i] состоят только из строчных английских букв.
Все строки в wordDict уникальны.
Входные данные сгенерированы так, что длина ответа не превышает 10^5.

*/

func wordBreak(s string, wordDict []string) []string {
	// Преобразуем словарь в хеш-множество для быстрой проверки наличия слова за O(1)
	wordSet := make(map[string]bool)
	for _, word := range wordDict {
		wordSet[word] = true
	}

	// Мемоизация: memo[i] хранит ВСЕ возможные предложения для подстроки s[i:]
	// Ключ — индекс в строке, значение — список уже собранных предложений.
	// Если ключа нет в map — результат для этого индекса ещё не вычислен.
	memo := make(map[int][]string)

	// backtrack возвращает все предложения, которые можно собрать начиная с индекса i
	var backtrack func(i int) []string
	backtrack = func(i int) []string {
		// Базовый случай: достигли конца строки.
		// Возвращаем слайс с пустой строкой как "заглушку" для конкатенации.
		// Это нужно, чтобы при добавлении последнего слова не получалось лишних пробелов.
		if i == len(s) {
			return []string{""}
		}

		// Проверяем кэш: если результат для этого индекса уже вычислен — возвращаем его.
		// Это и есть суть мемоизации: избегаем повторного перебора для уже рассмотренных суффиксов.
		if cached, ok := memo[i]; ok {
			return cached
		}

		// Здесь будем накапливать все валидные предложения для подстроки s[i:]
		var sentences []string

		// Пробуем все возможные префиксы, начинающиеся с позиции i
		for j := i; j < len(s); j++ {
			word := s[i : j+1] // Текущее слово-кандидат

			// Если слово есть в словаре, пробуем присоединить к нему разбиение хвоста
			if wordSet[word] {
				// Рекурсивно получаем все разбиения оставшейся части строки s[j+1:]
				rest := backtrack(j + 1)

				// Каждое разбиение хвоста комбинируем с текущим словом
				for _, tail := range rest {
					if tail == "" {
						// Это было последнее слово в предложении
						sentences = append(sentences, word)
					} else {
						// Текущее слово + пробел + уже готовое предложение из хвоста
						sentences = append(sentences, word+" "+tail)
					}
				}
			}
		}

		// Сохраняем вычисленный результат в кэш перед возвратом
		memo[i] = sentences
		return sentences
	}

	// Запускаем рекурсию с начала строки
	return backtrack(0)
}