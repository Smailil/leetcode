package main

/*
В ряд стоят n детей. Каждому ребенку назначен рейтинг, заданный в целочисленном массиве ratings.

Вы раздаете детям конфеты с учетом следующих требований:

Каждый ребенок должен получить хотя бы одну конфету.
Дети с более высоким рейтингом получают больше конфет, чем их соседи.
Верните минимальное количество конфет, которое нужно иметь, чтобы раздать их детям.

Пример 1:
Вход: ratings = [1,0,2]
Выход: 5
Пояснение: Первому, второму и третьему ребенку можно выдать соответственно 2, 1 и 2 конфеты.

Пример 2:
Вход: ratings = [1,2,2]
Выход: 4
Пояснение: Первому, второму и третьему ребенку можно выдать соответственно 1, 2 и 1 конфету.
Третий ребенок получает 1 конфету, потому что это удовлетворяет двум условиям выше.

Ограничения:
n == ratings.length
1 <= n <= 2 * 10^4
0 <= ratings[i] <= 2 * 10^4
*/

func candy(ratings []int) int {
    // Получаем количество детей
    n := len(ratings)
    
    // Создаём массив для хранения количества конфет у каждого ребёнка
    // Изначально каждому ребёнку даём по 1 конфете (минимальное условие)
    arr := make([]int, n)
    for i := range arr {
        arr[i] = 1
    }

    // Первый проход: слева направо
    // Если рейтинг текущего ребёнка больше, чем у соседа слева,
    // то даём ему на 1 конфету больше, чем у левого соседа
    for i := 1; i < n; i++ {
        if ratings[i-1] < ratings[i] {
            arr[i] = arr[i-1] + 1
        }
    }

    // Второй проход: справа налево
    // Если рейтинг текущего ребёнка больше, чем у соседа справа,
    // то у него должно быть больше конфет, чем у правого соседа.
    // Берём максимум между текущим значением и (конфеты правого соседа + 1),
    // чтобы удовлетворить оба условия одновременно
    for i := n - 2; i >= 0; i-- {
        if ratings[i] > ratings[i+1] {
            if arr[i+1]+1 > arr[i] {
                arr[i] = arr[i+1] + 1
            }
        }
    }

    // Подсчитываем общее количество конфет
    total := 0
    for _, a := range arr {
        total += a
    }
    return total
}
