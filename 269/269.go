package main

/*
Появился новый инопланетный язык, использующий английский алфавит. Однако порядок букв вам неизвестен.

Вам дан список строковых слов из словаря инопланетного языка. 
Сейчас утверждается, что строки в словах сортируются лексикографически по правилам этого нового языка.

Если это утверждение неверно, и данное расположение строк в словах не может соответствовать какому-либо порядку букв, верните «».
В противном случае верните строку уникальных букв нового инопланетного языка, отсортированную в лексикографически возрастающем порядке по правилам нового языка. 
Если существует несколько решений, верните любое из них.

 
Пример 1:
Input: words = ["wrt","wrf","er","ett","rftt"]
Output: "wertf"

Пример 2:
Input: words = ["z","x"]
Output: "zx"

Пример 3:

Input: words = ["z","x","z"]
Output: ""
Объяснение: Список недействителен, поэтому верните "".

Ограничения:
1 <= words.length <= 100
1 <= words[i].length <= 100
word[i] состоит только из строчных английских букв.

*/

func foreignDictionary(words []string) string {
    // Граф смежности: adj[char] содержит множество символов, которые идут ПОСЛЕ char
    adj := make(map[byte]map[byte]struct{})
    // indegree[char] хранит количество входящих рёбер (сколько символов должно быть перед char)
    indegree := make(map[byte]int)

    // Добавляем все уникальные символы из слов в граф
    for _, word := range words {
        for i := 0; i < len(word); i++ {
            char := word[i]
            if _, exists := adj[char]; !exists {
                adj[char] = make(map[byte]struct{})
            }
            indegree[char] = 0
        }
    }

    // Сравниваем соседние слова, чтобы определить порядок символов
    for i := 0; i < len(words)-1; i++ {
        w1, w2 := words[i], words[i+1]
        minLen := min(len(w2), len(w1))

        // Проверка на валидность: если префикс одинаковый, но первое слово длиннее —
        // это нарушение словарного порядка (например, "abc" перед "ab")
        if len(w1) > len(w2) && w1[:minLen] == w2[:minLen] {
            return ""
        }

        // Находим первый различающийся символ — он определяет порядок
        for j := range minLen {
            if w1[j] != w2[j] {
                // Добавляем ребро: w1[j] -> w2[j] (символ w1[j] идёт перед w2[j])
                if _, exists := adj[w1[j]][w2[j]]; !exists {
                    adj[w1[j]][w2[j]] = struct{}{}
                    indegree[w2[j]]++
                }
                break // Важен только первый различающийся символ
            }
        }
    }

    // === Топологическая сортировка (алгоритм Кана) ===
    // Инициализируем очередь символами с нулевой входящей степенью
    q := []byte{}
    for char := range indegree {
        if indegree[char] == 0 {
            q = append(q, char)
        }
    }

    res := []byte{}
    // Обрабатываем очередь, постепенно уменьшая indegree соседей
    for len(q) > 0 {
        char := q[0]
        q = q[1:]
        res = append(res, char)

        // Уменьшаем indegree всех символов, которые идут после текущего
        for neighbor := range adj[char] {
            indegree[neighbor]--
            if indegree[neighbor] == 0 {
                q = append(q, neighbor)
            }
        }
    }

    // Если не все символы добавлены в результат — есть цикл (невалидный порядок)
    if len(res) != len(indegree) {
        return ""
    }

    return string(res)
}
