package main

/*
Дана закодированная строка — верните её декодированный вид.

Правило кодирования: k[encoded_string],
где encoded_string внутри квадратных скобок повторяется ровно k раз.
Гарантируется, что k — положительное целое число.

Можно считать, что входная строка всегда корректна:
лишних пробелов нет, квадратные скобки расставлены правильно и т.д.
Также можно считать, что исходные данные не содержат цифр —
цифры используются только для обозначения количества повторений k.
Например, входных данных вида 3a или 2[4] не будет.

Тесты составлены так, что длина результирующей строки не превысит 10^5.

Пример 1:
Вход: s = "3[a]2[bc]"
Выход: "aaabcbc"

Пример 2:
Вход: s = "3[a2[c]]"
Выход: "accaccacc"

Пример 3:
Вход: s = "2[abc]3[cd]ef"
Выход: "abcabccdcdcdef"

Ограничения:
1 <= s.length <= 30
s состоит из строчных латинских букв, цифр и квадратных скобок '[]'.
Гарантируется, что s — корректная входная строка.
Все целые числа в s находятся в диапазоне [1, 300].

*/

func decodeString(s string) string {
	// Указатель на текущую позицию в строке. 
	// Является общим для всех уровней рекурсии, что позволяет 
	// последовательно проходить по строке без передачи индекса в аргументах.
	i := 0

	// Рекурсивная функция-замыкание, декодирующая содержимое текущего уровня вложенности.
	var helper func() string
	helper = func() string {
		res := "" // Накопитель результата для текущего уровня
		k := 0    // Множитель повторений для следующей вложенной строки

		for i < len(s) {
			c := s[i]

			if c >= '0' && c <= '9' {
				// Собираем число. Формула поддерживает многозначные множители (напр., 12[a])
				k = k*10 + int(c-'0')
			} else if c == '[' {
				i++ // Пропускаем открывающую скобку
				// Рекурсивно декодируем содержимое до соответствующей ']'
				subRes := helper()
				// Повторяем полученную подстроку k раз и добавляем к текущему результату
				for j := 0; j < k; j++ {
					res += subRes
				}
				k = 0 // Сбрасываем множитель после применения
			} else if c == ']' {
				// Закрывающая скобка означает конец текущего блока вложенности.
				// Возвращаем накопленный результат на уровень выше.
				return res
			} else {
				// Обычный символ (буква) — сразу добавляем в результат
				res += string(c)
			}

			i++ // Переходим к следующему символу строки
		}

		return res
	}

	// Запускаем процесс декодирования с начала строки
	return helper()
}
