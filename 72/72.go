package main

/*
Даны две строки word1 и word2, верните минимальное количество операций, необходимых для преобразования word1 в word2.
Над словом разрешены следующие три операции:
Вставить символ
Удалить символ
Заменить символ

Пример 1:
Input: word1 = "horse", word2 = "ros"
Output: 3
Объяснение: 
horse -> rorse (заменить букву «h» на «r»)
rorse -> rose (убрать букву «r»)
rose -> ros (убрать букву «e»)

Пример 2:
Input: word1 = "intention", word2 = "execution"
Output: 5
Объяснение: 
intention -> inention (убрать букву «t»)
inention -> enention (заменить букву «i» на «e»)
enention -> exention (заменить букву «n» на «x»)
exention -> exection (заменить букву «n» на «c»)
exection -> execution (вставить букву «u»)

Ограничения:
0 <= word1.length, word2.length <= 500
word1 и word2 состоят из строчных английских букв.

*/

func minDistance(word1, word2 string) int {
    m, n := len(word1), len(word2)
    
    // Оптимизация: гарантируем, что word1 всегда длиннее или равна word2.
    // Это позволяет использовать меньше памяти для DP-массива (O(min(m,n)))
    if m < n {
        word1, word2 = word2, word1
        m, n = n, m
    }

    // dp[j] хранит минимальное расстояние между суффиксом word1 и 
    // суффиксом word2[j:]. Используем 1D массив для оптимизации памяти.
    // Изначально заполняем для случая, когда word1 пустая строка.
    dp := make([]int, n+1)
    for j := 0; j <= n; j++ {
        // Если word1 пустая, нужно удалить все символы из word2 (n-j операций)
        dp[j] = n - j
    }

    for i := m - 1; i >= 0; i-- {
        // nextDp хранит значение dp[i+1][j+1] из предыдущей итерации
        // (диагональный элемент в 2D версии алгоритма)
        nextDp := dp[n]
        
        // dp[n] соответствует случаю, когда word2 пустая строка.
        // Нужно удалить все оставшиеся символы из word1 (m-i операций)
        dp[n] = m - i
        
        // Проходим по символам word2 справа налево
        for j := n - 1; j >= 0; j-- {
            // Сохраняем текущее значение dp[j] перед обновлением.
            // Оно понадобится как диагональный элемент для следующей итерации.
            temp := dp[j]
            
            // Если символы совпадают, операция не нужна.
            // Берём значение из диагонали (nextDp = dp[i+1][j+1])
            if word1[i] == word2[j] {
                dp[j] = nextDp
            } else {
                // Если символы разные, выбираем минимальную стоимость из трёх операций:
                // 1. dp[j]     - удаление символа из word1 (вертикально вниз в 2D)
                // 2. dp[j+1]   - вставка символа в word1 (горизонтально вправо в 2D)
                // 3. nextDp    - замена символа (диагонально в 2D)
                // +1 за выполнение операции
                dp[j] = 1 + min(dp[j], dp[j+1], nextDp)
            }
            
            // Обновляем nextDp для следующей итерации (становится диагональным элементом)
            nextDp = temp
        }
    }

    return dp[0]
}