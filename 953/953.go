package main

/*
В инопланетном языке, как ни странно, тоже используются строчные английские буквы,
но, возможно, в другом порядке.
Порядок алфавита является некоторой перестановкой строчных букв.

Дана последовательность слов, записанных на инопланетном языке,
и порядок букв в алфавите.
Верните true тогда и только тогда, когда данные слова отсортированы
лексикографически в этом инопланетном языке.

Пример 1:
Вход: words = ["hello","leetcode"], order = "hlabcdefgijkmnopqrstuvwxyz"
Выход: true
Пояснение: поскольку в этом языке 'h' идет перед 'l',
последовательность отсортирована.

Пример 2:
Вход: words = ["word","world","row"], order = "worldabcefghijkmnpqstuvxyz"
Выход: false
Пояснение: поскольку в этом языке 'd' идет после 'l',
words[0] > words[1], значит последовательность не отсортирована.

Пример 3:
Вход: words = ["apple","app"], order = "abcdefghijklmnopqrstuvwxyz"
Выход: false
Пояснение: первые три символа "app" совпадают,
а вторая строка короче.
По правилам лексикографического порядка "apple" > "app",
потому что 'l' > '∅', где '∅' определяется как пустой символ,
который меньше любого другого символа.

Ограничения:
1 <= words.length <= 100
1 <= words[i].length <= 20
order.length == 26
Все символы в words[i] и order являются строчными английскими буквами.

*/

func isAlienSorted(words []string, order string) bool {
    // Создаем массив для хранения индексов (позиций) каждой буквы в заданном алфавите.
    // Массив имеет размер 26, так как в английском алфавите 26 строчных букв.
    orderIndex := make([]int, 26)
    
    // Заполняем массив: для каждой буквы алфавита записываем её порядковый номер (индекс).
    // Выражение `c - 'a'` преобразует символ (например, 'c') в индекс от 0 до 25.
    for i, c := range order {
        orderIndex[c-'a'] = i
    }

    // Проходим по всем парам соседних слов в массиве, чтобы проверить их порядок.
    for i := 0; i < len(words)-1; i++ {
        w1, w2 := words[i], words[i+1]

        // Сравниваем слова посимвольно.
        for j := 0; j < len(w1); j++ {
            // Если мы дошли до конца второго слова (w2), но первое слово (w1) ещё не закончилось,
            // это означает, что w2 является префиксом w1 (например, "app" и "apple").
            // В правильном лексикографическом порядке более короткое слово должно идти первым.
            if j == len(w2) {
                return false
            }

            // Если символы на текущей позиции отличаются, сравниваем их порядок в нашем алфавите.
            if w1[j] != w2[j] {
                // Если индекс символа из первого слова больше индекса символа из второго слова,
                // значит слова стоят в неправильном порядке.
                if orderIndex[w1[j]-'a'] > orderIndex[w2[j]-'a'] {
                    return false
                }
                // Если порядок правильный (индекс первого меньше), нам не нужно сравнивать 
                // остальные символы этих двух слов. Переходим к следующей паре.
                break
            }
        }
    }
    
    // Если все пары соседних слов оказались в правильном порядке, возвращаем true.
    return true
}
