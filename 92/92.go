package main

/*
Дан head односвязного списка и два целых числа left и right, где left <= right.
Разверните узлы списка с позиции left до позиции right и верните развёрнутый список.

Пример 1:
https://assets.leetcode.com/uploads/2021/02/19/rev2ex2.jpg
Вход: head = [1,2,3,4,5], left = 2, right = 4
Выход: [1,4,3,2,5]

Пример 2:
Вход: head = [5], left = 1, right = 1
Выход: [5]

Ограничения:
Количество узлов в списке равно n.
1 <= n <= 500
-500 <= Node.val <= 500
1 <= left <= right <= n
*/

/**
 * Определение односвязного списка.
 * type ListNode struct {
 *     Val int
 *     Next *ListNode
 * }
 */

func reverseBetween(head *ListNode, left int, right int) *ListNode {
    // Создаём dummy-узел. Он упрощает обработку случая, когда left == 1,
    // так как нам всегда нужен узел, предшествующий разворачиваемому участку.
    dummy := &ListNode{Val: 0, Next: head}
    
    // leftPrev будет указывать на узел, стоящий прямо перед left-й позицией
    leftPrev := dummy
    
    // cur будет двигаться к left-й позиции
    cur := head

    // Смещаем leftPrev и cur на нужные стартовые позиции.
    // После цикла cur указывает на left-й узел, leftPrev — на узел перед ним.
    for range(left-1) {
        leftPrev = cur
        cur = cur.Next
    }

    // Стандартный итеративный разворот связного списка.
    // Разворачиваем ровно (right - left + 1) узлов.
    var prev *ListNode // Будет хранить предыдущий узел в процессе разворота
    
    for range(right-left+1) {
        tmpNext := cur.Next // Сохраняем следующий узел, чтобы не потерять ссылку на остаток списка
        cur.Next = prev     // Меняем направление указателя текущего узла
        prev = cur          // Сдвигаем prev вперёд
        cur = tmpNext       // Сдвигаем cur вперёд
    }

    // После завершения цикла:
    // • prev указывает на новую голову развёрнутого подсписка (бывший right-й узел)
    // • cur указывает на первый узел после развёрнутого участка (бывший right.Next)
    // • leftPrev.Next всё ещё указывает на бывшую голову подсписка, которая теперь стала его хвостом

    // Соединяем хвост развёрнутого подсписка с оставшейся частью списка
    leftPrev.Next.Next = cur
    
    // Соединяем часть списка до left-й позиции с новой головой развёрнутого подсписка
    leftPrev.Next = prev

    // Возвращаем голову списка. dummy.Next может отличаться от head, если left == 1
    return dummy.Next
}