package main

/*
Напишите алгоритм, определяющий, является ли число n счастливым.
Счастливое число — это число, определяемое следующим процессом:
Начиная с любого положительного целого числа, замените число суммой квадратов его цифр.
Повторяйте процесс до тех пор, пока число не станет равным 1 (где оно и останется), или пока он не будет повторяться бесконечно в цикле, который не включает 1.
Счастливыми являются те числа, для которых этот процесс заканчивается на 1.
Возвращайте true, если n — счастливое число, и false, если нет.

Пример 1:
Input: n = 19
Output: true
Объяснение:
12 + 92 = 82
82 + 22 = 68
62 + 82 = 100
12 + 02 + 02 = 1

Пример 2:
Input: n = 2
Output: false

Ограничения:
1 <= n <= 2^31 - 1

*/

// Алгоритм использует метод Флойда "черепаха и заяц" (Floyd's cycle detection)
// для обнаружения цикла без использования дополнительной памяти.
func isHappy(n int) bool {
    // Инициализируем два указателя:
    // slow ("черепаха") — делает один шаг за итерацию
    // fast ("заяц") — делает два шага за итерацию
    slow, fast := n, sumOfSquares(n)

    // Двигаем указатели, пока они не встретятся.
    // Если есть цикл — они обязательно встретятся внутри него.
    // Если число счастливое — оба указателя сойдутся на значении 1.
    for slow != fast {
        // "Заяц" делает два шага: применяем sumOfSquares дважды
        fast = sumOfSquares(fast)
        fast = sumOfSquares(fast)
        // "Черепаха" делает один шаг
        slow = sumOfSquares(slow)
    }

    // Если указатели сошлись на 1 — число счастливое.
    // Если сошлись на другом числе — значит, попали в цикл, число не счастливое.
    return fast == 1
}

func sumOfSquares(n int) int {
    output := 0
    for n > 0 {
        digit := n % 10
        output += digit * digit
        n = n / 10
    }
    return output
}