/*
Разработайте структуру данных типа стек для добавления элементов
и извлечения наиболее часто встречающегося элемента из стека.

Реализуйте класс FreqStack:

FreqStack() — конструирует пустой частотный стек.
void push(int val) — добавляет целое число val на вершину стека.
int pop() — удаляет и возвращает наиболее часто встречающийся элемент в стеке.
При равной частоте удаляется и возвращается элемент,
находящийся ближе всего к вершине стека.

Пример 1:

Ввод
["FreqStack", "push", "push", "push", "push", "push", "push", "pop", "pop", "pop", "pop"]
[[], [5], [7], [5], [7], [4], [5], [], [], [], []]
Вывод
[null, null, null, null, null, null, null, 5, 7, 5, 4]

Пояснение
FreqStack freqStack = new FreqStack();
freqStack.push(5); // Стек: [5]
freqStack.push(7); // Стек: [5,7]
freqStack.push(5); // Стек: [5,7,5]
freqStack.push(7); // Стек: [5,7,5,7]
freqStack.push(4); // Стек: [5,7,5,7,4]
freqStack.push(5); // Стек: [5,7,5,7,4,5]
freqStack.pop();   // возвращает 5, так как 5 — наиболее частый. Стек: [5,7,5,7,4].
freqStack.pop();   // возвращает 7, так как 5 и 7 одинаково часты, но 7 ближе к вершине. Стек: [5,7,5,4].
freqStack.pop();   // возвращает 5, так как 5 — наиболее частый. Стек: [5,7,4].
freqStack.pop();   // возвращает 4, так как 4, 5 и 7 одинаково часты, но 4 ближе к вершине. Стек: [5,7].

Ограничения:
0 <= val <= 10^9
Будет сделано не более 2 * 10^4 вызовов push и pop.
Гарантируется, что перед вызовом pop в стеке будет как минимум один элемент.

*/

type FreqStack struct {
    cnt    map[int]int   // cnt хранит текущую частоту каждого значения
    stacks map[int][]int // stacks хранит стеки значений, сгруппированные по их частоте
    maxCnt int           // maxCnt отслеживает максимальную частоту среди всех элементов в стеке
}

// Constructor инициализирует новый пустой FreqStack.
func Constructor() FreqStack {
    return FreqStack{
        cnt:    make(map[int]int),
        stacks: make(map[int][]int),
        maxCnt: 0,
    }
}

// Push добавляет элемент val в стек частот.
func (this *FreqStack) Push(val int) {
    // Увеличиваем счётчик частоты для значения val
    this.cnt[val]++
    valCnt := this.cnt[val]

    // Если новая частота превышает текущий максимум, обновляем maxCnt
    // и создаём новый пустой срез (стек) для этой частоты
    if valCnt > this.maxCnt {
        this.maxCnt = valCnt
        this.stacks[valCnt] = []int{}
    }

    // Добавляем val в стек, соответствующий его текущей частоте.
    // Таким образом, elements с одинаковой частотой хранятся в порядке добавления.
    this.stacks[valCnt] = append(this.stacks[valCnt], val)
}

// Pop извлекает и возвращает элемент с максимальной частотой.
// При равной частоте возвращается элемент, добавленный последним.
func (this *FreqStack) Pop() int {
    // Получаем стек, содержащий элементы с максимальной частотой
    stack := this.stacks[this.maxCnt]

    // Извлекаем последний добавленный элемент из этого стека
    res := stack[len(stack)-1]

    // Удаляем последний элемент из среза
    this.stacks[this.maxCnt] = stack[:len(stack)-1]

    // Уменьшаем счётчик частоты для извлечённого элемента
    this.cnt[res]--

    // Если стек для текущей максимальной частоты стал пустым,
    // значит, элементов с такой частотой больше не существует.
    // Уменьшаем maxCnt, чтобы следующий Pop обращался к правильной группе.
    if len(this.stacks[this.maxCnt]) == 0 {
        this.maxCnt--
    }

    return res
}

