package main

import "sort"

/*
Вам дан массив people, где people[i] — вес i-го человека,
и бесконечное количество лодок, каждая из которых может выдержать
максимальный вес limit. Каждая лодка вмещает не более двух человек одновременно,
при условии, что сумма их весов не превышает limit.

Верните минимальное количество лодок, необходимых для перевозки всех людей.

Пример 1:
Вход: people = [1,2], limit = 3
Выход: 1
Пояснение: 1 лодка (1, 2)

Пример 2:
Вход: people = [3,2,2,1], limit = 3
Выход: 3
Пояснение: 3 лодки (1, 2), (2) и (3)

Пример 3:
Вход: people = [3,5,3,4], limit = 5
Выход: 4
Пояснение: 4 лодки (3), (3), (4), (5)

Ограничения:
1 <= people.length <= 5 * 10^4
1 <= people[i] <= limit <= 3 * 10^4
*/

func numRescueBoats(people []int, limit int) int {
	// Сортируем массив весов по возрастанию.
	// Это позволяет эффективно применять жадный алгоритм с двумя указателями.
	sort.Ints(people)

	res := 0          // Счётчик использованных лодок
	l := 0            // Указатель на самого лёгкого человека (начало массива)
	r := len(people) - 1 // Указатель на самого тяжёлого человека (конец массива)

	// Пока указатели не пересеклись, есть люди, которых нужно распределить по лодкам
	for l <= r {
		// Обязательно сажаем в лодку самого тяжёлого человека
		remain := limit - people[r] // Вычисляем оставшуюся грузоподъёмность
		r--                         // Сдвигаем указатель тяжёлых влево
		res++                       // Увеличиваем количество лодок

		// Если в лодке осталось достаточно места, забираем и самого лёгкого человека
		// Проверка l <= r нужна, так как после r-- указатели могли пересечься
		if l <= r && remain >= people[l] {
			l++ // Сдвигаем указатель лёгких вправо
		}
	}

	return res
}
