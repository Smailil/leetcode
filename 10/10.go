package main

/*
Дана входная строка s и шаблон p, реализуйте сопоставление регулярных выражений с поддержкой '.' и '*' где:
'.' Соответствует любому отдельному символу.​​​​
'*' Соответствует нулю или более предыдущих элементов.
Возвращает логическое значение, указывающее, охватывает ли сопоставление всю входную строку (не часть)

Пример 1:
Input: s = "aa", p = "a"
Output: false
Объяснение: "a" не соответствует всей строке "aa".

Пример 2:
Input: s = "aa", p = "a*"
Output: true
Пояснение: '*' означает ноль или более предыдущего элемента 'a'. Следовательно, повторив «а» один раз, получится «аа».

Пример 3:
Input: s = "ab", p = ".*"
Output: true
Пояснение: «.*» означает «ноль или более (*) любого символа (.)».

Ограничения:
1 <= s.length <= 20
1 <= p.length <= 20
s содержит только строчные английские буквы.
p содержит только строчные английские буквы, «.» и «*».
Гарантируется, что для каждого появления символа «*» будет соответствовать предыдущий допустимый символ.

*/

func isMatch(s, p string) bool {
	m, n := len(s), len(p)

	// dp хранит значения для "нижней" строки таблицы (i+1 в 2D-версии).
	// Изначально соответствует состоянию, когда суффикс строки s пуст.
	dp := make([]bool, n+1)
	// Базовый случай для "воображаемой" строки i = m+1:
	// пустой паттерн совпадает только с пустой строкой.
	dp[n] = true

	// Проходим по строке s с конца к началу.
	// Каждая итерация вычисляет значения для текущей строки i.
	for i := m; i >= 0; i-- {
		// nextDp будет хранить результаты для текущей строки i
		nextDp := make([]bool, n+1)

		// Базовый случай для текущей строки:
		// Если паттерн пуст (j == n), совпадение возможно только тогда,
		// когда и суффикс строки пуст (i == m).
		nextDp[n] = i == m

		// Проходим по паттерну p с конца к началу.
		for j := n - 1; j >= 0; j-- {
			// Проверяем совпадение текущих символов.
			// i < m защищает от паники при выходе за границы строки.
			match := i < m && (s[i] == p[j] || p[j] == '.')

			if j+1 < n && p[j+1] == '*' {
				// Обработка '*': ноль или более повторений предыдущего символа.
				// nextDp[j+2] → dp[i][j+2] : пропускаем символ и '*' (0 повторений).
				//               Значение уже вычислено в ЭТОЙ итерации, т.к. j убывает.
				// dp[j]       → dp[i+1][j] : символ совпал, "съедаем" его из s,
				//               но остаёмся на том же паттерне (1+ повторений).
				//               Значение взято из ПРЕДЫДУЩЕЙ итерации внешнего цикла.
				nextDp[j] = nextDp[j+2] || (match && dp[j])
			} else if match {
				// Обычный символ или '.':
				// dp[j+1] → dp[i+1][j+1] : символы совпали, двигаемся дальше по обоим строкам.
				//               Значение также взято из ПРЕДЫДУЩЕЙ итерации.
				nextDp[j] = dp[j+1]
			}
			// Если совпадения нет, nextDp[j] остаётся false (значение по умолчанию в Go)
		}

		// "Сдвигаем" окно: текущая вычисленная строка становится нижней
		// для следующей итерации (i-1). Старый dp отбрасывается сборщиком мусора.
		dp = nextDp
	}

	// dp[0] содержит результат для полных строк: s[0:] и p[0:]
	return dp[0]
}

