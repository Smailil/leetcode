package main

/*
Даны строки s1, s2 и s3. Определите, образована ли строка s3 чередованием строк s1 и s2.
Чередование двух строк s и t — это конфигурация, в которой s и t делятся на n и m подстрок соответственно, так что:
s = s1 + s2 + ... + sn
t = t1 + t2 + ... + tm
|n - m| <= 1
Чередование: s1 + t1 + s2 + t2 + s3 + t3 + ... или t1 + s1 + t2 + s2 + t3 + s3 + ...
Примечание: a + b — это объединение строк a и b.

Пример 1:

Input: s1 = "aabcc", s2 = "dbbca", s3 = "aadbbcbcac"
Output: true
Пояснение: Один из способов получить s3:
Разделите s1 на s1 = «aa» + «bc» + «c», а s2 на s2 = «dbbc» + «a».
Чередуя два разделения, мы получаем «aa» + «dbbc» + «bc» + «a» + «c» = «aadbbcbcac».
Поскольку s3 можно получить путем чередования s1 и s2, мы возвращаем true.

Пример 2:
Input: s1 = "aabcc", s2 = "dbbca", s3 = "aadbbbaccc"
Output: false
Пояснение: Обратите внимание, что невозможно чередовать s2 с какой-либо другой строкой, чтобы получить s3.

Пример 3:
Input: s1 = "", s2 = "", s3 = ""
Output: true

Ограничения:
0 <= s1.length, s2.length <= 100
0 <= s3.length <= 200
s1, s2 и s3 состоят из строчных английских букв.

*/

func isInterleave(s1, s2, s3 string) bool {
	m, n := len(s1), len(s2)
	
	// Проверка длины: s3 должна быть ровно такой же длины, как s1 + s2
	if m+n != len(s3) {
		return false
	}
	
	// Оптимизация: меняем строки местами, чтобы s2 была длиннее
	// Тогда массив dp будет размером с длину s2 (меньше памяти)
	if n < m {
		s1, s2 = s2, s1
		m, n = n, m
	}
	
	// dp[j] отвечает на вопрос:
	// "Можно ли собрать оставшуюся часть s3, если мы уже использовали:
	//  - первые i символов из s1 (текущая позиция i во внешнем цикле)
	//  - первые j символов из s2 (текущая позиция j во внутреннем цикле)"
	// 
	// Проще: dp[j] = true, если суффикс s3 можно получить из суффиксов s1 и s2
	dp := make([]bool, n+1)
	
	// Базовый случай: если мы использовали все символы из обеих строк,
	// значит мы успешно собрали s3 → возвращаем true
	dp[n] = true
	
	// Проходим по всем позициям с конца к началу
	// i — сколько символов из s1 уже использовали
	// j — сколько символов из s2 уже использовали
	for i := m; i >= 0; i-- {
		// nextDp хранит результат для позиции (i, j+1)
		// То есть: "можно ли продолжить, если возьмём символ из s2"
		// В начале строки (j=n) это значение равно тому, дошли ли мы до конца s1
		nextDp := i == m
		
		for j := n; j >= 0; j-- {
			// По умолчанию считаем, что собрать строку нельзя
			res := false
			
			// Если дошли до конца обеих строк — берём предыдущее значение
			if j == n {
				res = nextDp
			}
			
			// ─────────────────────────────────────────────────────────────
			// ВАРИАНТ 1: Берём следующий символ из строки s1
			// ─────────────────────────────────────────────────────────────
			// Проверяем три условия:
			// 1. i < m        — в s1 ещё есть символы, которые мы не использовали
			// 2. s1[i] == s3[i+j] — текущий символ s1 совпадает с нужным символом в s3
			//    (позиция в s3 = i+j, потому что мы уже взяли i символов из s1 и j из s2)
			// 3. dp[j] == true — ВСЁ, ЧТО ОСТАЛОСЬ ПОСЛЕ ЭТОГО, МОЖНО СОБРАТЬ
			//    (dp[j] хранит результат для позиции (i+1, j) из предыдущей итерации)
			if i < m && s1[i] == s3[i+j] && dp[j] {
				res = true
			}
			
			// ─────────────────────────────────────────────────────────────
			// ВАРИАНТ 2: Берём следующий символ из строки s2
			// ─────────────────────────────────────────────────────────────
			// Проверяем три условия:
			// 1. j < n        — в s2 ещё есть символы, которые мы не использовали
			// 2. s2[j] == s3[i+j] — текущий символ s2 совпадает с нужным символом в s3
			// 3. nextDp == true — ВСЁ, ЧТО ОСТАЛОСЬ ПОСЛЕ ЭТОГО, МОЖНО СОБРАТЬ
			//    (nextDp хранит результат для позиции (i, j+1))
			if j < n && s2[j] == s3[i+j] && nextDp {
				res = true
			}
			
			// Записываем результат: можно ли собрать s3 с текущей позиции (i, j)
			dp[j] = res
			
			// Сохраняем текущий результат для следующей итерации
			// На следующем шаге (j-1) это значение будет означать "позиция (i, j)"
			nextDp = dp[j]
		}
	}
	
	// dp[0] = можно ли собрать всю строку s3, начиная с самого начала
	// (используя 0 символов из s1 и 0 символов из s2 на старте)
	return dp[0]
}
