package main

import "container/heap"

/*
Дана строка s. Переставьте символы строки s так, чтобы любые два соседних символа не совпадали.

Верните любую возможную перестановку строки s или верните "", если это невозможно.

Пример 1:
Вход: s = "aab"
Выход: "aba"

Пример 2:
Вход: s = "aaab"
Выход: ""

Ограничения:
1 <= s.length <= 500
s состоит из строчных английских букв.
*/

// MaxHeap — максимальная куча (max-heap), хранящая пары [частота, символ].
// Каждый элемент — это []int из двух значений: {частота символа, индекс символа (0-25)}.
// Куча упорядочивается по убыванию частоты, чтобы извлекать самый частый символ первым.
type MaxHeap [][]int

// Len возвращает количество элементов в куче.
// Требуется интерфейсом heap.Interface.
func (h MaxHeap) Len() int { return len(h) }

// Less определяет порядок: возвращает true, если элемент i больше элемента j.
// Это превращает стандартную min-heap Go в max-heap — всегда на вершине будет
// элемент с наибольшей частотой (h[i][0] > h[j][0]).
func (h MaxHeap) Less(i, j int) bool { return h[i][0] > h[j][0] }

// Swap меняет местами элементы по индексам i и j.
// Требуется интерфейсом heap.Interface.
func (h MaxHeap) Swap(i, j int) { h[i], h[j] = h[j], h[i] }

// Push добавляет новый элемент в кучу.
// Принимает any и приводит к []int, затем добавляет в слайс.
// Вызывается пакетом heap при heap.Push().
func (h *MaxHeap) Push(x any) { *h = append(*h, x.([]int)) }

// Pop извлекает и возвращает последний элемент кучи (вершину после sift-down).
// Усекает слайс, удаляя последний элемент, и возвращает его.
// Вызывается пакетом heap при heap.Pop().
func (h *MaxHeap) Pop() any {
	old := *h
	n := len(old)
	x := old[n-1]        // Последний элемент — это вершина после sift-down
	*h = old[0 : n-1]    // Удаляем последний элемент из слайса
	return x
}

// Алгоритм (жадный):
//  1. Считаем частоты всех симвлов.
//  2. Помещаем их в max-heap, чтобы всегда брать самый частый доступный символ.
//  3. На каждом шаге извлекаем самый частый символ и добавляем его в результат.
//     Но чтобы не было повторов подряд, только что использованный символ
//     запоминается в prev и возвращается обратно в кучу только на СЛЕДУЮЩЕМ шаге.
//  4. Если куча пуста, а prev ещё не израсходован — значит, оставшиеся символы
//     одного типа и их невозможно разнести. Возвращаем "".
func reorganizeString(s string) string {
	// Считаем частоту каждого символа в массиве из 26 элементов (a-z).
	freq := make([]int, 26)
	for _, c := range s {
		freq[c-'a']++
	}

	// Инициализируем max-heap и заполняем его символами с ненулевой частотой.
	h := &MaxHeap{}
	heap.Init(h)
	for i := range 26 {
		if freq[i] > 0 {
			// Добавляем пару: {частота, индекс символа}
			heap.Push(h, []int{freq[i], i})
		}
	}

	res := []byte{}   // Результирующая строка (в виде байтов для эффективности)
	var prev []int    // Символ, использованный на предыдущем шаге (задерживается на 1 шаг)

	// Продолжаем, пока в куче есть элементы или есть отложенный символ.
	for h.Len() > 0 || prev != nil {
		// Если куча пуста, но есть отложенный символ — значит, мы не можем
		// поставить его рядом с ним же. Перестройка невозможна.
		if prev != nil && h.Len() == 0 {
			return ""
		}

		// Извлекаем самый частый символ из кучи.
		curr := heap.Pop(h).([]int)
		res = append(res, byte(curr[1]+'a')) // Добавляем символ в результат
		curr[0]--                             // Уменьшаем его частоту на 1

		// Возвращаем отложенный (предыдущий) символ обратно в кучу.
		// Теперь он снова доступен для выбора, т.к. прошёл один шаг
		// и он не будет стоять рядом с собой же.
		if prev != nil {
			heap.Push(h, prev)
			prev = nil
		}

		// Если у текущего символа ещё остались неиспользованные экземпляры,
		// откладываем его на следующий шаг (нельзя ставить подряд).
		if curr[0] > 0 {
			prev = curr
		}
	}

	return string(res)
}