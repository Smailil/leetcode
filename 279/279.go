package main

/*
Дано целое число n.
Верните минимальное количество полных квадратов, сумма которых равна n.

Полный квадрат - это целое число, равное квадрату другого целого числа.
Иными словами, это произведение некоторого целого числа на само себя.
Например, 1, 4, 9 и 16 являются полными квадратами, а 3 и 11 - нет.

Пример 1:
Вход: n = 12
Выход: 3
Пояснение: 12 = 4 + 4 + 4.

Пример 2:
Вход: n = 13
Выход: 2
Пояснение: 13 = 4 + 9.

Ограничения:
1 <= n <= 10^4
*/

func numSquares(n int) int {
    // Создаем массив dp размером n+1. 
    // dp[i] будет хранить минимальное количество квадратов чисел, 
    // сумма которых в точности равна i.
    dp := make([]int, n+1)
    
    // Инициализируем все значения числом n. 
    // Это заведомо большее (или равное) значение, так как в худшем случае 
    // любое число можно разложить на сумму единиц (1^2 + 1^2 + ... + 1^2).
    for i := range dp {
        dp[i] = n
    }
    
    // Базовый случай: чтобы получить сумму 0, нам нужно 0 квадратов.
    dp[0] = 0

    // Последовательно вычисляем минимальное количество квадратов для каждого числа от 1 до n.
    for target := 1; target <= n; target++ {
        // Перебираем все возможные квадраты чисел (s*s), 
        // которые меньше или равны текущему целевому числу (target).
        for s := 1; s*s <= target; s++ {
            // Мы можем взять текущий квадрат (s*s). Тогда нам понадобится 1 шаг 
            // плюс минимальное количество шагов для оставшейся суммы (target - s*s).
            // Выбираем минимум между текущим значением dp[target] и новым вариантом.
            dp[target] = min(dp[target], 1+dp[target-s*s])
        }
    }

    // Итоговый ответ для числа n находится в последней ячейке массива.
    return dp[n]
}