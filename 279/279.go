package main

/*
Дано целое число n.
Верните минимальное количество полных квадратов, сумма которых равна n.

Полный квадрат - это целое число, равное квадрату другого целого числа.
Иными словами, это произведение некоторого целого числа на само себя.
Например, 1, 4, 9 и 16 являются полными квадратами, а 3 и 11 - нет.

Пример 1:
Вход: n = 12
Выход: 3
Пояснение: 12 = 4 + 4 + 4.

Пример 2:
Вход: n = 13
Выход: 2
Пояснение: 13 = 4 + 9.

Ограничения:
1 <= n <= 10^4
*/

import "math"

func numSquares(n int) int {
    // ШАГ 1: Проверяем, требует ли число ровно 4 квадрата (Теорема Лежандра).
    // Делим число на 4 до тех пор, пока оно делится без остатка.
    temp := n
    for temp%4 == 0 {
        temp /= 4
    }
    // Если после всех делений остаток от деления на 8 равен 7, 
    // то число имеет вид 4^a * (8b + 7). Ответ точно 4.
    if temp%8 == 7 {
        return 4
    }

    // ШАГ 2: Проверяем, является ли само число полным квадратом (Ответ: 1).
    if isPerfectSquare(n) {
        return 1
    }

    // ШАГ 3: Проверяем, можно ли представить число в виде суммы двух квадратов (Ответ: 2).
    // Перебираем i от 1 до sqrt(n) и проверяем, является ли (n - i*i) полным квадратом.
    for i := 1; i*i <= n; i++ {
        if isPerfectSquare(n - i*i) {
            return 2
        }
    }

    // ШАГ 4: Если число не подходит под случаи 1, 2 и 4, 
    // то по теореме Лагранжа ответ строго 3.
    return 3
}

// Вспомогательная функция для проверки, является ли число полным квадратом.
func isPerfectSquare(x int) bool {
    // Извлекаем корень, приводим к int и проверяем обратным умножением.
    s := int(math.Sqrt(float64(x)))
    return s*s == x
}

