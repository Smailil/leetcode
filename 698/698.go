package main

/*
Дан целочисленный массив nums и целое число k.
Верните true, если этот массив можно разбить на k непустых подмножеств,
суммы которых все равны.

Пример 1:
Вход: nums = [4,3,2,3,5,2,1], k = 4
Выход: true
Пояснение: массив можно разбить на 4 подмножества (5), (1, 4), (2,3), (2,3) с равными суммами.
Пример 2:
Вход: nums = [1,2,3,4], k = 3
Выход: false

Ограничения:
1 <= k <= nums.length <= 16
1 <= nums[i] <= 10^4
Частота каждого элемента находится в диапазоне [1, 4].
*/

func canPartitionKSubsets(nums []int, k int) bool {
    // 1. Считаем общую сумму всех элементов массива
    sum := 0
    for _, num := range nums {
        sum += num
    }
    
    // Если общая сумма не делится на k без остатка, 
    // то разбить массив на k подмножеств с равной суммой математически невозможно
    if sum%k != 0 {
        return false
    }

    // Целевая сумма, которую должно иметь каждое из k подмножеств
    target := sum / k
    
    // 2. Сортируем массив по убыванию. 
    // Это значительно ускоряет бэктрекинг: алгоритм сначала пытается пристроить 
    // самые крупные числа. Если они не подходят, мы быстрее это поймем и отсечем ветку.
    sort.Sort(sort.Reverse(sort.IntSlice(nums)))
    
    // Оптимизация: если самый большой элемент в отсортированном массиве больше target,
    // он никуда не поместится, поэтому решение невозможно.
    if len(nums) > 0 && nums[0] > target {
        return false
    }
    
    // Слайс для отметок: использован ли элемент в каком-либо подмножестве
    used := make([]bool, len(nums))

    // 3. Вспомогательная рекурсивная функция (поиск с возвратом / бэктрекинг)
    // i: индекс, с которого начинаем перебор элементов для текущего подмножества (чтобы не дублировать комбинации)
    // k: количество оставшихся подмножеств, которые нужно собрать
    // subsetSum: текущая сумма формируемого подмножества
    var backtrack func(i, k, subsetSum int) bool
    backtrack = func(i, k, subsetSum int) bool {
        // Базовый случай: если осталось собрать 0 подмножеств, значит мы успешно собрали все k штук
        if k == 0 {
            return true
        }
        
        // Если текущее подмножество собрано (сумма достигла target), 
        // переходим к сбору следующего. Начинаем перебор заново с индекса 0.
        if subsetSum == target {
            return backtrack(0, k-1, 0)
        }
        
        // Перебираем доступные элементы, начиная с индекса i
        for j := i; j < len(nums); j++ {
            // Пропускаем элемент, если он уже взят в другое подмножество 
            // или его добавление превысит целевую сумму
            if used[j] || subsetSum+nums[j] > target {
                continue
            }
            
            // Делаем выбор: берем элемент в текущее подмножество
            used[j] = true
            
            // Рекурсивно пытаемся завершить текущее подмножество с оставшимися числами
            if backtrack(j+1, k, subsetSum+nums[j]) {
                return true // Если решение найдено, прерываем поиск и возвращаем true
            }
            
            // Откатываем действие (бэктрекинг), если текущий путь не привел к решению
            used[j] = false
            
            // Оптимизация: если мы пытаемся добавить элемент в ПУСТОЕ подмножество (subsetSum == 0) 
            // и это не привело к успеху, то нет смысла пробовать другие элементы на этом месте.
            // Мы просто получим те же самые комбинации в другом порядке.
            if subsetSum == 0 {
                break
            }
        }
        
        // Если перебор закончился и собрать подмножество не удалось
        return false
    }

    // Стартуем процесс поиска с первого элемента (индекс 0), собирая k подмножеств с начальной суммой 0
    return backtrack(0, k, 0)
}
