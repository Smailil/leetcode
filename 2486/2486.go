package main

/*
Даны две строки s и t, состоящие только из строчных английских букв.

Верните минимальное количество символов, которые нужно дописать в конец s,
чтобы t стала подпоследовательностью s.

Подпоследовательность — это строка, которую можно получить из другой строки,
удалив некоторые символы (возможно, ни одного) без изменения порядка
оставшихся символов.

Пример 1:
Ввод: s = "coaching", t = "coding"
Вывод: 4
Пояснение: добавьте символы "ding" в конец s,
чтобы получить s = "coachingding".
Теперь t является подпоследовательностью s ("coachingding").
Можно доказать, что добавление любых трёх символов в конец s
никогда не позволит сделать t подпоследовательностью s.

Пример 2:
Ввод: s = "abcde", t = "a"
Вывод: 0
Пояснение: t уже является подпоследовательностью s ("abcde").

Пример 3:
Ввод: s = "z", t = "abcde"
Вывод: 5
Пояснение: добавьте символы "abcde" в конец s,
чтобы получить s = "zabcde".
Теперь t является подпоследовательностью s ("zabcde").
Можно доказать, что добавление любых четырёх символов в конец s
никогда не позволит сделать t подпоследовательностью s.

Ограничения:
1 <= s.length, t.length <= 10^5
Строки s и t состоят только из строчных английских букв.
*/

// appendCharacters находит максимальный префикс t, который уже является
// подпоследовательностью s. Время работы — O(len(s)), память — O(1).
func appendCharacters(s string, t string) int {
    // i сканирует исходную строку s, а j указывает на следующий символ t,
    // который требуется сопоставить.
    i, j := 0, 0

    // Инвариант цикла: t[:j] уже является подпоследовательностью s[:i].
    for i < len(s) && j < len(t) {
        if s[i] == t[j] {
            // Совпадение расширяет найденную подпоследовательность,
            // поэтому переходим к следующим символам обеих строк.
            i++
            j++
        } else {
            // Текущий символ s не подходит: пропускаем только его,
            // продолжая искать тот же символ t.
            i++
        }
    }

    // t[j:] — несопоставленный суффикс t. Его целиком нужно дописать к s,
    // поэтому ответ равен количеству оставшихся символов.
    return len(t) - j
}
