package main

import (
	"strconv"
	"strings"
)

/*
Даны два неотрицательных целых числа num1 и num2, представленных в виде строк.
Верните произведение num1 и num2, также в виде строки.

Примечание: нельзя использовать встроенные библиотеки для больших чисел
или напрямую преобразовывать входные данные в целое число.

Пример 1:
Вход: num1 = "2", num2 = "3"
Выход: "6"
.
Пример 2:
Вход: num1 = "123", num2 = "456"
Выход: "56088"

Ограничения:
1 <= num1.length, num2.length <= 200
num1 и num2 состоят только из цифр.
num1 и num2 не содержат ведущих нулей, кроме числа 0.
*/

func multiply(num1 string, num2 string) string {
	if num1 == "0" || num2 == "0" {
		return "0"
	}

	// Массив для хранения цифр результата. Максимальная длина произведения двух чисел
	// длиной n и m равна n + m. В Go слайс инициализируется нулями, что удобно для аккумуляции.
	res := make([]int, len(num1)+len(num2))

	// Проходим по цифрам справа налево (от младших разрядов к старшим)
	for i1 := len(num1) - 1; i1 >= 0; i1-- {
		for i2 := len(num2) - 1; i2 >= 0; i2-- {
			// Вычисляем позицию в массиве res для текущей пары цифр.
			// (len(num1)-1-i1) и (len(num2)-1-i2) — это смещения от конца строк.
			// Их сумма даёт разряд, в который попадает произведение текущих цифр.
			pos := len(num1) - 1 - i1 + len(num2) - 1 - i2

			// Преобразуем ASCII-символы в числа (вычитаем код '0') и перемножаем
			digit := int(num1[i1]-'0') * int(num2[i2]-'0')

			// Добавляем произведение к уже накопленному значению в этой позиции
			res[pos] += digit

			// Переносим десятки в старший разряд (pos+1)
			res[pos+1] += res[pos] / 10

			// Оставляем в текущей позиции только единицы
			res[pos] = res[pos] % 10
		}
	}

	// Собираем итоговую строку из массива цифр
	var result strings.Builder
	start := len(res) - 1

	// Пропускаем ведущие нули (находим старший значащий разряд)
	for start >= 0 && res[start] == 0 {
		start--
	}

	// На случай, если массив состоит только из нулей (защита от edge-case)
	if start < 0 {
		return "0"
	}

	// Записываем цифры в обратном порядке: от старшего разряда к младшему
	for i := start; i >= 0; i-- {
		result.WriteString(strconv.Itoa(res[i]))
	}

	return result.String()
}
