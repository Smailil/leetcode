func findTargetSumWays(nums []int, target int) int {
    // dp хранит количество способов получить каждую возможную сумму
    // Ключ — текущая сумма, Значение — количество способов её получить
    dp := make(map[int]int)
    
    // Базовый случай: сумма 0 достигается одним способом (ничего не выбираем)
    dp[0] = 1

    // Проходим по каждому числу в массиве
    for _, num := range nums {
        // Создаём новую карту для следующего шага DP
        nextDp := make(map[int]int)
        
        // Для каждой уже достигнутой суммы пробуем два варианта:
        for total, count := range dp {
            // Вариант 1: добавляем текущее число со знаком "+"
            nextDp[total+num] += count
            
            // Вариант 2: добавляем текущее число со знаком "-"
            nextDp[total-num] += count
        }
        
        // Обновляем dp для следующей итерации
        dp = nextDp
    }

    // Возвращаем количество способов получить целевую сумму
    // Если target не существует в map, вернётся 0 (значение по умолчанию)
    return dp[target]
}
