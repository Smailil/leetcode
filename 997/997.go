package main

/*
В городе есть n человек, пронумерованных от 1 до n. Ходит слух, что один из них тайно является городским судьёй.

Если городской судья существует, то:
Городской судья никому не доверяет.
Все, кроме городского судьи, доверяют городскому судье.
Существует ровно один человек, который удовлетворяет свойствам 1 и 2.
Дан массив trust, где trust[i] = [a_i, b_i] означает, что человек с номером a_i доверяет человеку с номером b_i.
Если отношения доверия нет в массиве trust, значит такого отношения доверия не существует.

Верните номер городского судьи, если он существует и его можно определить.
Иначе верните -1.

Пример 1:
Вход: n = 2, trust = [[1,2]]
Выход: 2

Пример 2:
Вход: n = 3, trust = [[1,3],[2,3]]
Выход: 3

Пример 3:
Вход: n = 3, trust = [[1,3],[2,3],[3,1]]
Выход: -1

Ограничения:
1 <= n <= 1000
0 <= trust.length <= 10^4
trust[i].length == 2
Все пары trust уникальны.
a_i != b_i
1 <= a_i, b_i <= n
*/

func findJudge(n int, trust [][]int) int {
    // Создаем срез для подсчета «чистого доверия» (in-degree минус out-degree).
    // Размер n+1, чтобы индексы массива соответствовали номерам людей (от 1 до n).
    delta := make([]int, n+1)

    // Проходим по всем записям о доверии.
    // Каждый элемент t — это пара [a, b], где a доверяет b.
    for _, t := range trust {
        // Уменьшаем счетчик тому, кто доверяет.
        // Судья не может никому доверять, поэтому его счетчик не должен уменьшаться.
        delta[t[0]]--
        
        // Увеличиваем счетчик тому, кому доверяют.
        // Судье должны доверять все остальные жители.
        delta[t[1]]++
    }

    // Проверяем каждого человека от 1 до n.
    for i := 1; i <= n; i++ {
        // Единственный человек, чей счетчик может быть равен n-1 — это судья.
        // Это означает, что ему доверяют n-1 человек, а сам он не доверил никому 
        // (иначе счетчик был бы уменьшен и стал бы меньше n-1).
        if delta[i] == n-1 {
            return i // Судья найден, возвращаем его номер
        }
    }

    // Если никого с показателем n-1 не найдено, судьи в городе нет.
    return -1
}
