package main

/*
Строка s называется счастливой, если выполняются следующие условия:
s содержит только буквы 'a', 'b' и 'c'.
s не содержит подстрок "aaa", "bbb" или "ccc".
s содержит не более a вхождений буквы 'a'.
s содержит не более b вхождений буквы 'b'.
s содержит не более c вхождений буквы 'c'.
Даны три целых числа a, b и c.
Верните самую длинную возможную счастливую строку.
Если существует несколько самых длинных счастливых строк, верните любую из них.
Если такой строки не существует, верните пустую строку "".

Подстрока — это непрерывная последовательность символов внутри строки.

Пример 1:
Вход: a = 1, b = 1, c = 7
Выход: "ccaccbcc"
Пояснение: "ccbccacc" тоже был бы корректным ответом.

Пример 2:
Вход: a = 7, b = 1, c = 0
Выход: "aabaa"
Пояснение: В этом случае это единственный корректный ответ.

Ограничения:
0 <= a, b, c <= 100
a + b + c > 0
*/

import "container/heap"

// MaxHeap — максимальная куча (priority queue) на основе среза пар [количество, символ].
// Используется для жадного выбора символа с наибольшим оставшимся количеством.
type MaxHeap [][]int

// Len возвращает количество элементов в куче (требуется интерфейсом heap.Interface).
func (h MaxHeap) Len() int { return len(h) }

// Less определяет порядок: чем больше количество (h[i][0]), тем «выше» элемент в куче.
// Это делает кучу максимальной (max-heap).
func (h MaxHeap) Less(i, j int) bool { return h[i][0] > h[j][0] }

// Swap меняет местами два элемента кучи.
func (h MaxHeap) Swap(i, j int) { h[i], h[j] = h[j], h[i] }

// Push добавляет новый элемент в кучу. Вызывается heap.Push().
func (h *MaxHeap) Push(x interface{}) {
	*h = append(*h, x.([]int))
}

// Pop извлекает и возвращает последний элемент (после того, как heap «сдвинул»
// максимум в конец). Вызывается heap.Pop().
func (h *MaxHeap) Pop() interface{} {
	old := *h
	n := len(old)
	x := old[n-1]
	*h = old[0 : n-1] // отрезаем последний элемент
	return x
}

// Алгоритм (жадный):
//  1. Кладём все ненулевые счётчики в max-heap.
//  2. На каждом шаге берём символ с максимальным остатком.
//  3. Если добавление этого символа создаст тройку подряд (XXX),
//     то вместо него берём второй по величине символ из кучи.
//  4. Уменьшаем счётчик использованного символа и возвращаем его в кучу,
//     если остались неиспользованные экземпляры.
func longestDiverseString(a int, b int, c int) string {
	res := []byte{}        // результирующая строка в виде среза байт
	maxHeap := &MaxHeap{}  // куча приоритетов [count, charCode]
	heap.Init(maxHeap)     // инициализируем кучу

	// Добавляем в кучу только те символы, количество которых > 0.
	if a > 0 {
		heap.Push(maxHeap, []int{a, 'a'})
	}
	if b > 0 {
		heap.Push(maxHeap, []int{b, 'b'})
	}
	if c > 0 {
		heap.Push(maxHeap, []int{c, 'c'})
	}

	// Продолжаем, пока в куче есть доступные символы.
	for maxHeap.Len() > 0 {
		// Извлекаем символ с наибольшим оставшимся количеством.
		first := heap.Pop(maxHeap).([]int)
		count, ch := first[0], byte(first[1])

		// Проверяем, не образуются ли три одинаковых символа подряд.
		if len(res) > 1 && res[len(res)-1] == ch && res[len(res)-2] == ch {
			// Если других символов в куче нет — строим строку дальше невозможно.
			if maxHeap.Len() == 0 {
				break
			}
			// Берём второй по величине символ, чтобы «разбавить» повторяющуюся пару.
			second := heap.Pop(maxHeap).([]int)
			count2, ch2 := second[0], byte(second[1])
			res = append(res, ch2)
			count2--
			if count2 > 0 {
				heap.Push(maxHeap, []int{count2, int(ch2)})
			}
			// Возвращаем «заблокированный» первый символ обратно в кучу —
			// он будет использован на следующем шаге.
			heap.Push(maxHeap, []int{count, int(ch)})
		} else {
			// Тройки не будет — просто добавляем символ.
			res = append(res, ch)
			count--
			if count > 0 {
				heap.Push(maxHeap, []int{count, int(ch)})
			}
		}
	}

	return string(res)
}