package main

/*
Дан массив nums размером n. Верните мажоритарный элемент.

Мажоритарный элемент — это элемент, который встречается более ⌊n / 2⌋ раз.
Гарантируется, что мажоритарный элемент в массиве всегда существует.

Пример 1:
Вход: nums = [3,2,3]
Выход: 3

Пример 2:
Вход: nums = [2,2,1,1,1,2,2]
Выход: 2

Ограничения:
n == nums.length
1 <= n <= 5 * 104
-109 <= nums[i] <= 109
Входные данные гарантируют существование мажоритарного элемента в массиве.

*/

// Реализует алгоритм голосования Бойера-Мура (Boyer-Moore Voting Algorithm).
func majorityElement(nums []int) int {
	// res — текущий кандидат на роль мажоритарного элемента
	// count — баланс "голосов": увеличивается при совпадении с кандидатом, уменьшается при различии
	res, count := 0, 0

	for _, num := range nums {
		// Если баланс голосов обнулился, "забываем" старого кандидата 
		// и выбираем текущий элемент как нового претендента
		if count == 0 {
			res = num
		}
		// Если текущий элемент совпадает с кандидатом, увеличиваем баланс
		// иначе уменьшаем (отменяем один голос за кандидата)
		if num == res {
			count++
		} else {
			count--
		}
	}
	// После полного прохода по массиву в res гарантированно остаётся мажоритарный элемент
	return res
}
