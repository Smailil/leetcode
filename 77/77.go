package main

/*
Даны два целых числа n и k.
Верни все возможные комбинации из k чисел, выбранных из диапазона [1, n].

Ответ можно вернуть в любом порядке.

Пример 1:
Вход: n = 4, k = 2
Выход: [[1,2],[1,3],[1,4],[2,3],[2,4],[3,4]]
Пояснение: Из 4 выбрать 2 = 6 комбинаций всего.
Комбинации неупорядочены, то есть [1,2] и [2,1] считаются одной и той же комбинацией.

Пример 2:
Вход: n = 1, k = 1
Выход: [[1]]
Пояснение: Из 1 выбрать 1 = 1 комбинация всего.

Ограничения:
1 <= n <= 20
1 <= k <= n

*/

func combine(n int, k int) [][]int {
    // Инициализируем пустой срез для хранения всех найденных комбинаций
    res := [][]int{}

    // Объявляем функцию backtracking для рекурсивного построения комбинаций
    // start - следующее число, с которого можно начинать добавлять в комбинацию
    // comb - текущая строящаяся комбинация
    var backtrack func(start int, comb []int)
    backtrack = func(start int, comb []int) {
        // Базовый случай: если длина комбинации достигла k, сохраняем её в результат
        if len(comb) == k {
            // Создаем копию текущей комбинации, чтобы сохранить её состояние
            // Без копирования мы бы сохраняли ссылку на один и тот же срез,
            // который будет изменяться при дальнейших итерациях
            temp := make([]int, len(comb))
            copy(temp, comb)
            res = append(res, temp)
            return
        }

        // Перебираем все возможные числа от start до n
        for i := start; i <= n; i++ {
            // Добавляем текущее число в комбинацию
            comb = append(comb, i)
            
            // Рекурсивно строим комбинацию дальше, начиная с числа i+1
            // Это гарантирует, что числа в комбинации не повторяются и идут по возрастанию
            backtrack(i+1, comb)
            
            // Backtracking: удаляем последнее добавленное число,
            // чтобы попробовать другие варианты на этой позиции
            comb = comb[:len(comb)-1]
        }
    }

    // Запускаем backtracking с начального числа 1 и пустой комбинации
    backtrack(1, []int{})
    
    // Возвращаем все найденные комбинации
    return res
}