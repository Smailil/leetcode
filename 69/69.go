package main

/*
Дано неотрицательное целое число x — верните квадратный корень из x,
округлённый вниз до ближайшего целого.
Возвращаемое целое число тоже должно быть неотрицательным.

Нельзя использовать встроенные функции возведения в степень или соответствующие операторы.
Например, не используйте pow(x, 0.5) в C++ или x ** 0.5 в Python.

Пример 1:
Вход: x = 4
Выход: 2
Пояснение: Квадратный корень из 4 равен 2, поэтому возвращаем 2.

Пример 2:
Вход: x = 8
Выход: 2
Пояснение: Квадратный корень из 8 равен 2.82842...,
и поскольку мы округляем вниз до ближайшего целого, возвращается 2.

Ограничения:
0 <= x <= 2^31 - 1
*/

func mySqrt(x int) int {
    // Границы диапазона поиска: от 0 до x
    l, r := int64(0), int64(x)

    
    // Хранит текущий лучший кандидат на ответ (когда m*m < x)
    res := int64(0)

    // Бинарный поиск по отсортированному диапазону возможных корней
    for l <= r {
        // Вычисляем середину. Формула l + (r-l)/2 защищает от переполнения
        // при больших значениях l и r, в отличие от (l+r)/2
        m := l + (r-l)/2
		sqrtM := m*m
        if sqrtM > int64(x) {
            // Квадрат середины больше x → корень лежит левее
            r = m - 1
        } else if sqrtM < int64(x) {
            // Квадрат середины меньше x → корень лежит правее
            // Запоминаем m как потенциальный ответ (floor(sqrt(x)))
            l = m + 1
            res = m
        } else {
            // Точное совпадение: m*m == x
            return int(m)
        }
    }

    // Если точный корень не найден, возвращаем наибольшее целое,
    // квадрат которого не превышает x
    return int(res)
}
