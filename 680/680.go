/*
Дана строка s. Верните true, если из строки s можно получить палиндром,
удалив не более одного символа.

Пример 1:
Вход: s = "aba"
Выход: true

Пример 2:
Вход: s = "abca"
Выход: true
Пояснение: можно удалить символ 'c'.

Пример 3:
Вход: s = "abc"
Выход: false

Ограничения:
1 <= s.length <= 10^5
s состоит из строчных букв английского алфавита.
*/

func validPalindrome(s string) bool {
	isPalindrome := func(l, r int) bool {
		for l < r {
			if s[l] != s[r] {
				return false
			}
			l++
			r--
		}
		return true
	}

	l, r := 0, len(s)-1

	for l < r {
		// Если символы не совпадают, у нас есть только один шанс исправить строку:
		// удалить либо символ слева, либо символ справа.
		// Пробуем оба варианта через вспомогательную функцию:
		// 1. isPalindrome(l+1, r)  → пропускаем левый символ
		// 2. isPalindrome(l, r-1)  → пропускаем правый символ
		if s[l] != s[r] {
			return isPalindrome(l+1, r) || isPalindrome(l, r-1)
		}
		l++
		r--
	}

	// Если цикл завершился без несовпадений, строка уже является палиндромом.
	return true
}
