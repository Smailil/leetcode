package main

/*
Дан массив положительных целых чисел nums и положительное целое число target.
Верните минимальную длину подмассива, сумма которого больше или равна target.
Если такого подмассива не существует, верните 0.

Пример 1:
Вход: target = 7, nums = [2,3,1,2,4,3]
Выход: 2
Пояснение: Подмассив [4,3] имеет минимальную длину при данных ограничениях задачи.
Пример 2:
Вход: target = 4, nums = [1,4,4]
Выход: 1
Пример 3:
Вход: target = 11, nums = [1,1,1,1,1,1,1,1]
Выход: 0

Ограничения:
1 <= target <= 10^9
1 <= nums.length <= 10^5
1 <= nums[i] <= 10^4
*/

func minSubArrayLen(target int, nums []int) int {
	// l — левая граница окна, total — текущая сумма элементов в окне
	l, total := 0, 0
	
	// res хранит минимальную найденную длину. 
	// Инициализируем значением, превышающим любую возможную длину подмассива.
	res := len(nums) + 1

	// r — правая граница окна. Последовательно расширяем окно вправо.
	for r := range nums {
		total += nums[r] // Добавляем новый элемент в текущую сумму

		// Пока сумма в окне удовлетворяет условию (>= target),
		// пытаемся сократить окно слева, чтобы найти минимальную длину.
		for total >= target {
			// Обновляем результат, если текущее окно короче предыдущего минимума
			if r-l+1 < res {
				res = r - l + 1
			}
			total -= nums[l] // Исключаем левый элемент из суммы
			l++              // Сдвигаем левую границу окна вправо
		}
	}

	// Если res не был обновлён, значит ни один подмассив не набрал нужную сумму
	if res == len(nums)+1 {
		return 0
	}
	return res
}