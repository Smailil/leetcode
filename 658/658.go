package main

/*
Дан отсортированный массив целых чисел arr, два целых числа k и x.
Верни k ближайших к x целых чисел из массива.
Результат должен быть отсортирован по возрастанию.

Целое число a ближе к x, чем целое число b, если:

|a - x| < |b - x|, или
|a - x| == |b - x| и a < b

Пример 1:
Вход: arr = [1,2,3,4,5], k = 4, x = 3
Выход: [1,2,3,4]

Пример 2:
Вход: arr = [1,1,2,3,4,5], k = 4, x = -1
Выход: [1,1,2,3]

Ограничения:
1 <= k <= arr.length
1 <= arr.length <= 10^4
arr отсортирован по возрастанию.
-10^4 <= arr[i], x <= 10^4
*/

func findClosestElements(arr []int, k int, x int) []int {
	// Ищем начальный индекс окна длины k.
	// Диапазон возможных стартовых позиций: [0, len(arr)-k]
	l, r := 0, len(arr)-k

	// Бинарный поиск оптимального начального индекса
	for l < r {
		m := (l + r) / 2

		// Сравниваем расстояния от x до левого края текущего окна (arr[m])
		// и до элемента сразу за правым краем окна (arr[m+k]).
		//
		// x - arr[m]     -> расстояние до первого элемента окна
		// arr[m+k] - x   -> расстояние до следующего элемента после окна
		if x-arr[m] > arr[m+k]-x {
			// Элемент arr[m+k] ближе к x, чем arr[m].
			// Значит, оптимальное окно должно начинаться правее m.
			l = m + 1
		} else {
			// arr[m] ближе или расстояния равны.
			// Оптимальное окно начинается в m или левее.
			// При равенстве расстояний оставляем l, чтобы гарантировать
			// возврат подмассива с меньшими элементами (требование задачи).
			r = m
		}
	}

	// l и r сошлись в оптимальном стартовом индексе.
	// Возвращаем подмассив длины k.
	return arr[l : l+k]
}
