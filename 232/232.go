/*
Реализуйте очередь FIFO (первым вошёл — первым вышел), используя только два стека.
Реализованная очередь должна поддерживать все стандартные операции обычной очереди
(push, peek, pop и empty).

Реализуйте класс MyQueue:

void push(int x) — добавляет элемент x в конец очереди.
int pop() — удаляет элемент из начала очереди и возвращает его.
int peek() — возвращает элемент в начале очереди.
boolean empty() — возвращает true, если очередь пуста, иначе false.

Примечания:

Разрешено использовать только стандартные операции стека: добавление на вершину,
просмотр/удаление с вершины, получение размера и проверка на пустоту.
В зависимости от языка программирования стек может не поддерживаться нативно.
Вы можете симулировать стек с помощью списка или дека (двусторонней очереди),
используя только стандартные операции стека.

Пример 1:

Вход
["MyQueue", "push", "push", "peek", "pop", "empty"]
[[], [1], [2], [], [], []]
Выход
[null, null, null, 1, 1, false]

Пояснение
MyQueue myQueue = new MyQueue();
myQueue.push(1); // очередь: [1]
myQueue.push(2); // очередь: [1, 2] (левый элемент — начало очереди)
myQueue.peek(); // вернёт 1
myQueue.pop(); // вернёт 1, очередь: [2]
myQueue.empty(); // вернёт false

Ограничения:
1 <= x <= 9
К методам push, pop, peek и empty будет сделано не более 100 вызовов.
Все вызовы pop и peek гарантированно корректны.

*/

// MyQueue реализует очередь (FIFO) с использованием двух стеков,
// представленных срезами Go.
// s1 — входной стек: принимает все новые элементы при вызове Push.
// s2 — выходной стек: отдаёт элементы в порядке очереди при Pop и Peek.
// Перенос элементов из s1 в s2 происходит только когда s2 пуст,
// что обеспечивает амортизированную сложность O(1) для операций Pop/Peek.
type MyQueue struct {
	s1 []int
	s2 []int
}

// Constructor создаёт и возвращает новую пустую очередь.
func Constructor() MyQueue {
	return MyQueue{
		s1: []int{},
		s2: []int{},
	}
}

// Push добавляет элемент x в конец очереди.
// Элемент просто помещается в конец среза s1 (имитация push в стек).
// Временная сложность: O(1).
func (this *MyQueue) Push(x int) {
	this.s1 = append(this.s1, x)
}

// Pop удаляет и возвращает элемент из начала очереди.
// Если выходной стек s2 пуст, все элементы из s1 переносятся в s2.
// При переносе порядок элементов меняется на обратный, что превращает
// LIFO-поведение стека в FIFO-поведение очереди.
// Амортизированная временная сложность: O(1).
func (this *MyQueue) Pop() int {
	// Переносим элементы из входного стека в выходной только при необходимости
	if len(this.s2) == 0 {
		for len(this.s1) > 0 {
			// Извлекаем верхний элемент s1 и помещаем его в s2
			this.s2 = append(this.s2, this.s1[len(this.s1)-1])
			this.s1 = this.s1[:len(this.s1)-1]
		}
	}
	
	// Запоминаем верхний элемент s2 (это и есть начало очереди)
	res := this.s2[len(this.s2)-1]
	// Удаляем его из s2
	this.s2 = this.s2[:len(this.s2)-1]
	return res
}

// Peek возвращает элемент из начала очереди без его удаления.
// Использует ту же стратегию ленивого переноса элементов, что и Pop.
// Амортизированная временная сложность: O(1).
func (this *MyQueue) Peek() int {
	if len(this.s2) == 0 {
		for len(this.s1) > 0 {
			this.s2 = append(this.s2, this.s1[len(this.s1)-1])
			this.s1 = this.s1[:len(this.s1)-1]
		}
	}
	// Возвращаем верхний элемент s2, не изменяя структуру стека
	return this.s2[len(this.s2)-1]
}

// Empty проверяет, пуста ли очередь.
// Очередь считается пустой только когда оба стека не содержат элементов.
// Временная сложность: O(1).
func (this *MyQueue) Empty() bool {
	return len(this.s1) == 0 && len(this.s2) == 0
}

