package main

/*
Дан массив целых чисел stones, где stones[i] — вес i-го камня.

Мы играем в игру с камнями. На каждом ходу выбираем любые два камня
и разбиваем их друг о друга. Пусть веса камней равны x и y, где x <= y.
Результат такого удара:
Если x == y, оба камня уничтожаются.
Если x != y, камень веса x уничтожается,
а камень веса y получает новый вес y - x.
В конце игры остаётся не более одного камня.

Верните минимально возможный вес оставшегося камня.
Если камней не осталось, верните 0.

Пример 1:
Вход: stones = [2,7,4,1,8,1]
Выход: 1
Пояснение:
Можно объединить 2 и 4 и получить 2,
поэтому массив становится [2,7,1,8,1].
Затем можно объединить 7 и 8 и получить 1,
поэтому массив становится [2,1,1,1].
Затем можно объединить 2 и 1 и получить 1,
поэтому массив становится [1,1,1].
Затем можно объединить 1 и 1 и получить 0,
поэтому массив становится [1]; это оптимальное значение.

Пример 2:
Вход: stones = [31,26,33,21,40]
Выход: 5

Ограничения:
1 <= stones.length <= 30
1 <= stones[i] <= 100
*/

func lastStoneWeightII(stones []int) int {
    // Вычисляем общую сумму всех камней
    stoneSum := 0
    for _, stone := range stones {
        stoneSum += stone
    }
    
    // Наша цель: найти подмножество камней с суммой, максимально близкой к stoneSum/2
    // Это позволит минимизировать разницу между двумя группами камней
    target := stoneSum / 2
    
    // dp[t] - максимальная сумма, которую можно получить из камней,
    // не превышая вес t (аналогично задаче о рюкзаке)
    dp := make([]int, target+1)

    // Для каждого камня пытаемся добавить его в возможные комбинации
    for _, stone := range stones {
        // Идем в обратном порядке, чтобы каждый камень использовался только один раз
        // (это важно для задачи 0/1 knapsack, а не unbounded knapsack)
        for t := target; t >= stone; t-- {
            // Проверяем: можем ли мы улучшить dp[t], добавив текущий камень?
            // dp[t-stone] + stone - сумма, если мы добавим камень к предыдущей комбинации
            if dp[t-stone]+stone > dp[t] {
                dp[t] = dp[t-stone] + stone
            }
        }
    }

    // dp[target] содержит максимальную сумму одной группы (близкую к stoneSum/2)
    // Вторая группа будет иметь вес: stoneSum - dp[target]
    // Разница между группами: (stoneSum - dp[target]) - dp[target] = stoneSum - 2*dp[target]
    // Это и есть минимальный вес последнего камня
    return stoneSum - 2*dp[target]
}