package main

/*
Дана сетка размером row x col, представляющая карту,
где grid[i][j] = 1 означает сушу,
а grid[i][j] = 0 означает воду.

Клетки сетки соединяются по горизонтали или вертикали,
но не по диагонали.
Сетка полностью окружена водой, и в ней ровно один остров,
то есть одна или несколько соединённых клеток суши.

На острове нет «озёр»: вода внутри острова не соединена
с водой вокруг него.
Одна клетка — это квадрат со стороной 1.
Сетка прямоугольная, ширина и высота не превышают 100.
Определите периметр острова.

Пример 1:
https://assets.leetcode.com/uploads/2018/10/12/island.png

Вход: grid = [[0,1,0,0],[1,1,1,0],[0,1,0,0],[1,1,0,0]]
Выход: 16
Пояснение: периметр — это 16 жёлтых полос на изображении выше.

Пример 2:
Вход: grid = [[1]]
Выход: 4

Пример 3:
Вход: grid = [[1,0]]
Выход: 4

Ограничения:
row == grid.length
col == grid[i].length
1 <= row, col <= 100
grid[i][j] равно 0 или 1.
В grid ровно один остров.
*/

func islandPerimeter(grid [][]int) int {
    // Получаем размеры сетки: количество строк (m) и столбцов (n)
    m, n := len(grid), len(grid[0])
    
    // Переменная для накопления общего периметра острова
    res := 0
    
    // Проходим по каждой клетке сетки построчно
    for i := range m {
        for j := range n {
            
            // Рассматриваем только клетки суши (1)
            if grid[i][j] == 1 {
                
                // 1. Проверяем нижнюю сторону клетки
                // Если мы вышли за нижнюю границу сетки (i+1 >= m) 
                // ИЛИ клетка снизу — это вода (grid[i+1][j] == 0),
                // значит, эта сторона является частью внешнего периметра.
                if i+1 >= m || grid[i+1][j] == 0 {
                    res++
                }
                
                // 2. Проверяем правую сторону клетки
                // Аналогично: выход за правую границу или вода справа
                if j+1 >= n || grid[i][j+1] == 0 {
                    res++
                }
                
                // 3. Проверяем верхнюю сторону клетки
                // Выход за верхнюю границу (i-1 < 0) или вода сверху
                if i-1 < 0 || grid[i-1][j] == 0 {
                    res++
                }
                
                // 4. Проверяем левую сторону клетки
                // Выход за левую границу (j-1 < 0) или вода слева
                if j-1 < 0 || grid[i][j-1] == 0 {
                    res++
                }
            }
        }
    }
    
    // Возвращаем итоговый периметр
    return res
}

