package main

import "container/heap"

/*
Вы — турист, готовитесь к предстоящему походу.
Вам дан `heights`, двумерный массив размера `rows x columns`,
где `heights[row][col]` обозначает высоту клетки `(row, col)`.
Вы находитесь в верхней левой клетке `(0, 0)` и хотите добраться
до нижней правой клетки `(rows - 1, columns - 1)`,
то есть индексация начинается с 0.
Можно двигаться вверх, вниз, влево или вправо.
Нужно найти маршрут, требующий минимального усилия.

Усилие маршрута — это максимальная абсолютная разница высот
между двумя соседними клетками маршрута.

Верните минимальное усилие, необходимое, чтобы добраться
из верхней левой клетки в нижнюю правую.

Пример 1:
https://assets.leetcode.com/uploads/2020/10/04/ex1.png
Вход: heights = [[1,2,2],[3,8,2],[5,3,5]]
Выход: 2
Пояснение: маршрут [1,3,5,3,5] имеет максимальную абсолютную разницу
между соседними клетками, равную 2.
Это лучше, чем маршрут [1,2,2,2,5], где максимальная абсолютная разница равна 3.

Пример 2:
https://assets.leetcode.com/uploads/2020/10/04/ex2.png
Вход: heights = [[1,2,3],[3,8,4],[5,3,5]]
Выход: 1
Пояснение: маршрут [1,2,3,4,5] имеет максимальную абсолютную разницу
между соседними клетками, равную 1, что лучше маршрута [1,3,5,3,5].

Пример 3:
https://assets.leetcode.com/uploads/2020/10/04/ex3.png
Вход: heights = [[1,2,1,1,1],[1,2,1,2,1],[1,2,1,2,1],[1,2,1,2,1],[1,1,1,2,1]]
Выход: 0
Пояснение: этот маршрут не требует никакого усилия.

Ограничения:
rows == heights.length
columns == heights[i].length
1 <= rows, columns <= 100
1 <= heights[i][j] <= 10^6
*/

// Item описывает элемент, который будет храниться в куче.
// Представляет собой ячейку сетки и "усилие" (стоимость), затраченное на путь до неё.
type Item struct {
	diff int // Максимальная разница высот на пути от старта до этой ячейки
	r, c int // Координаты ячейки (строка и столбец)
}

// MinHeap — это тип данных для минимальной кучи (min-heap).
// Элемент с наименьшим значением `diff` всегда будет находиться в корне (извлекаться первым).
type MinHeap []Item

// Len возвращает количество элементов в куче (требуется интерфейсом heap.Interface)
func (h MinHeap) Len() int { return len(h) }

// Less определяет правило сортировки.
// Возвращает true, если элемент i должен находиться выше элемента j (меньшее усилие приоритетнее).
func (h MinHeap) Less(i, j int) bool { return h[i].diff < h[j].diff }

// Swap меняет местами два элемента в куче
func (h MinHeap) Swap(i, j int) { h[i], h[j] = h[j], h[i] }

// Push добавляет новый элемент в конец слайса (кучи)
func (h *MinHeap) Push(x any) { *h = append(*h, x.(Item)) }

// Pop удаляет и возвращает последний элемент слайса.
// Пакет container/heap сам перестраивает кучу так, чтобы корнем стал следующий минимальный элемент.
func (h *MinHeap) Pop() any {
	old := *h
	n := len(old)
	x := old[n-1]     // Забираем последний элемент
	*h = old[0 : n-1] // Обрезаем слайс, уменьшая размер кучи на 1
	return x
}

func minimumEffortPath(heights [][]int) int {
	rows, cols := len(heights), len(heights[0])
	
	// Матрица dist хранит минимально известное "усилие" для достижения каждой клетки.
	dist := make([][]int, rows)
	for i := range dist {
		dist[i] = make([]int, cols)
		for j := range dist[i] {
			// Изначально "усилие" для всех клеток равно "бесконечности" (максимальное значение int32)
			dist[i][j] = 1<<31 - 1
		}
	}
	// Усилие для стартовой клетки равно 0, так как мы уже в ней находимся
	dist[0][0] = 0

	// Массив возможных направлений движения: вправо, влево, вниз, вверх
	directions := [][]int{{0, 1}, {0, -1}, {1, 0}, {-1, 0}}

	// Инициализируем кучу, добавляя стартовую клетку с нулевым усилием
	minHeap := &MinHeap{Item{0, 0, 0}}
	heap.Init(minHeap) // Обязательно вызываем Init для корректной работы структуры кучи

	// Основной цикл алгоритма Дейкстры: обрабатываем клетки, пока куча не пуста
	for minHeap.Len() > 0 {
		// Извлекаем клетку с минимальным текущим усилием
		curr := heap.Pop(minHeap).(Item)
		diff, r, c := curr.diff, curr.r, curr.c

		// Если мы достигли целевой клетки (нижний правый угол), возвращаем результат.
		// Благодаря свойствам min-heap, первый раз, когда мы достаём финишную клетку,
		// это гарантированно будет оптимальный (минимальный) путь.
		if r == rows-1 && c == cols-1 {
			return diff
		}

		// Оптимизация: если извлеченное усилие больше, чем то, что мы уже записали в dist,
		// значит, в куче находится устаревшая (неактуальная) запись. Просто пропускаем её.
		if dist[r][c] < diff {
			continue
		}

		// Перебираем всех соседей текущей клетки
		for _, dir := range directions {
			newR, newC := r+dir[0], c+dir[1]
			
			// Проверяем, не вышли ли мы за границы сетки
			if newR < 0 || newC < 0 || newR >= rows || newC >= cols {
				continue
			}

			// Вычисляем новое усилие для перехода к соседу.
			// Оно равно максимуму между усилием текущего пути и абсолютной разницей высот.
			newDiff := max(diff, abs(heights[r][c]-heights[newR][newC]))
			
			// Если мы нашли более "легкий" путь к соседу, обновляем dist и добавляем его в кучу
			if newDiff < dist[newR][newC] {
				dist[newR][newC] = newDiff
				heap.Push(minHeap, Item{newDiff, newR, newC})
			}
		}
	}

	return 0 // Заглушка (в условиях этой задачи путь существует всегда)
}

// abs возвращает модуль числа (абсолютное значение)
func abs(x int) int {
	if x < 0 {
		return -x
	}
	return x
}
