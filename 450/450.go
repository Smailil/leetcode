package main

/*
Дана ссылка на корневой узел BST и ключ.
Удалите узел с данным ключом из BST.
Верните ссылку на корневой узел (возможно обновлённого) дерева.

В целом удаление можно разделить на два этапа:

Поиск узла для удаления.
Если узел найден — удалить его.


Пример 1:

https://assets.leetcode.com/uploads/2020/09/04/del_node_1.jpg
Вход: root = [5,3,6,2,4,null,7], key = 3
Выход: [5,4,6,2,null,null,7]
Пояснение: Данный ключ для удаления — 3.
Находим узел со значением 3 и удаляем его.
Один допустимый ответ — [5,4,6,2,null,null,7], показан на BST выше.
Другой допустимый ответ — [5,2,6,null,4,null,7] — тоже принимается.
https://assets.leetcode.com/uploads/2020/09/04/del_node_supp.jpg

Пример 2:

Вход: root = [5,3,6,2,4,null,7], key = 0
Выход: [5,3,6,2,4,null,7]
Пояснение: Дерево не содержит узла со значением = 0.

Пример 3:

Вход: root = [], key = 0
Выход: []


Ограничения:

Количество узлов в дереве в диапазоне [0, 10^4].
-10^5 <= Node.val <= 10^5
Каждый узел имеет уникальное значение.
root является корректным двоичным деревом поиска.
-10^5 <= key <= 10^5

*/

/**
 * Определение узла двоичного дерева.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */

func deleteNode(root *TreeNode, key int) *TreeNode {
    // Базовый случай: если дерево (или поддерево) пустое, удалять нечего.
    if root == nil {
        return nil
    }

    // Используем свойства бинарного дерева поиска (BST) для поиска узла:
    
    // Если искомый ключ больше текущего значения, узел находится в правом поддереве.
    if key > root.Val {
        root.Right = deleteNode(root.Right, key)
        
    // Если искомый ключ меньше текущего значения, узел находится в левом поддереве.
    } else if key < root.Val {
        root.Left = deleteNode(root.Left, key)
        
    } else {
        // Мы нашли узел, который нужно удалить (key == root.Val).
        
        // Случай 1: У узла нет левого потомка (или нет потомков вообще).
        // Заменяем удаляемый узел его правым потомком (или nil, если правого тоже нет).
        if root.Left == nil {
            return root.Right
        }
        
        // Случай 2: У узла нет правого потомка.
        // Заменяем удаляемый узел его левым потомком.
        if root.Right == nil {
            return root.Left
        }

        // Случай 3: У узла есть оба потомка.
        // Чтобы сохранить свойства BST, нам нужно заменить значение текущего узла
        // на значение его inorder-преемника (inorder successor).
        // Преемник — это самый маленький (левый) узел в правом поддереве.
        cur := root.Right
        for cur.Left != nil {
            cur = cur.Left // Спускаемся до упора влево
        }
        
        // Копируем значение найденного преемника в текущий узел.
        root.Val = cur.Val
        
        // Теперь рекурсивно удаляем сам узел-преемник из правого поддерева,
        // так как его значение уже скопировано и он больше не нужен на старом месте.
        // (Мы точно знаем, что у преемника нет левого потомка, поэтому это будет Случай 1 или 2).
        root.Right = deleteNode(root.Right, root.Val)
    }

    // Возвращаем указатель на корень (он мог измениться, если удалялся сам корень).
    return root
}
