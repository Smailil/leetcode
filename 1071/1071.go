package main

/*
Для двух строк s и t будем говорить, что «t делит s» тогда и только тогда,
когда s = t + t + t + ... + t + t, то есть строка t объединяется сама
с собой один или несколько раз.

Даны две строки str1 и str2.
Верните наибольшую строку x, которая делит как str1, так и str2.

Пример 1:
Вход: str1 = "ABCABC", str2 = "ABC"
Выход: "ABC"

Пример 2:
Вход: str1 = "ABABAB", str2 = "ABAB"
Выход: "AB"

Пример 3:
Вход: str1 = "LEET", str2 = "CODE"
Выход: ""

Пример 4:
Вход: str1 = "AAAAAB", str2 = "AAA"
Выход: ""

Ограничения:
1 <= str1.length, str2.length <= 1000
Строки str1 и str2 состоят из заглавных букв английского алфавита.
*/

// gcdOfStrings ищет самый длинный общий повторяющийся блок двух строк.
// Длина такого блока должна делить длины обеих строк, поэтому максимальный
// возможный кандидат имеет длину gcd(len(str1), len(str2)). После вычисления
// этой длины остаётся проверить, что обе строки действительно состоят
// из повторений одного и того же префикса str1.
//
// Время: O(len(str1) + len(str2)).
// Дополнительная память: O(log(min(len(str1), len(str2)))) из-за рекурсии gcd.
func gcdOfStrings(str1 string, str2 string) string {
    // Вычисляем НОД длин алгоритмом Евклида. На каждом шаге пара (a, b)
    // заменяется на (b, a % b), пока второй аргумент не станет нулём.
    var gcd func(a, b int) int
    gcd = func(a, b int) int {
        if b == 0 {
            return a
        }
        return gcd(b, a%b)
    }

    // По ограничениям обе строки непустые, поэтому g > 0 и операция i % g
    // в последующих циклах безопасна.
    g := gcd(len(str1), len(str2))

    // Проверяем, что str1 периодична с периодом g. Индекс i % g сопоставляет
    // каждый символ строки с соответствующим символом первого блока.
    for i := 0; i < len(str1); i++ {
        if str1[i] != str1[i%g] {
            return ""
        }
    }

    // Для str2 используем тот же эталонный блок из str1. Так проверяется
    // не только периодичность второй строки, но и совпадение общего делителя.
    for i := 0; i < len(str2); i++ {
        if str2[i] != str1[i%g] {
            return ""
        }
    }

    // Обе проверки пройдены: префикс максимальной допустимой длины делит
    // каждую из строк.
    return str1[:g]
}
