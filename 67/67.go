package main

/*
Даны две двоичные строки a и b.
Верните их сумму в виде двоичной строки.

Пример 1:
Вход: a = "11", b = "1"
Выход: "100"

Пример 2:
Вход: a = "1010", b = "1011"
Выход: "10101"

Ограничения:
1 <= a.length, b.length <= 10^4
Строки a и b состоят только из символов '0' и '1'.
Каждая строка не содержит ведущих нулей, кроме самой строки "0".
*/

func addBinary(a string, b string) string {
    // Разряды добавляются от младшего к старшему, поэтому до финального
    // разворота res хранит результат в обратном порядке.
    res := []byte{}

    // carry — перенос из уже обработанного младшего разряда.
    // Он всегда равен 0 или 1.
    carry := 0

    // Начинаем с правых концов строк, где находятся младшие разряды.
    // Отдельные индексы позволяют складывать строки разной длины.
    i, j := len(a)-1, len(b)-1

    // Даже после обработки обеих строк может остаться перенос.
    // Например, для "1" + "1" он создаёт старший разряд результата "10".
    for i >= 0 || j >= 0 || carry > 0 {
        // Если одна строка закончилась раньше, её отсутствующий разряд
        // считается нулём и не влияет на сумму.
        digitA := 0
        digitB := 0
        if i >= 0 {
            digitA = int(a[i] - '0')
        }
        if j >= 0 {
            digitB = int(b[j] - '0')
        }

        // Складываем два текущих бита и перенос. Остаток от деления на 2
        // образует бит результата, а целая часть становится новым переносом.
        total := digitA + digitB + carry
        res = append(res, byte(total%2)+'0')
        carry = total / 2

        // Переходим к следующим, более старшим разрядам обеих строк.
        i--
        j--
    }

    // Биты были добавлены от младшего к старшему, поэтому разворачиваем
    // срез на месте и получаем привычную запись числа.
    for l, r := 0, len(res)-1; l < r; l, r = l+1, r-1 {
        res[l], res[r] = res[r], res[l]
    }

    return string(res)
}
