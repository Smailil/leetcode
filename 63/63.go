package main

/*
Дан целочисленный массив grid размера m x n.
Робот изначально находится в левом верхнем углу, то есть в grid[0][0].
Робот пытается добраться до правого нижнего угла, то есть до grid[m - 1][n - 1].
В любой момент времени робот может двигаться только вниз или вправо.

Препятствие и свободная клетка обозначены в grid как 1 и 0 соответственно.
Путь, по которому движется робот, не может включать клетки с препятствиями.

Верните количество возможных уникальных путей, по которым робот может добраться
до правого нижнего угла.

Тесты сгенерированы так, что ответ будет меньше или равен 2 * 10^9.

Пример 1:
https://assets.leetcode.com/uploads/2020/11/04/robot1.jpg
Вход: obstacleGrid = [[0,0,0],[0,1,0],[0,0,0]]
Выход: 2
Пояснение: В середине приведенной выше сетки 3x3 есть одно препятствие.
Есть два способа добраться до правого нижнего угла:
1. Вправо -> Вправо -> Вниз -> Вниз
2. Вниз -> Вниз -> Вправо -> Вправо

Пример 2:
https://assets.leetcode.com/uploads/2020/11/04/robot2.jpg
Вход: obstacleGrid = [[0,1],[0,0]]
Выход: 1

Ограничения:
m == obstacleGrid.length
n == obstacleGrid[i].length
1 <= m, n <= 100
obstacleGrid[i][j] равно 0 или 1.
*/

func uniquePathsWithObstacles(grid [][]int) int {
    // Получаем размеры сетки: M - количество строк, N - количество столбцов
    M, N := len(grid), len(grid[0])
    
    // dp будет хранить количество путей от текущей клетки до финиша.
    // Размер N+1 сделан для того, чтобы избежать выхода за границы массива (index out of bounds)
    // при обращении к dp[c+1], когда мы находимся в крайнем правом столбце (где c = N-1).
    // dp[N] всегда равен 0, что логично, так как идти вправо от правой границы невозможно.
    dp := make([]int, N+1)
    
    // Изначально предполагаем, что из финишной клетки (правый нижний угол) есть 1 путь до самого себя.
    // Если в финишной клетке окажется препятствие, оно корректно обнулится на первой же итерации цикла.
    dp[N-1] = 1

    // Итерируемся по сетке снизу вверх (от M-1 до 0).
    // Это позволяет использовать одномерный массив, так как для вычисления текущей строки 
    // нам нужна информация только из строки, находящейся непосредственно под ней.
    for r := M - 1; r >= 0; r-- {
        // Внутри каждой строки идем справа налево (от N-1 до 0)
        for c := N - 1; c >= 0; c-- {
            // Если в текущей клетке находится препятствие,
            // то пройти через неё нельзя, поэтому количество путей обнуляется.
            if grid[r][c] == 1 {
                dp[c] = 0
            } else {
                // Если препятствия нет, количество путей из текущей клетки равно
                // сумме путей из клетки снизу (текущее значение dp[c] с предыдущей итерации по r)
                // и клетки справа (значение dp[c+1], которое мы уже вычислили на текущей итерации).
                dp[c] += dp[c+1]
            }
        }
    }

    // В итоге dp[0] хранит общее количество путей от левого верхнего угла до правого нижнего
    return dp[0]
}
