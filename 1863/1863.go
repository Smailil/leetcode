package main

/*
XOR-сумма массива — побитовый XOR всех его элементов, или 0 для пустого массива.

Например, XOR-сумма массива [2,5,6] равна 2 XOR 5 XOR 6 = 1.
Дан массив nums. Верните сумму XOR-сумм всех подмножеств nums.

Примечание: подмножества с одинаковыми элементами считаются отдельно.

Массив a является подмножеством массива b,
если a можно получить из b удалением нуля или нескольких элементов.

Пример 1:
Вход: nums = [1,3]
Выход: 6
Пояснение: 4 подмножества [1,3]:
- Пустое подмножество имеет XOR-сумму 0.
- [1] имеет XOR-сумму 1.
- [3] имеет XOR-сумму 3.
- [1,3] имеет XOR-сумму 1 XOR 3 = 2.
0 + 1 + 3 + 2 = 6

Пример 2:
Вход: nums = [5,1,6]
Выход: 28
Пояснение: 8 подмножеств [5,1,6]:
- Пустое подмножество имеет XOR-сумму 0.
- [5] имеет XOR-сумму 5.
- [1] имеет XOR-сумму 1.
- [6] имеет XOR-сумму 6.
- [5,1] имеет XOR-сумму 5 XOR 1 = 4.
- [5,6] имеет XOR-сумму 5 XOR 6 = 3.
- [1,6] имеет XOR-сумму 1 XOR 6 = 7.
- [5,1,6] имеет XOR-сумму 5 XOR 1 XOR 6 = 2.
0 + 5 + 1 + 6 + 4 + 3 + 7 + 2 = 28

Пример 3:
Вход: nums = [3,4,5,6,7,8]
Выход: 480
Пояснение: Сумма XOR-сумм всех подмножеств равна 480.

Ограничения:
1 <= nums.length <= 12
1 <= nums[i] <= 20

*/

func subsetXORSum(nums []int) int {
    // Переменная res будет хранить битовую маску — побитовое ИЛИ всех элементов.
    res := 0

    // Проходим по всем числам массива.
    // Если какой-то бит равен 1 хотя бы в одном числе, он станет 1 и в переменной res.
    for _, num := range nums {
        res |= num
    }

    // МАТЕМАТИЧЕСКОЕ ОБОСНОВАНИЕ:
    // Пусть длина массива равна n. Всего существует 2^n различных подмножеств.
    // Если определенный бит установлен в 1 хотя бы в одном элементе массива,
    // то при переборе всех подмножеств этот бит окажется равен 1 ровно в половине 
    // из них (то есть в 2^(n-1) подмножествах). Если бит равен 0 во всех числах, 
    // он никогда не даст 1 в XOR-сумме.
    //
    // Таким образом, вклад каждого бита в общую сумму равен его значению, 
    // умноженному на 2^(n-1). 
    // В программировании умножение на степень двойки 2^(n-1) эквивалентно 
    // побитовому сдвигу влево на (n - 1).
    return res << (len(nums) - 1)
}
