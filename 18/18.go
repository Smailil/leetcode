package main

import "sort"

/*
Дан массив nums из n целых чисел.
Верните массив всех уникальных четвёрок [nums[a], nums[b], nums[c], nums[d]] таких, что:

0 <= a, b, c, d < n
a, b, c и d — попарно различны.
nums[a] + nums[b] + nums[c] + nums[d] == target
Ответ можно вернуть в любом порядке.

Пример 1:
Вход: nums = [1,0,-1,0,-2,2], target = 0
Выход: [[-2,-1,1,2],[-2,0,0,2],[-1,0,0,1]]

Пример 2:
Вход: nums = [2,2,2,2,2], target = 8
Выход: [[2,2,2,2]]

Ограничения:
1 <= nums.length <= 200
-10^9 <= nums[i] <= 10^9
-10^9 <= target <= 10^9
*/

func fourSum(nums []int, target int) [][]int {
	// Сортировка необходима для:
	// 1. Упрощённого пропуска дубликатов
	// 2. Работы двух указателей в базовом случае
	sort.Ints(nums)
	
	res := [][]int{} // Результирующий срез уникальных четвёрок
	quad := []int{}  // Временный срез для хранения текущей комбинации чисел

	// Рекурсивная функция для решения задачи k-Sum
	// k: сколько ещё чисел нужно подобрать
	// start: индекс, с которого начинаем перебор в отсортированном массиве
	// target: оставшаяся сумма, которую необходимо набрать
	var kSum func(k, start, target int)
	kSum = func(k, start, target int) {
		// Базовый случай: задача сводится к 2-Sum
		if k == 2 {
			l, r := start, len(nums)-1
			for l < r {
				sum := nums[l] + nums[r]
				if sum < target {
					l++ // Сумма меньше нужной, двигаем левый указатель вправо (увеличиваем сумму)
				} else if sum > target {
					r-- // Сумма больше нужной, двигаем правый указатель влево (уменьшаем сумму)
				} else {
					// Нашли пару, дающую нужную сумму
					// Важно создать копию quad, т.к. срез в Go ссылочный тип
					temp := make([]int, len(quad))
					copy(temp, quad)
					temp = append(temp, nums[l], nums[r])
					res = append(res, temp)

					// Сдвигаем оба указателя и пропускаем дубликаты
					l++
					r--
					for l < r && nums[l] == nums[l-1] {
						l++
					}
					for l < r && nums[r] == nums[r+1] {
						r--
					}
				}
			}
			return
		}

		// Рекурсивный случай: k > 2
		// Перебираем возможные первые числа для текущей комбинации.
		// Условие i <= len(nums)-k гарантирует, что справа останется минимум k-1 элемент.
		for i := start; i <= len(nums)-k; i++ {
			// Пропускаем дубликаты на текущем уровне рекурсии,
			// чтобы избежать повторных одинаковых четвёрок
			if i > start && nums[i] == nums[i-1] {
				continue
			}
			
			// Добавляем текущее число в путь
			quad = append(quad, nums[i])
			
			// Рекурсивно ищем оставшиеся k-1 чисел с обновлённой целью
			kSum(k-1, i+1, target-nums[i])
			
			// Backtracking: удаляем последнее число, чтобы попробовать следующий вариант
			quad = quad[:len(quad)-1]
		}
	}

	// Запускаем рекурсию для поиска 4-х чисел, начиная с 0-го индекса
	kSum(4, 0, target)
	return res
}
