package main

/*
Дан положительный целый k. Также даны:
2D-массив целых чисел rowConditions размера n, где rowConditions[i] = [above_i, below_i], и
2D-массив целых чисел colConditions размера m, где colConditions[i] = [left_i, right_i].
Оба массива содержат целые числа от 1 до k.

Нужно построить матрицу k x k, которая содержит каждое число от 1 до k ровно один раз. Остальные ячейки должны иметь значение 0.

Матрица также должна удовлетворять следующим условиям:
Число above_i должно находиться в строке строго выше строки, в которой находится число below_i, для всех i от 0 до n - 1.
Число left_i должно находиться в столбце строго левее столбца, в котором находится число right_i, для всех i от 0 до m - 1.
Верните любую матрицу, удовлетворяющую условиям. Если ответа не существует, верните пустую матрицу.

Пример 1:
https://assets.leetcode.com/uploads/2022/07/06/gridosdrawio.png
Вход: k = 3, rowConditions = [[1,2],[3,2]], colConditions = [[2,1],[3,2]]
Выход: [[3,0,0],[0,0,1],[0,2,0]]
Пояснение: Диаграмма выше показывает корректный пример матрицы, удовлетворяющей всем условиям.
Условия по строкам:
- Число 1 находится в строке 1, а число 2 - в строке 2, поэтому 1 находится выше 2 в матрице.
- Число 3 находится в строке 0, а число 2 - в строке 2, поэтому 3 находится выше 2 в матрице.
Условия по столбцам:
- Число 2 находится в столбце 1, а число 1 - в столбце 2, поэтому 2 находится левее 1 в матрице.
- Число 3 находится в столбце 0, а число 2 - в столбце 1, поэтому 3 находится левее 2 в матрице.
Обратите внимание, что корректных ответов может быть несколько.

Пример 2:
Вход: k = 3, rowConditions = [[1,2],[2,3],[3,1],[2,3]], colConditions = [[2,1]]
Выход: []
Пояснение: Из первых двух условий следует, что 3 должно быть ниже 1, но третье условие требует, чтобы 3 было выше 1.
Ни одна матрица не может удовлетворить всем условиям, поэтому возвращаем пустую матрицу.

Ограничения:
2 <= k <= 400
1 <= rowConditions.length, colConditions.length <= 10^4
rowConditions[i].length == colConditions[i].length == 2
1 <= above_i, below_i, left_i, right_i <= k
above_i != below_i
left_i != right_i
*/

// buildMatrix строит матрицу размером k x k, удовлетворяющую условиям для строк и столбцов.
// Условия задаются в виде ориентированных графов (списков ребер).
// Если выполнить условия невозможно (например, в графе есть циклы), возвращается пустая матрица.
func buildMatrix(k int, rowConditions [][]int, colConditions [][]int) [][]int {
    // topoSort — вспомогательная функция для топологической сортировки графа.
    // Принимает список ребер (условий) и возвращает порядок узлов.
    // Если в графе обнаружен цикл, возвращает nil.
    topoSort := func(edges [][]int) []int {
        // adj — список смежности для представления графа.
        // Поскольку узлы нумеруются от 1 до k, выделяем k+1 элементов (индекс 0 не используется).
        adj := make([][]int, k+1)
        for i := range adj {
            adj[i] = []int{}
        }
        
        // Заполняем список смежности на основе переданных условий.
        // Ребро [u, v] означает, что узел u должен располагаться до узла v.
        for _, edge := range edges {
            adj[edge[0]] = append(adj[edge[0]], edge[1])
        }

        // visit отслеживает полностью обработанные узлы, чтобы избежать повторных запусков DFS.
        visit := make(map[int]bool)
        // path отслеживает узлы в текущем стеке рекурсии для обнаружения циклов.
        path := make(map[int]bool)
        // order будет хранить узлы в обратном топологическом порядке (post-order обход).
        order := []int{}

        // dfs выполняет обход графа в глубину, начиная с узла src.
        // Возвращает false, если найден цикл, иначе true.
        var dfs func(src int) bool
        dfs = func(src int) bool {
            // Если узел находится в текущем пути рекурсии (path), значит, обнаружен цикл.
            if path[src] {
                return false
            }
            // Если узел уже был полностью обработан в предыдущих запусках, пропускаем его.
            if visit[src] {
                return true
            }
            
            // Помечаем узел как посещенный и добавляем в текущий путь рекурсии.
            visit[src] = true
            path[src] = true
            
            // Рекурсивно обходим всех соседей (зависимости).
            for _, nei := range adj[src] {
                if !dfs(nei) {
                    return false // Прерываем выполнение, если в подграфе найден цикл.
                }
            }
            
            // Удаляем узел из текущего пути при выходе из рекурсии (backtracking).
            delete(path, src)
            // Добавляем узел в список order.
            // Так как узел добавляется после всех своих потомков, формируется обратный топологический порядок.
            order = append(order, src)
            return true
        }

        // Запускаем DFS для каждого узла от 1 до k, чтобы обработать все компоненты связности графа.
        for i := 1; i <= k; i++ {
            if !visit[i] {
                if !dfs(i) {
                    return nil // Обнаружен цикл, топологическая сортировка невозможна.
                }
            }
        }

        // Список order сейчас содержит узлы в обратном топологическом порядке.
        // Разворачиваем его, чтобы получить корректный топологический порядок (от предков к потомкам).
        for i, j := 0, len(order)-1; i < j; i, j = i+1, j-1 {
            order[i], order[j] = order[j], order[i]
        }
        return order
    }

    // Получаем топологический порядок для строк.
    rowOrder := topoSort(rowConditions)
    // Если rowOrder равен nil, в условиях для строк есть цикл, построить матрицу нельзя.
    if rowOrder == nil {
        return [][]int{}
    }

    // Получаем топологический порядок для столбцов.
    colOrder := topoSort(colConditions)
    // Если colOrder равен nil, в условиях для столбцов есть цикл, построить матрицу нельзя.
    if colOrder == nil {
        return [][]int{}
    }

    // Создаем карту для быстрого получения индекса строки для каждого числа.
    // Индекс числа в срезе rowOrder становится его номером строки в матрице.
    valToRow := make(map[int]int)
    for i, num := range rowOrder {
        valToRow[num] = i
    }
    
    // Создаем карту для быстрого получения индекса столбца для каждого числа.
    // Индекс числа в срезе colOrder становится его номером столбца в матрице.
    valToCol := make(map[int]int)
    for i, num := range colOrder {
        valToCol[num] = i
    }

    // Инициализируем результирующую матрицу размером k x k, заполненную нулями.
    res := make([][]int, k)
    for i := range res {
        res[i] = make([]int, k)
    }
    
    // Расставляем каждое число от 1 до k в матрицу на вычисленные ранее позиции (строка и столбец).
    for num := 1; num <= k; num++ {
        r, c := valToRow[num], valToCol[num]
        res[r][c] = num
    }
    
    return res
}