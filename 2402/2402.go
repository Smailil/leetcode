package main

import (
	"container/heap"
	"sort"
)

/*
Дано целое число n. Есть n комнат, пронумерованных от 0 до n - 1.

Дан двумерный целочисленный массив meetings, где meetings[i] = [start_i, end_i]
означает, что встреча будет проходить в полуоткрытом временном
интервале [start_i, end_i). Все значения start_i уникальны.

Встречи распределяются по комнатам следующим образом:
Каждая встреча проводится в свободной комнате с минимальным номером.
Если свободных комнат нет, встреча откладывается до момента,
когда освободится какая-либо комната.
Отложенная встреча должна иметь ту же длительность, что и исходная.
Когда комната освобождается, её должна получить встреча
с более ранним исходным временем начала.
Верните номер комнаты, в которой прошло больше всего встреч.
Если таких комнат несколько, верните комнату с минимальным номером.

Полуоткрытый интервал [a, b) - это интервал от a до b,
включающий a и не включающий b.

Пример 1:
Вход: n = 2, meetings = [[0,10],[1,5],[2,7],[3,4]]
Выход: 0
Пояснение:
- В момент времени 0 обе комнаты не используются.
  Первая встреча начинается в комнате 0.
- В момент времени 1 не используется только комната 1.
  Вторая встреча начинается в комнате 1.
- В момент времени 2 обе комнаты используются. Третья встреча откладывается.
- В момент времени 3 обе комнаты используются. Четвёртая встреча откладывается.
- В момент времени 5 встреча в комнате 1 заканчивается.
  Третья встреча начинается в комнате 1 на период [5,10).
- В момент времени 10 встречи в обеих комнатах заканчиваются.
  Четвёртая встреча начинается в комнате 0 на период [10,11).
В комнатах 0 и 1 прошло по 2 встречи, поэтому возвращаем 0.

Пример 2:
Вход: n = 3, meetings = [[1,20],[2,10],[3,5],[4,9],[6,8]]
Выход: 1
Пояснение:
- В момент времени 1 все три комнаты не используются.
  Первая встреча начинается в комнате 0.
- В момент времени 2 комнаты 1 и 2 не используются.
  Вторая встреча начинается в комнате 1.
- В момент времени 3 не используется только комната 2.
  Третья встреча начинается в комнате 2.
- В момент времени 4 все три комнаты используются. Четвёртая встреча откладывается.
- В момент времени 5 встреча в комнате 2 заканчивается.
  Четвёртая встреча начинается в комнате 2 на период [5,10).
- В момент времени 6 все три комнаты используются. Пятая встреча откладывается.
- В момент времени 10 встречи в комнатах 1 и 2 заканчиваются.
  Пятая встреча начинается в комнате 1 на период [10,12).
В комнате 0 прошла 1 встреча, а в комнатах 1 и 2 - по 2 встречи,
поэтому возвращаем 1.

Ограничения:
1 <= n <= 100
1 <= meetings.length <= 10^5
meetings[i].length == 2
0 <= start_i < end_i <= 5 * 10^5
Все значения start_i уникальны.
*/

func mostBooked(n int, meetings [][]int) int {
	// Сортируем встречи по времени начала.
	// Это критически важно: мы должны обрабатывать события в хронологическом порядке,
	// чтобы корректно освобождать и занимать комнаты.
	sort.Slice(meetings, func(i, j int) bool {
		return meetings[i][0] < meetings[j][0]
	})

	// Мин-куча для свободных комнат. Хранит номера комнат.
	// Гарантирует, что при наличии свободных комнат мы всегда берем комнату с минимальным номером.
	available := &IntHeap{}
	heap.Init(available)
	for i := 0; i < n; i++ {
		heap.Push(available, i)
	}

	// Мин-куча для занятых комнат. Хранит пары [время_освобождения, номер_комнаты].
	// Позволяет быстро находить комнату, которая освободится раньше всех.
	used := &RoomHeap{}
	heap.Init(used)

	// Счетчик бронирований для каждой комнаты.
	count := make([]int, n)

	for _, meeting := range meetings {
		// Используем int64 для предотвращения переполнения при сложении временных меток.
		start, end := int64(meeting[0]), int64(meeting[1])

		// Освобождаем все комнаты, которые закончили работу к моменту start.
		// Перемещаем их из кучи занятых обратно в кучу свободных.
		for used.Len() > 0 && (*used)[0][0] <= start {
			room := heap.Pop(used).([2]int64)[1]
			heap.Push(available, int(room))
		}

		// Если свободных комнат нет, приходится ждать.
		// Берем комнату, которая освободится раньше всего (вершина кучи used).
		if available.Len() == 0 {
			top := heap.Pop(used).([2]int64)
			// Новое время окончания = время освобождения занятой комнаты + длительность текущей встречи.
			// Встреча "сдвигается" во времени, но её продолжительность сохраняется.
			end = top[0] + (end - start)
			// Возвращаем эту же комнату в пул доступных, чтобы сразу занять её ниже.
			// Примечание: можно было бы использовать top[1] напрямую, но повторный push/pop
			// унифицирует логику занятия комнаты для обоих ветвей if/else.
			heap.Push(available, int(top[1]))
		}

		// Занимаем комнату с минимальным номером из доступных.
		room := heap.Pop(available).(int)
		// Добавляем комнату в кучу занятых с новым временем освобождения.
		heap.Push(used, [2]int64{end, int64(room)})
		count[room]++
	}

	// Находим комнату с максимальным количеством бронирований.
	// Цикл начинается с 1, так как maxRoom инициализирован нулем.
	// Условие count[i] > count[maxRoom] (строгое неравенство) гарантирует,
	// что при равенстве счетчиков будет выбрана комната с меньшим индексом.
	maxRoom := 0
	for i := 1; i < n; i++ {
		if count[i] > count[maxRoom] {
			maxRoom = i
		}
	}
	return maxRoom
}

// --- Реализация мин-кучи для целых чисел (номера свободных комнат) ---

type IntHeap []int

func (h IntHeap) Len() int           { return len(h) }
// Сравнение по значению: меньший номер комнаты имеет приоритет.
func (h IntHeap) Less(i, j int) bool { return h[i] < h[j] }
func (h IntHeap) Swap(i, j int)      { h[i], h[j] = h[j], h[i] }
func (h *IntHeap) Push(x any)        { *h = append(*h, x.(int)) }
func (h *IntHeap) Pop() any {
	old := *h
	n := len(old)
	x := old[n-1]
	*h = old[0 : n-1]
	return x
}

// --- Реализация мин-кучи для занятых комнат ---

// Элемент кучи: [0] = время освобождения, [1] = номер комнаты.
type RoomHeap [][2]int64

func (h RoomHeap) Len() int { return len(h) }

// Приоритет определяется временем освобождения.
// Если времена совпадают, приоритет у комнаты с меньшим номером.
// Это обеспечивает детерминированное поведение и соответствие условию задачи.
func (h RoomHeap) Less(i, j int) bool {
	if h[i][0] == h[j][0] {
		return h[i][1] < h[j][1]
	}
	return h[i][0] < h[j][0]
}

func (h RoomHeap) Swap(i, j int) { h[i], h[j] = h[j], h[i] }
func (h *RoomHeap) Push(x any)   { *h = append(*h, x.([2]int64)) }
func (h *RoomHeap) Pop() any {
	old := *h
	n := len(old)
	x := old[n-1]
	*h = old[0 : n-1]
	return x
}
