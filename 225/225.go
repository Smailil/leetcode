package main

/*
Реализуйте стек «последним пришёл — первым вышел» (LIFO), используя только две очереди.
Реализованный стек должен поддерживать все функции обычного стека (push, top, pop и empty).

Реализуйте класс MyStack:

void push(int x) — добавляет элемент x на вершину стека.
int pop() — удаляет элемент на вершине стека и возвращает его.
int top() — возвращает элемент на вершине стека.
boolean empty() — возвращает true, если стек пуст, иначе false.

Примечания:

Используйте только стандартные операции очереди: добавление в конец, просмотр/извлечение из начала, размер и проверка на пустоту.
В зависимости от языка очередь может не поддерживаться нативно.
Можно симулировать очередь с помощью списка или двусторонней очереди (deque),
используя только стандартные операции очереди.

Пример 1:

Вход
["MyStack", "push", "push", "top", "pop", "empty"]
[[], [1], [2], [], [], []]
Выход
[null, null, null, 2, 2, false]

Пояснение
MyStack myStack = new MyStack();
myStack.push(1);
myStack.push(2);
myStack.top(); // вернуть 2
myStack.pop(); // вернуть 2
myStack.empty(); // вернуть False

Ограничения:
1 <= x <= 9
Не более 100 вызовов методов push, pop, top и empty.
Все вызовы pop и top гарантированно корректны.
*/

// MyStack реализует структуру данных "Стек" с использованием одного среза,
// имитирующего очередь. Верхний элемент стека всегда хранится в q[0].
type MyStack struct {
    q []int // срез, хранящий элементы стека
}

// Constructor создает и возвращает новый пустой стек.
func Constructor() MyStack {
    return MyStack{q: []int{}}
}

// Push добавляет элемент x в стек.
// Чтобы верхушка стека всегда находилась в начале среза (q[0]),
// после добавления нового элемента мы циклически переносим все предыдущие
// элементы в конец среза. Это гарантирует O(1) доступ к вершине стека,
// но делает операцию Push O(n) по времени.
func (this *MyStack) Push(x int) {
    this.q = append(this.q, x) // добавляем элемент в конец среза
    // Сдвигаем все старые элементы за только что добавленный x.
    // В результате x оказывается на позиции q[0] (вершина стека).
    for i := len(this.q) - 1; i > 0; i-- {
        this.q = append(this.q, this.q[0]) // копируем первый элемент в конец
        this.q = this.q[1:]                // "откусываем" первый элемент (сдвиг указателя)
    }
}

// Pop удаляет и возвращает верхний элемент стека.
// Поскольку вершина всегда в q[0], операция выполняется за O(1).
func (this *MyStack) Pop() int {
    top := this.q[0]
    this.q = this.q[1:] // сдвигаем начало среза, удаляя вершину
    return top
}

// Top возвращает верхний элемент стека без его удаления.
// Работает за O(1), так как вершина всегда находится в q[0].
func (this *MyStack) Top() int {
    return this.q[0]
}

// Empty проверяет, пуст ли стек.
// Работает за O(1).
func (this *MyStack) Empty() bool {
    return len(this.q) == 0
}


