package main

import "math"

/*
Алиса и Боб продолжают свои игры с кучами камней.
Несколько камней расположены в ряд, и у каждого камня есть связанное с ним значение -
целое число, заданное в массиве stoneValue.

Алиса и Боб ходят по очереди, Алиса начинает первой.
В свой ход игрок может взять 1, 2 или 3 камня из начала оставшегося ряда.

Счет каждого игрока равен сумме значений взятых им камней.
Изначально счет каждого игрока равен 0.

Цель игры - завершить ее с наибольшим счетом.
Победителем становится игрок с наибольшим счетом, также возможна ничья.
Игра продолжается, пока не будут взяты все камни.

Считайте, что Алиса и Боб играют оптимально.

Верните "Alice", если выиграет Алиса, "Bob", если выиграет Боб,
или "Tie", если они закончат игру с одинаковым счетом.

Пример 1:
Вход: stoneValue = [1,2,3,7]
Выход: "Bob"
Пояснение: Алиса всегда проиграет.
Ее лучший ход - взять три кучи, и ее счет станет 6.
Теперь счет Боба равен 7, и Боб выигрывает.

Пример 2:
Вход: stoneValue = [1,2,3,-9]
Выход: "Alice"
Пояснение: Первым ходом Алиса должна выбрать все три кучи,
чтобы победить и оставить Боба с отрицательным счетом.
Если Алиса выберет одну кучу, ее счет будет 1,
а следующим ходом счет Боба станет 5.
Затем Алиса возьмет кучу со значением -9 и проиграет.
Если Алиса выберет две кучи, ее счет будет 3,
а следующим ходом счет Боба станет 3.
Затем Алиса возьмет кучу со значением -9 и тоже проиграет.
Помните, что оба играют оптимально, поэтому здесь Алиса выберет сценарий,
который позволит ей выиграть.

Пример 3:
Вход: stoneValue = [1,2,3,6]
Выход: "Tie"
Пояснение: Алиса не может выиграть эту игру.
Она может завершить игру вничью, если решит выбрать первые три кучи,
иначе она проиграет.

Ограничения:
1 <= stoneValue.length <= 5 * 10^4
-1000 <= stoneValue[i] <= 1000

*/

func stoneGameIII(stoneValue []int) string {
    n := len(stoneValue)
    
    // Массив dp хранит максимальную разницу в счете между текущим игроком и его противником,
    // если игра начинается с камня под индексом i (dp[i] = ОчкиТекущегоИгрока - ОчкиПротивника).
    // 
    // Для оптимизации памяти мы используем массив длины 4 вместо массива длины n.
    // Это работает, так как текущее состояние i зависит только от состояний i+1, i+2 и i+3.
    // Взятие остатка от деления на 4 (i%4) превращает массив в кольцевой буфер.
    dp := make([]int, 4)

    // Начинаем вычисления с самого последнего камня (с конца массива) и движемся к началу.
    // Это стандартный подход в динамическом программировании: чтобы узнать результат 
    // для начала, нужно сначала рассчитать все возможные финалы.
    for i := n - 1; i >= 0; i-- {
        total := 0
        // Инициализируем максимально возможную выгоду минимальным значением,
        // чтобы корректно искать максимум среди возможных ходов (взять 1, 2 или 3 камня).
        dp[i%4] = math.MinInt32
        
        // Рассматриваем все варианты: взять 1, 2 или 3 камня подряд.
        // j — это индекс последнего взятого камня в текущем ходе.
        for j := i; j < min(i+3, n); j++ {
            // total — это сумма очков за камни, которые текущий игрок забрал себе в этом ходе.
            total += stoneValue[j]
            
            // Логика минимакса (игра с нулевой суммой):
            // Если мы забираем камни до индекса j, мы получаем `total` очков.
            // Затем ход переходит к противнику, который начинает играть с индекса j+1.
            // В этой оставшейся игре (с индекса j+1) противник будет "текущим игроком" и 
            // получит преимущество над нами в размере dp[(j+1)%4].
            // Следовательно, наше итоговое преимущество над противником составит: 
            // (наши очки) - (преимущество противника в оставшейся игре).
            // Мы выбираем тот вариант хода, который максимизирует эту разницу.
            dp[i%4] = max(dp[i%4], total - dp[(j+1)%4])
        }
    }

    // В dp[0] (индекс 0 % 4) хранится итоговая максимальная разница очков (Алиса - Боб),
    // когда Алиса делает первый ход с самого начала массива.
    if dp[0] == 0 {
        return "Tie" // Разница равна нулю -> очки равны, ничья
    }
    if dp[0] > 0 {
        return "Alice" // Разница положительная -> победила Алиса
    }
    return "Bob" // Разница отрицательная -> победил Боб
}