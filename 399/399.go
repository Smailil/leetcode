package main

/*
Даны массив пар переменных equations и массив вещественных чисел values,
где equations[i] = [A_i, B_i], а values[i] задает уравнение A_i / B_i = values[i].
Каждая A_i или B_i - строка, представляющая одну переменную.

Также дан массив запросов queries, где queries[j] = [C_j, D_j] представляет j-й запрос,
для которого нужно найти ответ на C_j / D_j = ?.

Верните ответы для всех запросов.
Если отдельный ответ определить невозможно, верните -1.0.

Примечание: входные данные всегда корректны.
Можно считать, что вычисление запросов не приведет к делению на ноль
и что противоречий нет.

Примечание: переменные, которые не встречаются в списке уравнений, не определены,
поэтому ответ для них определить невозможно.

Пример 1:
Вход:
equations = [["a","b"],["b","c"]], values = [2.0,3.0],
queries = [["a","c"],["b","a"],["a","e"],["a","a"],["x","x"]]
Выход: [6.00000,0.50000,-1.00000,1.00000,-1.00000]
Пояснение:
Дано: a / b = 2.0, b / c = 3.0
Запросы: a / c = ?, b / a = ?, a / e = ?, a / a = ?, x / x = ?
Вернуть: [6.0, 0.5, -1.0, 1.0, -1.0]
Примечание: x не определена => -1.0

Пример 2:
Вход:
equations = [["a","b"],["b","c"],["bc","cd"]], values = [1.5,2.5,5.0],
queries = [["a","c"],["c","b"],["bc","cd"],["cd","bc"]]
Выход: [3.75000,0.40000,5.00000,0.20000]

Пример 3:
Вход:
equations = [["a","b"]], values = [0.5],
queries = [["a","b"],["b","a"],["a","c"],["x","y"]]
Выход: [0.50000,2.00000,-1.00000,-1.00000]

Ограничения:
1 <= equations.length <= 20
equations[i].length == 2
1 <= A_i.length, B_i.length <= 5
values.length == equations.length
0.0 < values[i] <= 20.0
1 <= queries.length <= 20
queries[i].length == 2
1 <= C_j.length, D_j.length <= 5
A_i, B_i, C_j, D_j состоят из строчных английских букв и цифр.
*/

func calcEquation(equations [][]string, values []float64, queries [][]string) []float64 {
    // adj — это список смежности для представления ориентированного графа.
    // Ключ — это имя переменной (узел), значение — список соседей и вес ребра (множитель).
    adj := make(map[string][][2]any)

    // Построение графа на основе заданных уравнений и их значений.
    for i, eq := range equations {
        a, b := eq[0], eq[1]
        // Если a / b = values[i], то добавляем направленное ребро от a к b с весом values[i].
        adj[a] = append(adj[a], [2]any{b, values[i]})
        // Добавляем обратное ребро от b к a с весом 1.0 / values[i] (так как b / a = 1 / (a / b)).
        adj[b] = append(adj[b], [2]any{a, 1.0 / values[i]})
    }

    // bfs — вспомогательная функция для поиска результата деления src / target 
    // с помощью алгоритма поиска в ширину (BFS).
    bfs := func(src, target string) float64 {
        // Если исходная или целевая переменная отсутствует в графе, результат вычислить невозможно.
        if _, ok := adj[src]; !ok {
            return -1.0
        }
        if _, ok := adj[target]; !ok {
            return -1.0
        }

        // Очередь для BFS. Хранит пары: [текущая переменная, накопленный множитель от начала пути].
        queue := [][2]any{{src, 1.0}}
        
        // Множество посещенных узлов, чтобы избежать бесконечных циклов при обходе графа.
        visited := make(map[string]bool)
        visited[src] = true

        // Стандартный цикл обхода в ширину.
        for len(queue) > 0 {
            // Извлекаем первый элемент из очереди (принцип FIFO).
            node := queue[0][0].(string)
            weight := queue[0][1].(float64)
            queue = queue[1:] // Удаляем обработанный элемент из очереди.

            // Если мы достигли целевой переменной, возвращаем накопленный множитель.
            if node == target {
                return weight
            }

            // Перебираем всех соседей текущей переменной.
            for _, nei := range adj[node] {
                neiNode := nei[0].(string)
                neiWeight := nei[1].(float64)
                
                // Если сосед еще не был посещен, добавляем его в очередь.
                if !visited[neiNode] {
                    visited[neiNode] = true
                    // Новый накопленный вес равен старому, умноженному на вес ребра до соседа.
                    queue = append(queue, [2]any{neiNode, weight * neiWeight})
                }
            }
        }

        // Если путь между переменными не найден (граф несвязный), возвращаем -1.0.
        return -1.0
    }

    // Массив для хранения ответов на все запросы.
    res := make([]float64, len(queries))
    
    // Выполняем поиск (BFS) для каждого запроса.
    for i, q := range queries {
        res[i] = bfs(q[0], q[1])
    }

    return res
}