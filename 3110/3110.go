package main

/*
Вам дана строка s. Оценка строки определяется как сумма абсолютных
разностей ASCII-кодов соседних символов.

Верните оценку строки s.

Пример 1:
Ввод: s = "hello"
Вывод: 13
Пояснение:
ASCII-коды символов строки s: 'h' = 104, 'e' = 101, 'l' = 108,
'o' = 111. Поэтому оценка строки s равна
|104 - 101| + |101 - 108| + |108 - 108| + |108 - 111| =
3 + 7 + 0 + 3 = 13.

Пример 2:
Ввод: s = "zaz"
Вывод: 50
Пояснение:
ASCII-коды символов строки s: 'z' = 122, 'a' = 97.
Поэтому оценка строки s равна
|122 - 97| + |97 - 122| = 25 + 25 = 50.

Ограничения:
2 <= s.length <= 100
Строка s состоит только из строчных английских букв.
*/

// scoreOfString последовательно суммирует абсолютные разности ASCII-кодов
// соседних символов. Время работы — O(len(s)), память — O(1).
func scoreOfString(s string) int {
    // res хранит сумму разностей для всех уже обработанных соседних пар.
    res := 0

    // Каждая итерация рассматривает ровно одну пару s[i] и s[i+1].
    // Граница len(s)-1 не позволяет обратиться за пределы строки.
    for i := 0; i < len(s)-1; i++ {
        // Сначала вычисляем знаковую разность ASCII-кодов символов пары.
        diff := int(s[i]) - int(s[i+1])

        // Если разность отрицательна, смена знака превращает её в модуль.
        if diff < 0 {
            diff = -diff
        }

        // Добавляем вклад текущей пары к общей оценке строки.
        res += diff
    }

    // После обхода res содержит сумму для всех соседних пар строки.
    return res
}
