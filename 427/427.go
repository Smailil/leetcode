/**
 * Определение узла QuadTree.
 * type Node struct {
 *     Val bool
 *     IsLeaf bool
 *     TopLeft *Node
 *     TopRight *Node
 *     BottomLeft *Node
 *     BottomRight *Node
 * }
 */

// construct — главная функция, точка входа для построения QuadTree.
func construct(grid [][]int) *Node {
    // Запускаем рекурсивный обход (DFS).
    // Передаем: саму сетку, размер стороны квадрата (вся сетка имеет размер N x N),
    // и координаты верхнего левого угла (0, 0).
    return dfs(grid, len(grid), 0, 0)
}

// dfs — рекурсивная функция для построения дерева для заданной квадратной области.
// n — длина стороны текущего квадрата.
// r, c — индексы строки и столбца верхнего левого угла текущего квадрата.
func dfs(grid [][]int, n, r, c int) *Node {
    // Базовый случай рекурсии: размер области стал 1x1 (дошли до одной клетки).
    // Создаём листовой узел. Val будет true, если в клетке 1, иначе false.
    if n == 1 {
        return &Node{Val: grid[r][c] == 1, IsLeaf: true}
    }

    // Вычисляем половину стороны для разделения квадрата на 4 равные части.
    mid := n / 2
    
    // Рекурсивно строим поддеревья для четырёх квадрантов, передавая их размеры 
    // и координаты их верхних левых углов:
    topLeft := dfs(grid, mid, r, c)             // Верхний левый
    topRight := dfs(grid, mid, r, c+mid)        // Верхний правый
    bottomLeft := dfs(grid, mid, r+mid, c)      // Нижний левый
    bottomRight := dfs(grid, mid, r+mid, c+mid) // Нижний правый

    // Оптимизация (сжатие) QuadTree:
    // Если все 4 дочерних узла являются листьями И содержат одинаковое значение,
    // то текущую область тоже можно считать однородной.
    // В этом случае мы не создаём внутренний узел с 4 потомками, а "схлопываем" его в один лист.
    if topLeft.IsLeaf && topRight.IsLeaf &&
        bottomLeft.IsLeaf && bottomRight.IsLeaf &&
        topLeft.Val == topRight.Val &&
        topLeft.Val == bottomLeft.Val &&
        topLeft.Val == bottomRight.Val {
        return &Node{Val: topLeft.Val, IsLeaf: true}
    }

    // Если квадранты неоднородны (содержат разные значения или среди них есть не-листы),
    // создаём внутренний узел (IsLeaf = false).
    // По условию задачи, значение Val для не-листовых узлов игнорируется при проверке,
    // поэтому можно ставить любое (здесь используем false).
    return &Node{
        Val:         false, 
        IsLeaf:      false,
        TopLeft:     topLeft,
        TopRight:    topRight,
        BottomLeft:  bottomLeft,
        BottomRight: bottomRight,
    }
}