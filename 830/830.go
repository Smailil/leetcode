package main

/*
В киоске с лимонадом каждый лимонад стоит $5.
Покупатели стоят в очереди и покупают у вас по одному
в порядке, заданном массивом bills.
Каждый покупатель покупает только один лимонад
и платит купюрой $5, $10 или $20.
Вы должны дать каждому покупателю правильную сдачу,
чтобы в итоге покупатель заплатил ровно $5.

Обратите внимание, что изначально у вас нет сдачи.

Дан целочисленный массив bills, где bills[i] — купюра,
которой платит i-й покупатель.
Верните true, если вы можете дать правильную сдачу
каждому покупателю, иначе верните false.

Пример 1:
Вход: bills = [5,5,5,10,20]
Выход: true
Пояснение:
От первых 3 покупателей по порядку мы получаем три купюры по $5.
От четвёртого покупателя мы получаем купюру $10 и отдаём $5.
Пятому покупателю мы отдаём купюру $10 и купюру $5.
Так как все покупатели получили правильную сдачу, выводим true.

Пример 2:
Вход: bills = [5,5,10,10,20]
Выход: false
Пояснение:
От первых двух покупателей по порядку мы получаем две купюры по $5.
От следующих двух покупателей по порядку мы получаем купюру $10
и отдаём купюру $5.
Последнему покупателю мы не можем дать сдачу $15,
потому что у нас есть только две купюры по $10.
Так как не каждый покупатель получил правильную сдачу, ответ — false.

Ограничения:
1 <= bills.length <= 10^5
bills[i] равен 5, 10 или 20.

*/

func lemonadeChange(bills []int) bool {
    // Считаем количество купюр по $5 и $10, которые есть в кассе для сдачи.
    // Купюры по $20 для сдачи не нужны, поэтому их не считаем.
    five, ten := 0, 0

    for _, b := range bills {
        switch b {
		case 5:
            // Покупатель платит $5 — сдачи не требуется, просто кладём купюру в кассу
            five++
        case 10:
            // Покупатель платит $10 — нужно дать сдачу $5
            ten++
            if five > 0 {
                five-- // Отдаём одну пятёрку
            } else {
                return false // Пятёрок в кассе нет — дать сдачу невозможно
            }
        default:
            // Покупатель платит $20 — нужно дать сдачу $15
            // Приоритет: сначала отдаём $10 + $5 (крупные купюры менее универсальны)
            if five > 0 && ten > 0 {
                five--
                ten--
            } else if five >= 3 {
                // Альтернатива: три пятёрки
                five -= 3
            } else {
                return false // Не хватает купюр для сдачи $15
            }
        }
    }

    // Все покупатели получили сдачу
    return true
}