package main

/*
Дано целое число columnNumber.
Верните соответствующее ему название столбца,
как оно отображается на листе Excel.

Например:
A -> 1
B -> 2
C -> 3
...
Z -> 26
AA -> 27
AB -> 28
...

Пример 1:
Вход: columnNumber = 1
Выход: "A"

Пример 2:
Вход: columnNumber = 28
Выход: "AB"

Пример 3:
Вход: columnNumber = 701
Выход: "ZY"

Ограничения:
1 <= columnNumber <= 2^31 - 1
*/

// convertToTitle переводит номер столбца в позиционную систему Excel.
// Она похожа на систему счисления по основанию 26, но использует цифры
// от 1 до 26: A соответствует 1, а Z — 26. Нулевой цифры в ней нет.
//
// При фиксированной разрядности int время и дополнительная память равны O(1):
// количество разрядов результата ограничено числом битов входного значения.
func convertToTitle(columnNumber int) string {
    // Разряды извлекаются от младшего к старшему, поэтому сначала символы
    // накапливаются в обратном порядке.
    res := []byte{}

    for columnNumber > 0 {
        // Переходим от диапазона цифр 1..26 к обычному диапазону 0..25.
        // Это особенно важно для чисел, кратных 26: столбец 26 должен дать Z,
        // а не нулевой разряд с лишним старшим символом.
        columnNumber--

        // Остаток определяет текущую букву: 0 соответствует A, 25 — Z.
        offset := columnNumber % 26
        res = append(res, byte('A'+offset))

        // Частное содержит ещё не обработанные старшие разряды.
        columnNumber /= 26
    }

    // Возвращаем буквы в прямой порядок: от старшего разряда к младшему.
    for i, j := 0, len(res)-1; i < j; i, j = i+1, j-1 {
        res[i], res[j] = res[j], res[i]
    }

    return string(res)
}
