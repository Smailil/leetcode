package main

/*
Даны два целых числа left и right, задающие диапазон [left, right].
Верните результат побитовой операции И для всех чисел
в этом диапазоне включительно.

Пример 1:
Вход: left = 5, right = 7
Выход: 4

Пример 2:
Вход: left = 0, right = 0
Выход: 0

Пример 3:
Вход: left = 1, right = 2147483647
Выход: 0

Ограничения:
0 <= left <= right <= 2^31 - 1
*/

// rangeBitwiseAnd находит общий двоичный префикс границ диапазона.
// Все разряды правее первого различающегося бита обнуляются при побитовом И:
// внутри непрерывного диапазона для каждого такого разряда встретится число,
// у которого этот бит равен 0. Поэтому значимым остаётся только общий префикс
// двоичных представлений left и right.
//
// Время: O(1), поскольку int имеет фиксированную разрядность,
// а количество сдвигов ограничено числом его битов.
// Дополнительная память: O(1).
func rangeBitwiseAnd(left int, right int) int {
    // i хранит количество младших разрядов, отброшенных у обеих границ.
    // После нахождения общего префикса это же количество нулей нужно будет
    // вернуть в результат сдвигом влево.
    i := 0

    // Пока границы различаются, их двоичные записи ещё содержат несовпадающий
    // суффикс, который гарантированно превратится в нули в результате AND.
    for left != right {
        // Одновременно удаляем по одному младшему биту. Когда значения станут
        // равны, в left и right останется их максимальный общий префикс.
        left >>= 1
        right >>= 1
        i++
    }

    // Возвращаем общий префикс на исходные позиции. Освободившиеся справа
    // разряды заполняются нулями — именно такими они будут после AND диапазона.
    return left << i
}
