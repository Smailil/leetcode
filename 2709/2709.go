package main

import "math"

/*
Дан целочисленный массив nums с индексацией с 0.
Разрешено перемещаться между его индексами. Можно перейти между индексами
i и j, i != j, тогда и только тогда, когда gcd(nums[i], nums[j]) > 1,
где gcd - наибольший общий делитель.

Нужно определить, существует ли для каждой пары индексов i и j массива nums,
где i < j, последовательность переходов, по которой можно попасть из i в j.

Верни true, если можно перемещаться между всеми такими парами индексов,
иначе верни false.

Пример 1:
Вход: nums = [2,3,6]
Выход: true
Пояснение: В этом примере есть 3 возможные пары индексов: (0, 1), (0, 2)
и (1, 2).
Чтобы попасть из индекса 0 в индекс 1, можно использовать последовательность
переходов 0 -> 2 -> 1: сначала перейти из индекса 0 в индекс 2, потому что
gcd(nums[0], nums[2]) = gcd(2, 6) = 2 > 1, а затем перейти из индекса 2
в индекс 1, потому что gcd(nums[2], nums[1]) = gcd(6, 3) = 3 > 1.
Чтобы попасть из индекса 0 в индекс 2, можно перейти напрямую, потому что
gcd(nums[0], nums[2]) = gcd(2, 6) = 2 > 1. Аналогично, из индекса 1
в индекс 2 тоже можно перейти напрямую, потому что
gcd(nums[1], nums[2]) = gcd(3, 6) = 3 > 1.

Пример 2:
Вход: nums = [3,9,5]
Выход: false
Пояснение: В этом примере никакая последовательность переходов не может
привести нас из индекса 0 в индекс 2. Поэтому возвращаем false.

Пример 3:
Вход: nums = [4,3,12,8]
Выход: true
Пояснение: Есть 6 возможных пар индексов, между которыми нужно уметь
переходить: (0, 1), (0, 2), (0, 3), (1, 2), (1, 3) и (2, 3).
Для каждой пары существует корректная последовательность переходов,
поэтому возвращаем true.

Ограничения:
1 <= nums.length <= 10^5
1 <= nums[i] <= 10^5
*/

// ============================================================
// Структура UnionFind реализует систему непересекающихся
// множеств (Disjoint Set Union, DSU) с двумя оптимизациями:
//   1. Сжатие путей в методе Find (ускоряет последующие запросы).
//   2. Объединение по размеру: меньшее дерево подвешивается
//     к корню большего, чтобы высота дерева оставалась маленькой.
// ============================================================
type UnionFind struct {
	n      int   // количество оставшихся компонент связности
	Parent []int // Parent[i] — родитель элемента i
	Size   []int // Size[i] — размер дерева, корнем которого является i
}

// NewUnionFind создаёт новую структуру DSU для n элементов.
// Элементы нумеруются от 0 до n. Каждый элемент изначально
// является корнем собственного дерева размера 1.
// Поэтому количество компонент связности изначально равно n.
func NewUnionFind(n int) *UnionFind {
	// Создаём массив родителей длиной n+1.
	// Элементы массива инициализируются значениями 0, 1, 2, ..., n.
	// Каждый элемент сам является своим родителем — все деревья одноэлементные.
	parent := make([]int, n+1)
	for i := range parent {
		parent[i] = i
	}

	// Размер каждого дерева изначально равен 1.
	size := make([]int, n+1)
	for i := range size {
		size[i] = 1
	}

	return &UnionFind{
		n:      n,
		Parent: parent,
		Size:   size,
	}
}

// Find находит корень множества, которому принадлежит элемент node.
// Параллельно выполняет сжатие пути: все узлы по пути от node до
// корня переподвешиваются непосредственно к корню.
// Это гарантирует практически O(1) амортизированное время работы.
func (uf *UnionFind) Find(node int) int {
	// Пока элемент не является корнем своего дерева...
	if uf.Parent[node] != node {
		// ...рекурсивно находим корень и сразу записываем его
		// как родителя текущего элемента (сжатие пути).
		uf.Parent[node] = uf.Find(uf.Parent[node])
	}
	// Возвращаем корень множества.
	return uf.Parent[node]
}

// Union объединяет множества, содержащие элементы u и v.
// Возвращает true, если элементы были в разных множествах
// (и объединение произошло), и false, если они уже были в одном.
func (uf *UnionFind) Union(u, v int) bool {
	// Находим корни обоих элементов.
	pu := uf.Find(u)
	pv := uf.Find(v)

	// Если корни совпадают — элементы уже в одном множестве.
	// Объединять нечего, возвращаем false.
	if pu == pv {
		return false
	}

	// Уменьшаем счётчик компонент связности на 1,
	// так как две компоненты сливаются в одну.
	uf.n--

	// Объединение по размеру: делаем так, чтобы pu указывал
	// на корень БОЛЬШЕГО дерева. Это поддерживает деревья
	// сбалансированными.
	if uf.Size[pu] < uf.Size[pv] {
		pu, pv = pv, pu
	}

	// Подвешиваем меньшее дерево (pv) к корню большего (pu).
	uf.Size[pu] += uf.Size[pv] // размер нового дерева = сумма размеров
	uf.Parent[pv] = pu         // корень меньшего теперь ссылается на больший

	return true
}

// IsConnected проверяет, остались ли все элементы в одной компоненте.
// Изначально компонент было n, и каждая операция Union уменьшает
// их количество на 1. Если осталась ровно 1 компонента — граф связен.
func (uf *UnionFind) IsConnected() bool {
	return uf.n == 1
}

// canTraverseAllPairs определяет, можно ли «соединить» все элементы
// массива nums в одну связную группу, если пара элементов считается
// соединённой тогда и только тогда, когда у них есть общий делитель > 1.
//
// Идея алгоритма:
//   - Два числа связаны, если делятся на один и тот же простой делитель.
//   - Для каждого числа находим его простые делители.
//   - Ведём словарь factorToIndex: для каждого простого делителя p
//     запоминаем индекс какого-нибудь элемента, у которого p есть.
//   - Встретив новый элемент с делителем p, который уже встречался,
//     объединяем их через DSU.
//   - В конце проверяем, все ли элементы оказались в одной компоненте.
func canTraverseAllPairs(nums []int) bool {
	// Граничный случай: если массив состоит из одного элемента,
	// то «все пары» (которых нет) тривиально связаны.
	n := len(nums)
	if n == 1 {
		return true
	}

	// Создаём DSU на n элементов (индексы массива от 0 до n-1).
	uf := NewUnionFind(n)

	// Словарь «простой делитель → индекс первого элемента,
	// у которого этот делитель встретился».
	factorToIndex := make(map[int]int)

	// Проходим по всем элементам массива.
	for i := 0; i < n; i++ {
		num := nums[i]

		// Находим все простые делители числа num.
		// Достаточно проверять делители до sqrt(num).
		limit := int(math.Sqrt(float64(num)))
		for f := 2; f <= limit; f++ {
			if num%f == 0 {
				// f — простой делитель числа num.
				if prevIdx, exists := factorToIndex[f]; exists {
					// Этот делитель уже встречался у другого элемента.
					// Объединяем текущий элемент i с тем элементом.
					uf.Union(i, prevIdx)
				} else {
					// Впервые видим этот делитель — запоминаем индекс.
					factorToIndex[f] = i
				}

				// Выделяем все копии делителя f из числа,
				// чтобы не обрабатывать их повторно.
				for num%f == 0 {
					num /= f
				}
			}
		}

		// Если после удаления всех малых делителей осталось число > 1,
		// значит, оно само является простым делителем (и достаточно большим).
		if num > 1 {
			if prevIdx, exists := factorToIndex[num]; exists {
				uf.Union(i, prevIdx)
			} else {
				factorToIndex[num] = i
			}
		}
	}

	// Возвращаем true, если все элементы оказались в одной компоненте.
	return uf.IsConnected()
}
