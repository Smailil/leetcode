package main

/*
Дан массив arr.
Замените каждый его элемент на наибольший элемент среди элементов справа,
а последний элемент замените на -1.

После этого верните массив.

Пример 1:
Ввод: arr = [17,18,5,4,6,1]
Вывод: [18,6,6,6,1,-1]
Пояснение:
- индекс 0 --> наибольший элемент справа от него
  находится по индексу 1 (18).
- индекс 1 --> наибольший элемент справа от него
  находится по индексу 4 (6).
- индекс 2 --> наибольший элемент справа от него
  находится по индексу 4 (6).
- индекс 3 --> наибольший элемент справа от него
  находится по индексу 4 (6).
- индекс 4 --> наибольший элемент справа от него
  находится по индексу 5 (1).
- индекс 5 --> справа от него нет элементов, поэтому записываем -1.

Пример 2:
Ввод: arr = [400]
Вывод: [-1]
Пояснение: справа от элемента с индексом 0 нет других элементов.

Ограничения:
1 <= arr.length <= 10^4
1 <= arr[i] <= 10^5
*/

// replaceElements изменяет arr на месте, двигаясь справа налево.
// Время работы — O(n), а дополнительная память — O(1): отдельный массив
// результата не создаётся, и возвращаемый срез использует ту же память.
func replaceElements(arr []int) []int {
    // До первой итерации справа от последней позиции нет элементов,
    // поэтому начальный максимум равен требуемому значению -1.
    rightMax := -1
    for i := len(arr) - 1; i >= 0; i-- {
        // Сохраняем исходное значение до перезаписи: оно понадобится
        // для вычисления максимума справа на следующей итерации.
        current := arr[i]

        // Инвариант: rightMax содержит максимум строго справа от позиции i.
        // Записываем его непосредственно в исходный массив.
        arr[i] = rightMax

        // После записи ответа включаем прежний arr[i] в максимум.
        // Для позиции i-1 этот элемент уже будет находиться справа.
        if current > rightMax {
            rightMax = current
        }
    }

    return arr
}
