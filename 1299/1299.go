package main

/*
Дан массив arr.
Замените каждый его элемент на наибольший элемент среди элементов справа,
а последний элемент замените на -1.

После этого верните массив.

Пример 1:
Ввод: arr = [17,18,5,4,6,1]
Вывод: [18,6,6,6,1,-1]
Пояснение:
- индекс 0 --> наибольший элемент справа от него
  находится по индексу 1 (18).
- индекс 1 --> наибольший элемент справа от него
  находится по индексу 4 (6).
- индекс 2 --> наибольший элемент справа от него
  находится по индексу 4 (6).
- индекс 3 --> наибольший элемент справа от него
  находится по индексу 4 (6).
- индекс 4 --> наибольший элемент справа от него
  находится по индексу 5 (1).
- индекс 5 --> справа от него нет элементов, поэтому записываем -1.

Пример 2:
Ввод: arr = [400]
Вывод: [-1]
Пояснение: справа от элемента с индексом 0 нет других элементов.

Ограничения:
1 <= arr.length <= 10^4
1 <= arr[i] <= 10^5
*/

// replaceElements идёт справа налево, поддерживая максимум среди элементов,
// которые находятся строго правее текущей позиции. Время работы — O(n),
// память результата — O(n), а вспомогательная память — O(1).
func replaceElements(arr []int) []int {
    n := len(arr)

    // Результат хранится отдельно, поэтому исходный массив не изменяется.
    ans := make([]int, n)

    // Справа от последнего элемента ничего нет, поэтому начальный максимум
    // равен требуемому для него значению -1.
    rightMax := -1
    for i := n - 1; i >= 0; i-- {
        // Инвариант: до обновления rightMax содержит максимум элементов
        // с индексами от i+1 до n-1, то есть строго справа от arr[i].
        ans[i] = rightMax

        // Только после записи ответа включаем arr[i] в максимум.
        // На следующей итерации он уже будет находиться справа от позиции.
        if arr[i] > rightMax {
            rightMax = arr[i]
        }
    }
    return ans
}
