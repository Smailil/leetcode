/*
На сетке m x n находится робот. Робот изначально расположен в верхнем левом углу (т. е. сетка[0][0]). 
Робот пытается переместиться в правый нижний угол (т. е. сетку[m -1][n -1]). В любой момент времени робот может двигаться только вниз или вправо.

Учитывая два целых числа m и n, верните количество возможных уникальных путей, по которым робот может добраться до правого нижнего угла.

Тестовые случаи генерируются так, что ответ будет меньше или равен 2 *10^9.

Пример 1:
https://assets.leetcode.com/uploads/2018/10/22/robot_maze.png
Input: m = 3, n = 7
Output: 28

Пример 2:
Input: m = 3, n = 2
Output: 3
Пояснение: Из верхнего левого угла можно попасть в правый нижний угол тремя способами:
1. Вправо -> Вниз -> Вниз.
2. Вниз -> Вниз -> Вправо.
3. Вниз -> Вправо -> Вниз.

Ограничения:
1 <= m, n <= 100

*/

// Математическое решение через комбинаторику:
// Всего шагов: (m-1) + (n-1) = m+n-2
// Из них нужно выбрать: (m-1) шагов вниз (или (n-1) шагов вправо)
// Формула: C(m+n-2, m-1) = (m+n-2)! / ((m-1)! * (n-1)!)
func uniquePaths(m int, n int) int {
    // Базовый случай: если только одна строка или один столбец,
    // то путь только один (идти всё время прямо)
    if m == 1 || n == 1 {
        return 1
    }

    // Оптимизация: уменьшаем количество итераций цикла
    // C(n, k) = C(n, n-k), поэтому выбираем минимальное из m и n
    // Пример: C(8, 2) = C(8, 6), но 2 итерации быстрее чем 6
    if m < n {
        m, n = n, m
    }

    // Вычисляем биномиальный коэффициент итеративно:
    // C(m+n-2, n-1) = ∏(i=1 to n-1) (m+n-2-i+1) / i
    //
    // res — накапливаемый результат
    // j   — знаменатель (1, 2, 3, ..., n-1)
    // i   — числитель (m, m+1, m+2, ..., m+n-2)
    res, j := 1, 1
    
    // Выполняем (n-1) итераций
    // Диапазон: от m до (m + n - 2) включительно
    for i := m; i < m+n-1; i++ {
        res *= i  // Умножаем на следующий числитель
        res /= j  // Делим на следующий знаменатель
        j++
    }

    return res
}

