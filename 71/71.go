package main

import "strings"

/*
Вам дан абсолютный путь в файловой системе Unix,
который всегда начинается с косой черты '/'.
Ваша задача — преобразовать этот абсолютный путь
в упрощённый канонический вид.

Правила файловой системы Unix:

Одиночная точка '.' обозначает текущий каталог.
Двойная точка '..' обозначает родительский каталог (на уровень выше).
Несколько подряд идущих косых черт, например '//' и '///',
считаются одной косой чертой '/'.
Любая последовательность точек, не соответствующая правилам выше,
считается корректным именем каталога или файла.
Например, '...' и '....' — допустимые имена.

Упрощённый канонический путь должен соответствовать следующим правилам:

Путь должен начинаться с одной косой черты '/'.
Каталоги в пути должны разделяться ровно одной косой чертой '/'.
Путь не должен заканчиваться на '/', если только это не корневой каталог.
Путь не должен содержать точек ('.' и '..'),
обозначающих текущий или родительский каталог.

Верните упрощённый канонический путь.

Пример 1:
Вход: path = "/home/"
Выход: "/home"
Пояснение:
Завершающая косая черта удаляется.

Пример 2:
Вход: path = "/home//foo/"
Выход: "/home/foo"
Пояснение:
Несколько подряд идущих косых черт заменяются одной.

Пример 3:
Вход: path = "/home/user/Documents/../Pictures"
Выход: "/home/user/Pictures"
Пояснение:
Двойная точка ".." ведёт на уровень выше (в родительский каталог).

Пример 4:
Вход: path = "/../"
Выход: "/"
Пояснение:
Подъём выше корневого каталога невозможен.

Пример 5:
Вход: path = "/.../a/../b/c/../d/./"
Выход: "/.../b/d"
Пояснение:
"..." — допустимое имя каталога в данной задаче.

Ограничения:
1 <= path.length <= 3000
path состоит из латинских букв, цифр, точки '.', косой черты '/' или '_'.
path — корректный абсолютный путь Unix.
*/

func simplifyPath(path string) string {
    stack := []string{}
    cur := ""

    for _, c := range path + "/" {
        if c == '/' {
            if cur == ".." {
                if len(stack) > 0 {
                    stack = stack[:len(stack)-1]
                }
            } else if cur != "" && cur != "." {
                stack = append(stack, cur)
            }
            cur = ""
        } else {
            cur += string(c)
        }
    }

    return "/" + strings.Join(stack, "/")
}