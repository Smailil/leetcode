package main

/*
Дана бинарная строка s с индексацией с 0 и два целых числа minJump и maxJump.
В начале вы стоите в индексе 0, который равен '0'.
Можно перейти из индекса i в индекс j, если выполняются условия:
i + minJump <= j <= min(i + maxJump, s.length - 1), и
s[j] == '0'.
Верните true, если можно добраться до индекса s.length - 1 в s,
иначе верните false.

Пример 1:
Вход: s = "011010", minJump = 2, maxJump = 3
Выход: true
Пояснение:
На первом шаге перейдите из индекса 0 в индекс 3.
На втором шаге перейдите из индекса 3 в индекс 5.

Пример 2:
Вход: s = "01101110", minJump = 2, maxJump = 3
Выход: false

Ограничения:
2 <= s.length <= 10^5
s[i] равно либо '0', либо '1'.
s[0] == '0'
1 <= minJump <= maxJump < s.length
*/

func canReach(s string, minJump int, maxJump int) bool {
    n := len(s)
    
    // Быстрый выход: если последний символ '1', дойти до него невозможно в принципе.
    if s[n-1] == '1' {
        return false
    }

    // Очередь для обхода в ширину (BFS). Храним индексы, из которых можно совершить прыжок.
    // Начинаем с нулевого индекса, так как по условию s[0] всегда '0'.
    q := []int{0}

    // farthest хранит самый дальний индекс, который мы уже успели проверить.
    // Это ключевая оптимизация: она не дает нам повторно проверять одни и те же позиции
    // при обработке следующих элементов очереди, снижая сложность с O(N^2) до O(N).
    farthest := 0

    // Классический цикл BFS: пока в очереди есть достижимые позиции...
    for len(q) > 0 {
        // Достаем текущую позицию из начала очереди.
        i := q[0]
        q = q[1:]

        // Вычисляем начало диапазона для следующего прыжка.
        // Прыгать можно не ближе чем на minJump, то есть начиная с (i + minJump).
        // НО: все индексы до farthest мы уже проверили в предыдущих итерациях.
        // Поэтому начинаем проверку с максимума между (i + minJump) и (farthest + 1).
        start := max(i+minJump, farthest+1)

        // Перебираем все возможные длины прыжка из текущего индекса i.
        // Конец диапазона ограничен либо (i + maxJump), либо концом строки (n).
        for j := start; j < min(i+maxJump+1, n); j++ {
            // Прыгать можно только на безопасные клетки ('0').
            if s[j] == '0' {
                // Если мы достигли конца строки — победа!
                if j == n-1 {
                    return true
                }
                // Иначе добавляем эту позицию в очередь для дальнейших прыжков.
                q = append(q, j)
            }
        }
        
        // После того как мы просмотрели весь диапазон прыжков из точки i,
        // обновляем farthest. Теперь мы точно знаем, что всё до (i + maxJump) проверено.
        farthest = i + maxJump
    }

    // Очередь опустела, а конец строки так и не был достигнут.
    return false
}