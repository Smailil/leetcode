package main

/*
Дан массив nums из n объектов, окрашенных в красный, белый или синий цвет.
Отсортируйте их на месте так, чтобы объекты одного цвета стояли рядом,
а цвета шли в порядке: красный, белый, синий.

Для обозначения цветов используются целые числа 0, 1 и 2 — красный, белый и синий соответственно.

Задачу необходимо решить без использования стандартной функции сортировки.

Пример 1:
Вход: nums = [2,0,2,1,1,0]
Выход: [0,0,1,1,2,2]

Пример 2:
Вход: nums = [2,0,1]
Выход: [0,1,2]

Ограничения:
n == nums.length
1 <= n <= 300
nums[i] принимает значения 0, 1 или 2.

*/

// Реализует алгоритм "Голландский флаг" (Dutch National Flag) с тремя указателями.
// Время работы: O(n), Дополнительная память: O(1).
func sortColors(nums []int) {
    // i - текущий указатель при обходе массива
    // l - левая граница: все элементы слева от l уже равны 0
    // r - правая граница: все элементы справа от r уже равны 2
    i, l, r := 0, 0, len(nums) - 1

    // Обходим массив, пока текущий указатель не пересечёт правую границу.
    // Элементы после r уже гарантированно отсортированы (это все двойки).
    for i <= r {
        if nums[i] == 0 {
            // Если встретили 0, меняем его местами с элементом на позиции l
            nums[l], nums[i] = nums[i], nums[l]
            l++
            // Элемент, пришедший на место i, уже проверен (это 0 или 1), 
            // поэтому можно безопасно увеличивать i на следующей итерации.
        } else if nums[i] == 2 {
            // Если встретили 2, меняем его местами с элементом на позиции r
            nums[i], nums[r] = nums[r], nums[i]
            r--
            // Уменьшаем i, чтобы на следующей итерации проверить элемент, 
            // который пришёл с позиции r (он ещё не обработан).
            i--
        }
        // Если nums[i] == 1, он уже находится в правильной средней зоне.
        // Ничего менять не нужно, просто двигаемся дальше.
        i++
    }
}
