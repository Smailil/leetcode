package main

/*
Дан целочисленный массив nums, отсортированный в неубывающем порядке (не обязательно с уникальными значениями).

Перед передачей в функцию массив nums был повёрнут в неизвестной точке поворота k (0 <= k < nums.length),
так что результирующий массив имеет вид [nums[k], nums[k+1], ..., nums[n-1], nums[0], nums[1], ..., nums[k-1]] (0-индексация).
Например, [0,1,2,4,4,4,5,6,6,7] мог быть повёрнут в точке k=5 и стать [4,5,6,6,7,0,1,2,4,4].

Дан массив nums после поворота и целое число target.
Верни true, если target присутствует в nums, или false, если нет.

Необходимо минимизировать общее количество операций.

Пример 1:
Вход: nums = [2,5,6,0,0,1,2], target = 0
Выход: true

Пример 2:
Вход: nums = [2,5,6,0,0,1,2], target = 3
Выход: false

Ограничения:
1 <= nums.length <= 5000
-10^4 <= nums[i] <= 10^4
nums гарантированно повёрнут относительно некоторой точки.
-10^4 <= target <= 10^4
*/

func search(nums []int, target int) bool {
	l, r := 0, len(nums)-1 // Левая и правая границы текущего поискового окна

	for l <= r {
		m := l + (r-l)/2 // Середина диапазона (защита от целочисленного переполнения)

		if nums[m] == target {
			return true // Цель найдена
		}

		// Левая половина [l..m] отсортирована
		if nums[l] < nums[m] {
			// Если target попадает в диапазон отсортированной левой части
			if nums[l] <= target && target < nums[m] {
				r = m - 1 // Продолжаем поиск влево
			} else {
				l = m + 1 // Иначе target находится в правой (повёрнутой) части
			}
		// Левая половина не отсортирована → значит, правая [m..r] отсортирована
		} else if nums[l] > nums[m] {
			// Если target попадает в диапазон отсортированной правой части
			if nums[m] < target && target <= nums[r] {
				l = m + 1 // Продолжаем поиск вправо
			} else {
				r = m - 1 // Иначе target находится в левой (повёрнутой) части
			}
		// nums[l] == nums[m]: невозможно определить, какая часть отсортирована
		// (типичный случай при дубликатах, например: [1,0,1,1,1])
		} else {
			l++ // Пропускаем дубликат слева, чтобы сузить окно поиска
		}
	}

	return false // Цель не найдена во всём массиве
}
