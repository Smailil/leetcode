package main

/*
Дан несортированный массив целых чисел nums.
Верните наименьшее положительное целое число, отсутствующее в nums.

Необходимо реализовать алгоритм со временем выполнения O(n)
и вспомогательным пространством O(1).

Пример 1:
Вход: nums = [1,2,0]
Выход: 3
Пояснение: Все числа в диапазоне [1,2] присутствуют в массиве.

Пример 2:
Вход: nums = [3,4,-1,1]
Выход: 2
Пояснение: 1 есть в массиве, но 2 отсутствует.

Пример 3:
Вход: nums = [7,8,9,11,12]
Выход: 1
Пояснение: Наименьшее положительное целое число 1 отсутствует.

Ограничения:
1 <= nums.length <= 10^5
-2^31 <= nums[i] <= 2^31 - 1
*/

// Алгоритм работает за O(n) времени и O(1) дополнительной памяти,
// используя сам массив как хеш-таблицу для отметки присутствующих чисел.
func firstMissingPositive(nums []int) int {
    n := len(nums)

    // Шаг 1: Заменяем все отрицательные числа на 0.
    // Это нужно, чтобы:
    // 1) Отрицательные числа не влияли на поиск положительных
    // 2) Ноль мог использоваться как временный маркер
    for i := range n {
        if nums[i] < 0 {
            nums[i] = 0
        }
    }

    // Шаг 2: Отмечаем присутствие чисел от 1 до n.
    // Идея: индекс i в массиве соответствует числу i+1.
    // Если число val присутствует, мы делаем элемент nums[val-1] отрицательным.
    for i := range n {
        val := nums[i]
        
        // Если элемент уже был помечен (стал отрицательным), берём его абсолютное значение
        if val < 0 {
            val = -val
        }
        
        // Проверяем, что val находится в допустимом диапазоне [1, n]
        if val >= 1 && val <= n {
            // Если целевой элемент положительный, меняем его знак на минус
            if nums[val-1] > 0 {
                nums[val-1] *= -1
            } else if nums[val-1] == 0 {
                // Если целевой элемент равен 0, заменяем его на -(n+1).
                // Это нужно, чтобы 0 не остался неотмеченным (0 * -1 = 0),
                // а n+1 гарантированно выйдет за пределы диапазона [1, n] и не вызовет ложных отметок.
                nums[val-1] = -(n + 1)
            }
        }
    }

    // Шаг 3: Ищем первый неотрицательный элемент.
    // Индекс i, где nums[i] >= 0, означает, что число i+1 никогда не было отмечено,
    // следовательно, оно отсутствует в массиве.
    for i := range n {
        if nums[i] >= 0 {
            return i + 1
        }
    }

    // Шаг 4: Если все числа от 1 до n присутствуют (все элементы отрицательные),
    // то наименьшее отсутствующее положительное число равно n+1.
    return n + 1
}
