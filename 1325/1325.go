package main

/*
Дан корень бинарного дерева и целое число target.
Удалить все листовые узлы со значением target.

Обратите внимание: после удаления листового узла со значением target,
если его родительский узел становится листом и тоже имеет значение target —
он также должен быть удалён (продолжать до тех пор, пока удаление невозможно).

Пример 1:
https://assets.leetcode.com/uploads/2020/01/09/sample_1_1684.png
Вход: root = [1,2,3,2,null,2,4], target = 2
Выход: [1,null,3,null,4]
Пояснение: Листовые узлы со значением (target = 2) удаляются (рисунок слева).
После удаления новые узлы становятся листами со значением (target = 2)
(рисунок в центре).

Пример 2:
https://assets.leetcode.com/uploads/2020/01/09/sample_2_1684.png
Вход: root = [1,3,3,3,2], target = 3
Выход: [1,3,null,null,2]

Пример 3:
https://assets.leetcode.com/uploads/2020/01/15/sample_3_1684.png
Вход: root = [1,2,null,2,null,2], target = 2
Выход: [1]
Пояснение: Листовые узлы со значением (target = 2) удаляются на каждом шаге.

Ограничения:
Количество узлов в дереве находится в диапазоне [1, 3000].
1 <= Node.val, target <= 1000
*/

/**
 * Определение узла бинарного дерева.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */

// Важная особенность: если после удаления детей узел сам становится листом 
// со значением target, он также должен быть удален.
func removeLeafNodes(root *TreeNode, target int) *TreeNode {
    // Базовый случай рекурсии: если мы дошли до пустого узла, возвращаем nil.
    if root == nil {
        return nil
    }

    // Используем post-order обход (обход в постпорядке / снизу вверх).
    // Сначала рекурсивно обрабатываем левое и правое поддеревья.
    // Результат рекурсии сразу присваиваем указателям Left и Right.
    // Это автоматически "отрезает" удаленные поддеревья от текущего узла.
    root.Left = removeLeafNodes(root.Left, target)
    root.Right = removeLeafNodes(root.Right, target)

    // Теперь, когда дети уже обработаны (и, возможно, удалены),
    // проверяем текущий узел.
    // Если у него не осталось детей (оба указателя стали nil) 
    // И его значение равно target, значит, он стал целевым листом.
    if root.Left == nil && root.Right == nil && root.Val == target {
        // Удаляем текущий узел, возвращая nil его родителю.
        return nil
    }

    // Если условие удаления не выполнилось, узел остается в дереве.
    return root
}