package main

/*
Вам дан целочисленный массив coin, представляющий монеты разного номинала, и целочисленная сумма, представляющая общую сумму денег.
Возвращает количество комбинаций, составляющих эту сумму. Если эта сумма денег не может быть составлена ни одной комбинацией монет, верните 0.
Вы можете предположить, что у вас есть бесконечное количество монет каждого вида.
Ответ гарантированно умещается в 32-битное целое число со знаком.

Пример 1:
Input: amount = 5, coins = [1,2,5]
Output: 4
Пояснение: существует четыре способа пополнения суммы:
5=5
5=2+2+1
5=2+1+1+1
5=1+1+1+1+1

Пример 2:
Input: amount = 3, coins = [2]
Output: 0
Пояснение: сумму 3 невозможно составить только монетами номиналом 2.

Пример 3:
Input: amount = 10, coins = [10]
Output: 1

Ограничения:
1 <= coins.length <= 300
1 <= coins[i] <= 5000
Все номиналы монет уникальны.
0 <= amount <= 5000

*/

func change(amount int, coins []int) int {
    // dp[i] — количество способов собрать сумму i
    dp := make([]int, amount+1)
    
    // Базовый случай: есть 1 способ собрать сумму 0 (не брать монеты)
    dp[0] = 1

    // Проходим по каждой монете (в обратном порядке)
    for i := len(coins) - 1; i >= 0; i-- {
        // Обновляем количество способов для всех сумм от 1 до amount
        for a := 1; a <= amount; a++ {
            // Если текущую сумму можно дополнить монетой coins[i]
            if a-coins[i] >= 0 {
                // Добавляем способы собрать сумму (a - coins[i])
                dp[a] += dp[a - coins[i]]
            }
        }
    }

    // Возвращаем количество способов собрать целевую сумму
    return dp[amount]
}
