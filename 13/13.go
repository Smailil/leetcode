package main

/*
Римские числа представлены семью различными символами:
I, V, X, L, C, D и M.

Символ       Значение
I             1
V             5
X             10
L             50
C             100
D             500
M             1000

Например, число 2 записывается римскими цифрами как II —
это две единицы, поставленные рядом.
Число 12 записывается как XII, то есть X + II.
Число 27 записывается как XXVII, то есть XX + V + II.

Обычно римские числа записываются слева направо
от большего значения к меньшему.
Однако число четыре записывается не как IIII, а как IV.
Поскольку I стоит перед V, единица вычитается из пятёрки,
что даёт четыре.
Тот же принцип применяется к числу девять, которое записывается как IX.
Существует шесть случаев, в которых используется вычитание:
I можно поставить перед V (5) и X (10), чтобы получить 4 и 9.
X можно поставить перед L (50) и C (100), чтобы получить 40 и 90.
C можно поставить перед D (500) и M (1000), чтобы получить 400 и 900.

Преобразуйте данное римское число в целое число.

Пример 1:
Ввод: s = "III"
Вывод: 3
Пояснение: III = 3.

Пример 2:
Ввод: s = "LVIII"
Вывод: 58
Пояснение: L = 50, V = 5, III = 3.

Пример 3:
Ввод: s = "MCMXCIV"
Вывод: 1994
Пояснение: M = 1000, CM = 900, XC = 90 и IV = 4.

Ограничения:
1 <= s.length <= 15
Строка s содержит только символы ('I', 'V', 'X', 'L', 'C', 'D', 'M').
Гарантируется, что s является корректным римским числом
в диапазоне [1, 3999].
*/

// romanToInt обрабатывает строку слева направо и учитывает вычитающие пары
// с помощью просмотра следующего символа. Время работы — O(len(s)),
// а дополнительная память — O(1), поскольку таблица содержит семь записей.
func romanToInt(s string) int {
    // Таблица переводит каждый допустимый римский символ в его значение.
    roman := map[byte]int{
        'I': 1, 'V': 5, 'X': 10,
        'L': 50, 'C': 100, 'D': 500, 'M': 1000,
    }

    // После каждой итерации res хранит сумму вкладов уже обработанного
    // префикса строки с учётом правила вычитания.
    res := 0
    for i := 0; i < len(s); i++ {
        // Проверка границы защищает обращение к следующему символу.
        // Если текущий символ меньше следующего, он начинает пару вроде IV:
        // вычитаем его сейчас, а следующий символ добавится на своей итерации.
        if i+1 < len(s) && roman[s[i]] < roman[s[i+1]] {
            res -= roman[s[i]]
        } else {
            // Обычный символ, включая последний, увеличивает результат.
            res += roman[s[i]]
        }
    }
    return res
}
