package main

import (
	"container/heap"
	"sort"
)

/*
Даны n задач с метками от 0 до n - 1, представленные двумерным целочисленным массивом tasks,
где tasks[i] = [enqueueTimei, processingTimei] означает, что i-я задача станет доступна
для обработки в момент enqueueTimei, а на её выполнение потребуется processingTimei.

У вас есть однопоточный CPU, который может обрабатывать не более одной задачи одновременно
и работает следующим образом:

Если CPU простаивает и нет доступных для обработки задач, CPU продолжает простаивать.
Если CPU простаивает и есть доступные задачи, он выберет задачу с наименьшим временем обработки.
Если несколько задач имеют одинаковое минимальное время обработки, будет выбрана задача
с наименьшим индексом.
После запуска задачи CPU обработает её целиком без остановки.
CPU может завершить задачу и мгновенно начать новую.
Верните порядок, в котором CPU будет обрабатывать задачи.

Пример 1:
Вход: tasks = [[1,2],[2,4],[3,2],[4,1]]
Выход: [0,2,3,1]
Пояснение: события происходят следующим образом:
- В момент времени = 1 задача 0 доступна для обработки. Доступные задачи = {0}.
- Также в момент времени = 1 простаивающий CPU начинает обрабатывать задачу 0. Доступные задачи = {}.
- В момент времени = 2 задача 1 доступна для обработки. Доступные задачи = {1}.
- В момент времени = 3 задача 2 доступна для обработки. Доступные задачи = {1, 2}.
- Также в момент времени = 3 CPU завершает задачу 0 и начинает обрабатывать задачу 2,
  поскольку она самая короткая. Доступные задачи = {1}.
- В момент времени = 4 задача 3 доступна для обработки. Доступные задачи = {1, 3}.
- В момент времени = 5 CPU завершает задачу 2 и начинает обрабатывать задачу 3,
  поскольку она самая короткая. Доступные задачи = {1}.
- В момент времени = 6 CPU завершает задачу 3 и начинает обрабатывать задачу 1. Доступные задачи = {}.
- В момент времени = 10 CPU завершает задачу 1 и становится простаивающим.

Пример 2:
Вход: tasks = [[7,10],[7,12],[7,5],[7,4],[7,2]]
Выход: [4,3,2,0,1]
Пояснение: события происходят следующим образом:
- В момент времени = 7 все задачи становятся доступными. Доступные задачи = {0,1,2,3,4}.
- Также в момент времени = 7 простаивающий CPU начинает обрабатывать задачу 4. Доступные задачи = {0,1,2,3}.
- В момент времени = 9 CPU завершает задачу 4 и начинает обрабатывать задачу 3. Доступные задачи = {0,1,2}.
- В момент времени = 13 CPU завершает задачу 3 и начинает обрабатывать задачу 2. Доступные задачи = {0,1}.
- В момент времени = 18 CPU завершает задачу 2 и начинает обрабатывать задачу 0. Доступные задачи = {1}.
- В момент времени = 28 CPU завершает задачу 0 и начинает обрабатывать задачу 1. Доступные задачи = {}.
- В момент времени = 40 CPU завершает задачу 1 и становится простаивающим.

Ограничения:
tasks.length == n
1 <= n <= 10^5
1 <= enqueueTimei, processingTimei <= 10^9

*/

func getOrder(tasks [][]int) []int {
	n := len(tasks)
	
	// Добавляем исходный индекс к каждой задаче, так как после сортировки порядок элементов изменится.
	// Теперь формат задачи: [enqueueTime, processingTime, originalIndex]
	for i := range n {
		tasks[i] = append(tasks[i], i)
	}
	
	// Сортируем задачи по времени их поступления (enqueueTime) в порядке возрастания
	sort.Slice(tasks, func(i, j int) bool {
		return tasks[i][0] < tasks[j][0]
	})

	// Инициализируем мин-кучу для хранения доступных в данный момент задач.
	// Куча будет хранить пары: [processingTime, originalIndex]
	minHeap := &minHeap2{}
	heap.Init(minHeap)

	res := make([]int, 0, n) // Срез для результата (порядок выполнения индексов)
	i := 0                   // Индекс для итерации по отсортированным задачам
	time := tasks[0][0]      // Текущее время системы (начинаем с момента поступления первой задачи)

	// Цикл работает, пока в куче есть незавершенные задачи или не все задачи просмотрены
	for minHeap.Len() > 0 || i < n {
		// Добавляем в кучу все задачи, которые уже поступили к текущему моменту времени
		for i < n && time >= tasks[i][0] {
			// В кучу кладем только время выполнения и оригинальный индекс
			heap.Push(minHeap, [2]int{tasks[i][1], tasks[i][2]})
			i++
		}
		
		// Если куча пуста, значит, процессор простаивает в ожидании новой задачи.
		// "Перематываем" системное время к моменту поступления следующей доступной задачи.
		if minHeap.Len() == 0 {
			time = tasks[i][0]
		} else {
			// Иначе извлекаем задачу с наивысшим приоритетом 
			// (минимальное время выполнения, а при равенстве — минимальный индекс)
			task := heap.Pop(minHeap).([2]int)
			time += task[0]              // Увеличиваем текущее время на время выполнения взятой задачи
			res = append(res, task[1])   // Добавляем индекс выполненной задачи в результат
		}
	}
	return res
}

// minHeap2 реализует интерфейс heap.Interface для мин-кучи (приоритетной очереди).
// Элементами кучи являются массивы [2]int, где [0] - время выполнения, [1] - исходный индекс.
type minHeap2 [][2]int

// Len возвращает количество элементов в куче (обязательный метод интерфейса sort.Interface).
func (h minHeap2) Len() int { return len(h) }

// Less определяет приоритет элементов (обязательный метод интерфейса sort.Interface).
// Сначала сравниваем по времени выполнения (короткие задачи приоритетнее).
// Если время выполнения одинаковое, сравниваем по исходному индексу (меньший индекс приоритетнее).
func (h minHeap2) Less(i, j int) bool {
	if h[i][0] != h[j][0] {
		return h[i][0] < h[j][0]
	}
	return h[i][1] < h[j][1]
}

// Swap меняет местами два элемента в куче (обязательный метод интерфейса sort.Interface).
func (h minHeap2) Swap(i, j int)       { h[i], h[j] = h[j], h[i] }

// Push добавляет новый элемент в конец среза. 
// Пакет container/heap сам вызовет "просеивание" вверх для восстановления свойств кучи.
func (h *minHeap2) Push(x any) { *h = append(*h, x.([2]int)) }

// Pop удаляет и возвращает последний элемент среза.
// Пакет container/heap предварительно переместит корневой (минимальный) элемент в конец 
// и "просеет" его вниз, чтобы сохранить свойства кучи перед удалением.
func (h *minHeap2) Pop() any {
	old := *h
	n := len(old)
	x := old[n-1]
	*h = old[0 : n-1] // Уменьшаем длину среза на 1
	return x
}