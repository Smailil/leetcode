package main

/*
Дан массив целых чисел nums и целое число k.
Верните общее количество подмассивов, сумма которых равна k.

Подмассив — это непрерывная непустая последовательность элементов массива.

Пример 1:
Вход: nums = [1,1,1], k = 2
Выход: 2

Пример 2:
Вход: nums = [1,2,3], k = 3
Выход: 2

Ограничения:
1 <= nums.length <= 2 * 10^4
-1000 <= nums[i] <= 1000
-10^7 <= k <= 10^7
*/

// Использует метод префиксных сумм и хэш-таблицу.
// Время работы: O(n), Память: O(n), где n — длина массива nums.
func subarraySum(nums []int, k int) int {
	// res: итоговый счётчик найденных подмассивов
	// curSum: текущая префиксная сумма (сумма элементов от начала массива до текущего индекса)
	res, curSum := 0, 0

	// prefixSums хранит частоту появления каждой префиксной суммы.
	// Инициализируем {0: 1}, чтобы корректно обрабатывать подмассивы,
	// начинающиеся с индекса 0 (когда curSum == k, diff будет равен 0).
	prefixSums := map[int]int{0: 1}

	for _, num := range nums {
		// Обновляем текущую префиксную сумму
		curSum += num

		// Ищем разницу: если ранее встречалась префиксная сумма, равная (curSum - k),
		// то подмассив между той позицией и текущим индексом имеет сумму ровно k.
		diff := curSum - k

		// Добавляем к результату количество таких предыдущих префиксных сумм.
		// Примечание: в Go чтение несуществующего ключа из map возвращает 0,
		// что безопасно и корректно для данной логики.
		res += prefixSums[diff]

		// Фиксируем текущую префиксную сумму в карте для будущих итераций.
		// Если ключ уже существует, его счётчик увеличивается на 1.
		prefixSums[curSum]++
	}

	return res
}