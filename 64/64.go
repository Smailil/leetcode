package main

/*
Дана сетка m x n, заполненная неотрицательными числами.
Найдите путь из верхнего левого угла в нижний правый,
который минимизирует сумму всех чисел на пути.

Примечание: в каждый момент времени можно двигаться только вниз или вправо.

Пример 1:
https://assets.leetcode.com/uploads/2020/11/05/minpath.jpg
Вход: grid = [[1,3,1],[1,5,1],[4,2,1]]
Выход: 7
Пояснение: путь 1 → 3 → 1 → 1 → 1 даёт минимальную сумму.

Пример 2:
Вход: grid = [[1,2,3],[4,5,6]]
Выход: 12

Ограничения:
m == grid.length
n == grid[i].length
1 <= m, n <= 200
0 <= grid[i][j] <= 200
*/

func minPathSum(grid [][]int) int {
    // Получаем размеры сетки
    ROWS, COLS := len(grid), len(grid[0])

    // Создаём одномерный массив DP размером COLS+1 для экономии памяти.
    // dp[c] будет хранить минимальную сумму пути от ячейки (r, c)
    // до правого нижнего угла (bottom-right).
    //
    // Индекс COLS — фиктивный, нужен чтобы избежать выхода за границы
    // при обращении к dp[c+1] для самой правой колонки.
    dp := make([]int, COLS+1)

    // Инициализируем все значения "бесконечностью" (условно большое число),
    // чтобы они не влияли на выбор минимума.
    for i := range dp {
        dp[i] = 1 << 30
    }

    // Базовый случай: ячейка "ниже" правого нижнего угла.
    // Когда мы обрабатываем правую нижнюю ячейку сетки (r=ROWS-1, c=COLS-1),
    // значение dp[c+1] (т.е. dp[COLS]) остаётся бесконечностью,
    // а dp[c] (т.е. dp[COLS-1]) = 0. Это значит: из последней ячейки
    // путь до финиша стоит 0 — мы уже на месте.
    dp[COLS-1] = 0

    // Обходим сетку снизу вверх (от последней строки к первой)
    for r := ROWS - 1; r >= 0; r-- {
        // Внутри каждой строки — справа налево
        for c := COLS - 1; c >= 0; c-- {
            // На момент вычисления:
            //   dp[c]     — значение из строки НИЖЕ (движение ВНИЗ ↓)
            //   dp[c+1]   — значение из текущей строки, но СПРАВА (движение ВПРАВО →),
            //               уже обновлённое на предыдущей итерации
            //
            // Берём минимум из двух возможных направлений и прибавляем
            // стоимость текущей ячейки.
            dp[c] = grid[r][c] + min(dp[c], dp[c+1])
        }
    }

    // dp[0] теперь содержит минимальную сумму пути от (0,0) до (ROWS-1, COLS-1)
    return dp[0]
}