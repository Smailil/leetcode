package main

/*
Вам дан массив цен, где prices[i] — это цена данной акции на i-й день.

Найдите максимальную прибыль, которую вы можете получить. 
Вы можете совершить столько транзакций, сколько захотите (т. е. купить одну и продать одну акцию несколько раз) со следующими ограничениями:
После того, как вы продадите свои акции, вы не сможете купить их на следующий день (т. е. время восстановления один день).
Примечание. Вы не можете совершать несколько транзакций одновременно (т. е. вы должны продать акции, прежде чем купить их снова)..

Пример 1:
Input: prices = [1,2,3,0,2]
Output: 3
Пояснение: transactions = [buy, sell, cooldown, buy, sell]

Пример 2:
Input: prices = [1]
Output: 0

Ограничения:
1 <= prices.length <= 5000
0 <= prices[i] <= 1000

*/

func maxProfit(prices []int) int {
    n := len(prices)
    
    // === ОПТИМИЗАЦИЯ ПАМЯТИ: O(1) вместо O(n) ===
    // Вместо полной DP-таблицы храним только нужные предыдущие состояния
    
    // dp1_buy  = dp[i+1][1] — макс. прибыль с дня i+1, если НЕТ акции (можно покупать)
    // dp1_sell = dp[i+1][0] — макс. прибыль с дня i+1, если ЕСТЬ акция (можно продавать)
    // dp2_buy  = dp[i+2][1] — макс. прибыль с дня i+2, если НЕТ акции (нужен для кулдауна)
    dp1_buy, dp1_sell := 0, 0
    dp2_buy := 0

    // Идём с конца к началу, чтобы знать будущие состояния
    for i := n - 1; i >= 0; i-- {
        
        // === РЕШЕНИЕ ДЛЯ ДНЯ i ===
        
        // Вариант 1: ПОКУПАЕМ сегодня
        //   - Тратим prices[i]
        //   - На день i+1 переходим в состояние "есть акция" (dp1_sell)
        // Вариант 2: НЕ ПОКУПАЕМ (кулдаун/пропуск)
        //   - Остаёмся в состоянии "нет акции" на день i+1 (dp1_buy)
        dp_buy := max(dp1_sell - prices[i], dp1_buy)
        
        // Вариант 1: ПРОДАЁМ сегодня
        //   - Получаем prices[i]
        //   - На день i+2 переходим в состояние "нет акции" (dp2_buy) из-за кулдауна
        // Вариант 2: НЕ ПРОДАЁМ (держим акцию)
        //   - Остаёмся в состоянии "есть акция" на день i+1 (dp1_sell)
        dp_sell := max(dp2_buy + prices[i], dp1_sell)
        
        // === СДВИГ ОКНА: готовим переменные для следующей итерации (день i-1) ===
        // После вычисления dp[i], старые значения становятся "предыдущими" для следующего шага
        dp2_buy, dp1_sell = dp1_buy, dp_sell  // dp[i+2] ← dp[i+1], dp[i+1] ← dp[i]
        dp1_buy = dp_buy                       // dp[i+1] ← dp[i]
    }

    // Возвращаем макс. прибыль с дня 0 в состоянии "можно покупать"
    // (мы начинаем без акции, поэтому состояние 1)
    return dp1_buy
}

