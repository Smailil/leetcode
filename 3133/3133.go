package main

/*
Даны два целых числа n и x.
Необходимо построить массив положительных целых чисел nums размера n,
в котором для каждого 0 <= i < n - 1 выполняется
nums[i + 1] > nums[i], а результат побитовой операции И
над всеми элементами nums равен x.

Верните минимально возможное значение nums[n - 1].

Пример 1:
Вход: n = 3, x = 4
Выход: 6
Пояснение:
Массив nums может иметь вид [4,5,6], а его последний элемент равен 6.

Пример 2:
Вход: n = 2, x = 7
Выход: 15
Пояснение:
Массив nums может иметь вид [7,15], а его последний элемент равен 15.

Ограничения:
1 <= n, x <= 10^8
*/

// minEnd строит минимальный возможный последний элемент массива.
// Все единичные биты x обязаны присутствовать в каждом элементе, иначе
// побитовое И всего массива станет меньше x. Поэтому допустимые элементы
// получаются добавлением произвольных битов только в нулевые позиции x.
//
// Если последовательно подставлять числа 0, 1, 2, ... в свободные позиции x,
// получатся допустимые элементы в возрастающем порядке. Искомый n-й элемент
// соответствует числу n-1, биты которого нужно встроить в нулевые биты x.
//
// Время и дополнительная память: O(1), поскольку перебирается ограниченное
// количество битов целых чисел фиксированной разрядности.
func minEnd(n int, x int) int64 {
    // Начинаем с x, чтобы сохранить все обязательные единичные биты.
    // В дальнейшем res будет только дополняться битами через операцию OR.
    res := int64(x)

    // iX указывает текущую позицию в x и результате, а iN — текущий бит
    // числа n-1, который предстоит перенести в очередную свободную позицию x.
    var iX int64 = 1
    var iN int64 = 1
    nMinus1 := int64(n - 1)

    // Обрабатываем все значимые биты n-1. Для n == 1 значение n-1 равно нулю,
    // цикл не выполняется и минимальным последним элементом остаётся сам x.
    for iN <= nMinus1 {
        // Единичные позиции x уже заняты обязательными битами. Использовать
        // для кодирования n-1 можно только позиции, где бит x равен нулю.
        if iX&int64(x) == 0 {
            // Переносим текущий бит n-1 в найденную свободную позицию.
            // Нулевой бит ничего не меняет, единичный устанавливается через OR.
            if iN&nMinus1 != 0 {
                res |= iX
            }

            // Бит n-1 считается обработанным только после того, как для него
            // найдена свободная позиция в x.
            iN <<= 1
        }

        // Переходим к следующей позиции результата независимо от того,
        // была ли текущая позиция x свободна.
        iX <<= 1
    }

    return res
}
