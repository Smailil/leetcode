package main

/*
Задача о n ферзях состоит в том, чтобы разместить n ферзей на шахматной доске n x n так,
чтобы никакие два ферзя не атаковали друг друга.

Дан целочисленный параметр n.
Верните количество различных решений задачи о n ферзях.

Пример 1:
https://assets.leetcode.com/uploads/2020/11/13/queens.jpg
Вход: n = 4
Выход: 2
Пояснение: существует два различных решения задачи о 4 ферзях, как показано на изображении.

Пример 2:
Вход: n = 1
Выход: 1

Ограничения:
1 <= n <= 9

*/

func totalNQueens(n int) int {
    // Для быстрой проверки угроз (за O(1)) используем хеш-карты вместо двумерного массива.
    // Математика диагоналей:
    // - Сумма (r + c) одинакова для всех клеток на одной "положительной" диагонали (↘).
    // - Разность (r - c) одинакова для всех клеток на одной "отрицательной" диагонали (↙).
    col := make(map[int]bool)     // Занятые вертикали (колонки)
    posDiag := make(map[int]bool) // Занятые диагонали вида ↘ (top-left to bottom-right)
    negDiag := make(map[int]bool) // Занятые диагонали вида ↙ (top-right to bottom-left)
    
    res := 0 // Счетчик найденных валидных решений

    // Рекурсивная функция для перебора вариантов с возвратом (backtracking).
    // Принимает номер текущей строки r, в которую мы пытаемся поставить ферзя.
    var backtrack func(r int)
    backtrack = func(r int) {
        // Базовый случай: если мы успешно дошли до строки n, 
        // значит все n ферзей расставлены и не бьют друг друга.
        if r == n {
            res++ // Засчитываем найденную расстановку
            return
        }

        // Пробуем поставить ферзя в каждую колонку c текущей строки r
        for c := range n {
            // Если колонка или одна из пересекающих диагоналей уже занята другим ферзем,
            // ставить сюда нельзя — переходим к следующей колонке.
            if col[c] || posDiag[r+c] || negDiag[r-c] {
                continue
            }

            // Ставим ферзя: помечаем колонку и обе диагонали как занятые
            col[c] = true
            posDiag[r+c] = true
            negDiag[r-c] = true

            // Рекурсивно пытаемся расставить ферзей в следующей строке (r + 1)
            backtrack(r + 1)

            // Откатываемся назад (backtrack): убираем ферзя, чтобы освободить 
            // колонку и диагонали для проверки других вариантов в цикле.
            delete(col, c)
            delete(posDiag, r+c)
            delete(negDiag, r-c)
        }
    }

    // Запускаем поиск с самой верхней строки (индекс 0)
    backtrack(0)
    
    return res
}