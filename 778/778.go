package main

/*
Вам дана сетка целочисленной матрицы размера n x n, где каждое значение grid[i][j] представляет высоту в этой точке (i, j).

Начинается дождь, и со временем вода постепенно поднимается. В момент времени t уровень воды равен t, 
что означает, что любая ячейка с высотой менее t находится под водой или достижима.
Вы можете переплыть из квадрата в другой соседний в 4-х направлениях квадрат тогда и только тогда, когда высота обоих квадратов по отдельности не превышает t. 
Вы можете проплыть бесконечные расстояния за нулевое время. Конечно, во время плавания вы должны оставаться в пределах сетки.

Возвращайте минимальное время, пока вы не достигнете нижнего правого квадрата (n -1, n -1), если вы начнете с верхнего левого квадрата (0, 0).

Пример 1:
https://assets.leetcode.com/uploads/2021/06/29/swim1-grid.jpg
Input: grid = [[0,2],[1,3]]
Output: 3
Объяснение:
В момент времени 0 вы находитесь в координатной сетке (0, 0).
Вы не можете пойти куда-либо еще, потому что соседние в 4-х направлениях соседи имеют высоту выше, чем t = 0.
Вы не сможете достичь точки (1, 1) до момента 3.
Когда глубина воды равна 3, мы можем плыть в любом месте сетки.

Пример 2:
https://assets.leetcode.com/uploads/2021/06/29/swim2-grid-1.jpg
Input: grid = [[0,1,2,3,4],[24,23,22,21,5],[12,13,14,15,16],[11,17,18,19,20],[10,9,8,7,6]]
Output: 16
Пояснение: Показан конечный маршрут.
Нам нужно дождаться момента 16, чтобы (0, 0) и (4, 4) соединились.

Ограничения:
n == grid.length
n == grid[i].length
1 <= n <= 50
0 <= grid[i][j] < n2
Каждое значение grid[i][j] уникально.

*/

import (
	"container/heap"
)

// Структура для элемента кучи
type Node struct {
	t int // время/максимальная высота
	r int // строка
	c int // столбец
}

// Реализация интерфейса heap.Interface для MinHeap
type MinHeap []*Node

func (h MinHeap) Len() int           { return len(h) }
func (h MinHeap) Less(i, j int) bool { return h[i].t < h[j].t } // Мин-куча по времени
func (h MinHeap) Swap(i, j int)      { h[i], h[j] = h[j], h[i] }

func (h *MinHeap) Push(x any) {
	*h = append(*h, x.(*Node))
}

func (h *MinHeap) Pop() any {
	old := *h
	n := len(old)
	x := old[n-1]
	*h = old[0 : n-1]
	return x
}

func swimInWater(grid [][]int) int {
	N := len(grid)
	
	// Массив посещенных клеток
	visited := make([][]bool, N)
	for i := range visited {
		visited[i] = make([]bool, N)
	}

	// Инициализация кучи
	h := &MinHeap{}
	heap.Init(h)
	
	// Добавляем стартовую точку
	heap.Push(h, &Node{grid[0][0], 0, 0})
	visited[0][0] = true

	// Направления: вправо, влево, вниз, вверх
	directions := [][2]int{{0, 1}, {0, -1}, {1, 0}, {-1, 0}}

	for h.Len() > 0 {
		node := heap.Pop(h).(*Node)
		t, r, c := node.t, node.r, node.c

		// Если достигли правого нижнего угла
		if r == N-1 && c == N-1 {
			return t
		}

		// Перебор соседей
		for _, d := range directions {
			nr, nc := r+d[0], c+d[1]

			// Проверка границ и посещенности
			if nr >= 0 && nr < N && nc >= 0 && nc < N && !visited[nr][nc] {
				visited[nr][nc] = true
				// Время перехода равно максимуму из текущего времени и высоты новой клетки
				heap.Push(h, &Node{max(t, grid[nr][nc]), nr, nc})
			}
		}
	}

	return 0
}
