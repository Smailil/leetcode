package main

/*
Дан двумерный целочисленный массив matrix.
Верните транспонированную матрицу matrix.

Транспонированная матрица получается отражением исходной матрицы
относительно главной диагонали, при котором индексы строк и столбцов
меняются местами.
https://assets.leetcode.com/uploads/2021/02/10/hint_transpose.png

Пример 1:
Ввод: matrix = [[1,2,3],[4,5,6],[7,8,9]]
Вывод: [[1,4,7],[2,5,8],[3,6,9]]

Пример 2:
Ввод: matrix = [[1,2,3],[4,5,6]]
Вывод: [[1,4],[2,5],[3,6]]

Ограничения:
m == matrix.length
n == matrix[i].length
1 <= m, n <= 1000
1 <= m * n <= 10^5
-10^9 <= matrix[i][j] <= 10^9
*/

// transpose создаёт новую матрицу, меняя местами строки и столбцы исходной.
// Время работы составляет O(ROWS * COLS), память результата —
// O(ROWS * COLS), а вспомогательная память — O(1).
func transpose(matrix [][]int) [][]int {
    // Ограничения гарантируют непустую прямоугольную матрицу,
    // поэтому длину первой строки можно читать без дополнительной проверки.
    ROWS, COLS := len(matrix), len(matrix[0])

    // Каждый столбец исходной матрицы становится строкой результата.
    // Поэтому внешнее измерение результата имеет размер COLS.
    res := make([][]int, COLS)
    for i := range res {
        // В новой строке должно быть по элементу из каждой исходной строки.
        res[i] = make([]int, ROWS)
    }

    // Посещаем каждую ячейку ровно один раз, чтобы ничего не потерять
    // и не дублировать при транспонировании прямоугольной матрицы.
    for r := range ROWS {
        for c := range COLS {
            // После транспонирования координаты (r, c) превращаются в (c, r).
            res[c][r] = matrix[r][c]
        }
    }

    return res
}
